<p>Некоторые основные анонсы с проходящей конференции Google IO 2021 (проходит 18-20 мая).</p>
<h3>Android 12 Beta</h3>
<p>Вышла первая бета-версия новой операционной системы Android 12, которая ожидается осенью.</p>
<img src="./pics/android12.png" alt="Android 12" />
<p>Как отмечают в Google, в этой версии 
произошли самые большие изменения в дизайны в истории развития Android. Был переосмыслен пользовательский опыт работы с системой. Результатом этого переосмысления 
стал новый и единый язык дизайна - <span class="b">Material You</span>.</p>
<img src="./pics/materialyou.jpeg" alt="Material You in Android 12" style="width:800px; height:450px;" />
<p>Одним из изменений в Android 12 стали переработанные и обновленные старые виджеты. Кроме того, добавлены новые виджеты, такие как 
checkbox, switch, radiobutton. Также стало проще настраивать виджеты.</p> 
<p>Также добавлено новый API для работы с динамическими цветами для создания персонализованного и консистентного дизайна.</p>
<p>Также был добавлен <span class="b">Stretch overscroll</span> - системный эффект, который позволяет пользователям 
 узнать, что они завершили прокрутку доступного контента.</p>
 <p>Добавлен плавное затухание воспроизводимого аудио при потере аудио-приложением фокуса.</p>
<p>Отмечается, что в Android 12 повысилась производительность, так, процессорное время, необходимое для 
ключевых системных сервисов, снизилось на 22%, что позволяет устройствам работать быстрее. Также улучшилось потребление батареи.</p>
<p>В новой системе произвошли изменения в системе приватности, например, при использовании Bluetooth.</p>
<p>Для тестирования приложений под бету-версию Android 12 можно использовать эмуляторы. Также бета-версия уже доступна для некоторых устройств, например, для 
Pixel.</p>
<p>Подробнее про новвоведения в Android 12 можно найти тут: <a href="https://android-developers.googleblog.com/2021/05/whats-new-in-android-12-beta.html" rel="nofollow">https://android-developers.googleblog.com/2021/05/whats-new-in-android-12-beta.html</a>.</p>

<h3>Jetpack Compose</h3>
<p>На Google IO было объявлено, что релиз <span class="b">Jetpack Compose</span> (версия 1.0) ожидается в июле. 
<span class="b">Jetpack Compose</span> представляет современный тулкит для создания пользовательского интерфейса на языке Kotlin для устройств Android.</p>
<img src="./pics/android_compose2.jpg" alt="Jetpack Compose" />

<h3>Kotlin</h3>
<p>Как отмечают в Google, согласно последним опросам Kotlin стал наиболее используемым профессиональными разработчиками языком прогаммирования 
для создания приложений под Android. В частности, 1,2 миллиона приложений в Play Store используют Kotlin, в том числе, 80% из топ-1000. 
Что касается самого Google - более 70 приложений Google, таких как Drive, Home, Maps и Play используют Kotlin.</p>

<h3>Android Studio Arctic Fox</h3>
<p>Вышла бета-версия новой IDE для разработки Android-приложений - <span class="b">Android Studio Arctic Fox</span></p>
<img src="./pics/arctic1.gif" alt="Android Studio Arctic Fox" style="width:1000px; height:500px;" />
<p>Основные новведения и обновления:</p>
<ul>
<li><p>Compose Preview: возможность предварительного просмотра Compose UI</p></li>
<li><p>Compose Layout Inspector: позволяет мониторить структуру граического интерфейса приложения, написаного с помощью Compose</p></li>
<li><p>Deploy Preview to Device: позволяет развернуть на эмуляторе или реальном устройстве только часть приложения без необходимости 
полной установки приложения</p></li>
<li><p>Live Edit of literals: позволяет разработчикам, которые используют Compose, редактировать 
литералы (строки, числа, логические значения true и false) в коде и без перекомпиляции увидеть результат</p></li>
<li><p>Accessibility Scanner in Layout Editor: интегрирует фреймворк Android Accessibility Test Framework для отслеживания проблем, связанных с 
accessibility (доступность к функциям приложения)</p></li>
<li><p>Расширена поддержка для Wear OS и Google TV</p></li>
<li><p>Добавлена поддержка для MacOS на Apple Silicon (arm64) в режиме Preview</p></li>
<li><p>Добавлен Background Task Inspector, который позволяет визуализировать, мониторить и отлаживать работу фоновых воркеров с 
помощью библиотеки WorkManager версии 2.5.0и выше</p></li>
<li><p>Test Matrix: возможность параллельного тестирования на разных устройствах </p></li>
<li><p>Обновленный интерфейс для Memory Profiler</p></li>
</ul>
<p>Подробнее про новвоведения в Android Studio Arctic Fox можно посмотреть по ссылке <a href="https://android-developers.googleblog.com/2021/05/android-studio-arctic-fox-beta.html" rel="nofollow">https://android-developers.googleblog.com/2021/05/android-studio-arctic-fox-beta.html</a></p>

<h3>Android Jetpack</h3>
<p>Более 84% из топ-10000 приложений сейчас используют специальный набор библиотек - <span class="b">Android Jetpack</span>, в который были добавлены 
новые возможности. Например, был добавлен Jetpack Macrobenchmark (Alpha) для остлежвания производительности приложения при первом запуске. Также был добавлен новый API корутин Kotlin 
для более эффективного хранения данных с помощью Jetpack DataStore. Обновлен ряд уже существующих API.</p>
<p>Про новведения в Android Jetpack можно посмотреть здесь: <a href="https://android-developers.googleblog.com/2021/05/whats-new-in-jetpack.html" rel="nofollow">https://android-developers.googleblog.com/2021/05/whats-new-in-jetpack.html</a></p>
<h3>Dart 2.13</h3>
<p>Вышла новая подверсия языка Dart - <span class="b">Dart 2.13</span>, которая расширила поддержку для взаимодействия с нативым кодом, добавив поддержку 
массивов и упакванных структур в FFI. В Dart 2.13 также добавлена поддержка псевдонимов типов  для улучшения читабельности кода.</p>
<h3>Flutter 2.2</h3>
<p>Вышел новая подверсия тулкита для создания кроссплатформенных приложений - <span class="b">Flutter 2.2</span>. В этом релизе Google в основном сконцентрировался 
на улучшению уже существующих возможностей Flutter 2.</p>
<p>Из новвоведений стоит отметить, что <span class="b">null safety</span> теперь используется по умолчанию для всех проектов.</p>
<p>Также улучшена производительность создаваемых веб-приложений. Так, с помощью service workers они позволяют использовать фоновое кэширование.</p> 
<p>Для приложений на Android Flutter стал поддерживать "отложенные компоненты" (deferred components)<p/>
<p>Для приложений на iOS разработаны инструменты для предкомпиляции шейдеров, чтобы уменьшить время при первом запуске.</p>
<p>В инструменты разработки (DevTools) были добавлены новые возможности для отслеживания потребляемой памяти, а также добавлена поддержка для сторонних расширений.</p>
<p>Также в был обновлен SDK для встраивания рекламы и представлен новый плагин для платежей в приложениях.</p>
<p>Кроме того, в сотрудничестве с компанией Microsoft на этой неделе увидела свет альфа-версия поддержки Flutter для поддержки приложений UWP, создаваемых для Windows 10.</p>

<h3>Angular DevTools</h3>
<p>Для разработчиков, которые используют Angular, Google выпустил специальное расширение для браузера Chrome - <span class="b">Angular DevTools</span>. 
Оно доступно через магазин расширений Chrome Web Store. <span class="b">Angular DevTools</span> позволяет мониторить структуру приложения Angular и 
проверять его производительность</p>
<img src="./pics/angulardevtools.png" alt="Angular DevTools" />
<p>Angular DevTools поддерживает приложения, которые используют версию Angular v9 и выше и которые применяют движок Ivy. Более наглядно можно посмотреть на видео:</p>
<p>Подробнее про анонс <span class="b">Angular DevTools</span> можно прочитать здесь: <a href="https://blog.angular.io/introducing-angular-devtools-2d59ff4cf62f" rel="nofollow">https://blog.angular.io/introducing-angular-devtools-2d59ff4cf62f</a></p>

