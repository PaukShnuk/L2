<p>Вчера (9 апреля) была анонсирована новая превью-версия фреймворка .NET MAUI (.NET Multi-platform App UI) -  Preview 3. 
Напомню, что фреймворк .NET MAUI предназначен для кроссплатформенной разработки мобильных и десктопных приложений, который позволяет создать один проект для всех поддерживаемых платформ и который 
ожидает своего окончательного выхода вместе с .NET 6 в ноябре 2021.</p>
<p>Что добавляет данная превью-версия? Некоторые добавления и улучшения.</p> 
<ul>
<li><p>Добавлена поддержка платформы Windows с WinUI 3. Теперь ОС Windows присоединилась к Android, iOS и macOS в качестве целевых платформ, 
для которых можно создавать приложения с помощью .NET MAUI.</p></li>
<li><p>Добавлена утилита <span class="b">.NET MAUI Check Tool</span>, которая позволяет проверить наличие всех необходимых инструментов, 
требуемых для разработки на .NET MAUI.</p>
<p>Для установки этой утилиты необходимо выполнить команду</p>
<div class="console"><pre class="consoletext">dotnet tool install -g redth.net.maui.check</pre></div>
<p>Для запуска утилиты предназначена команда</p>
<div class="console"><pre class="consoletext">maui-check --dev</pre></div>
<p>В частности, на данный момент она проверяет наличие следующих инструментов:</p>
<ul>
<li><p>OpenJDK / Android SDK</p></li>
<li><p>.NET 6 Preview SDK</p></li>
<li><p>Пакеты для .NET MAUI / iOS / Android</p></li>
<li><p>Шаблоны .NET MAUI</p></li>
<li><p>Файлы Workload Resolver .sentinel для dotnet и Visual Studio Windows/Mac</p></li>
<li><p>Необходимые пакеты для WinUI3</p></li>
</ul>
</li>

<li><p>Теперь приложение .NET MAUI для запуска применяет Microsoft.Extensions HostBuilder (наподобие приложения ASP NET Core). 
А точка входа в приложение MAUI располагается в файле <span class="b">Startup.cs</span>, с которого начинается его выполнение:</p>
<pre class="brush:c#;">
public class Startup : IStartup
{
    public void Configure(IAppHostBuilder appBuilder)
    {
        appBuilder
            .UseMauiApp&lt;App&gt;();
    }
}
</pre>
<p>В данном случае устанавливает применение класса <span class="b">App</span> - производного класса от 
<span class="b">Application</span>, который отвечает за создание нового окна - объекта <span class="b">IWindow</span>:</p>
<pre class="brush:c#;">
public partial class App : Application
{
    public override IWindow CreateWindow(IActivationState activationState)
    {
        return new MainWindow();
    }
}
</pre>
<p>А в рамках окна IWindow разворачивается объект страницы - объект IPage, на котором собственно и располагаются все элементы управления:</p>
<pre class="brush:c#;">
public class MainWindow : IWindow
{
    public MainWindow()
    {
        Page = new MainPage();
    }

    public IPage Page { get; set; }

    public IMauiContext MauiContext { get; set; }
}
</pre>
</li>
<li><p>Добавлен класс <span class="b">ConfigureLifecycleEvents</span>, который позволяет 
задействовать события жизненного цикла приложения на конкретной платформе. Пример с обработкой нажатия на кнопку Назад на Android:</p>
<pre class="brush:c#;">
public class Startup : IStartup
{
    public void Configure(IAppHostBuilder appBuilder)
    {
        appBuilder
            .UseMauiApp&lt;App&gt;()
            .ConfigureLifecycleEvents(lifecycle =&gt; {
                #if ANDROID
                lifecycle.AddAndroid(d =&gt; {
                    d.OnBackPressed(activity =&gt; {
                        System.Diagnostics.Debug.WriteLine("Back button pressed!");
                    });
                });
                #endif
            });
    }
}
</pre></li>
</ul>
<p>Более подробно про добавления и изменения в .NET 6 MAUI Preview 3 можно найти в официальном блоге по адресу 
<a href="https://devblogs.microsoft.com/dotnet/announcing-net-multi-platform-app-ui-preview-3/" rel="nofollow">https://devblogs.microsoft.com/dotnet/announcing-net-multi-platform-app-ui-preview-3/</a>.</p>

