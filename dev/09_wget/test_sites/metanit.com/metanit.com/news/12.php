<p>Сегодня - 26 ноября 2020 года вышло крупное обновление языка программирования PHP - PHP 8.0.0. 
Напомню, что последний крупный релиз PHP - PHP 7 вышел аж 3 декабря 2015 году. Текущий релиз - PHP 8 содержит ряд улучшений и обновлений, 
среди которых именованные аргументы, тип union, атрибуты, паттерны сопоставления, упрощение определения конструктора, nullsafe-оператор, JIT, изменения в системе типов, обработке ошибок и многое другое.</p>
<p>Некоторые ключевые изменения в PHP8.</p>
<h3>Именованные аргументы</h3>
<p>Именованные аргументы могут передаваться в произвольном порядке и позволяют определять значения только для обязательных параметров. Например, код до PHP 8:</p>
<pre class="brush:php;">
htmlspecialchars($string, ENT_COMPAT | ENT_HTML401, 'UTF-8', false);
</pre>
<p>Код в PHP 8:</p>
<pre class="brush:php;">
htmlspecialchars($string, double_encode: false);
</pre>
<h3>Конструктор</h3>
<p>PHP 8 представляет упрощенный код конструктора, предназначенный для инициализации свойств. Например, код до PHP 8:</p>
<pre class="brush:php;">
class Point {
  public float $x;
  public float $y;
  public float $z;

  public function __construct(
    float $x = 0.0,
    float $y = 0.0,
    float $z = 0.0,
  ) {
    $this-&gt;x = $x;
    $this-&gt;y = $y;
    $this-&gt;z = $z;
  }
}
</pre>
<p>Код в PHP 8:</p>
<pre class="brush:php;">
class Point {
  public function __construct(
    public float $x = 0.0,
    public float $y = 0.0,
    public float $z = 0.0,
  ) {}
}
</pre>
<h3>Паттерны сопоставления</h3>
<p>Паттерны сопоставления позволяют упростить конструкцию <span class="b">switch</span> с помощью нового оператора <span class="b">match</span>. 
Например, код до PHP 8:</p>
<pre class="brush:php;">
switch (8.0) {
  case '8.0':
    $result = "строка";
    break;
  case 8.0:
    $result = "число";
    break;
}
echo $result;
// результат
// строка
</pre>
<p>Код в PHP 8:</p>
<pre class="brush:php;">
echo match (8.0) {
  '8.0' =&gt; "строка",
  8.0 =&gt; "число",
};
// результат
// число
</pre>
<h3>Оператор безопасного null</h3>
<p>Оператор безопасного null (или nullsafe-оператор) <span class="b">?-&gt;</span> позволяет упростить проверку на null. 
Например, код до PHP 8:</p>
<pre class="brush:php;">
$country =  null;

if ($session !== null) {
  $user = $session-&gt;user;

  if ($user !== null) {
    $address = $user-&gt;getAddress();
 
    if ($address !== null) {
      $country = $address-&gt;country;
    }
  }
}
</pre>
<p>Код в PHP 8:</p>
<pre class="brush:php;">
$country = $session?-&gt;user?-&gt;getAddress()?-&gt;country;
</pre>
<h3>JIT-компиляция</h3>
<p>PHP 8 вышел с двумя движками для JIT-компиляции. Движок Tracing JIT, наиболее многообещающий из этих двух, показывает более чем троекратное 
увеличение производительности по синтетическим бенчмаркам и 
увеличение производительности в 1,5–2 раза в некоторых долгоработающих приложенниях. При этом обычные операции в приложении остаюся на уровне PHP 7.4.</p>
<p>Более подробную информацию про все обновления в PHP 8.0 можно найти в официальном блоге по адресу <a href="https://www.php.net/releases/8.0/en.php" rel="nofollow">https://www.php.net/releases/8.0/en.php</a></p>

