<p>24 июня 2021 года Microsoft официально преставил новую версию Windows - <span class="b">Windows 11</span>. 
В новой ОС Microsoft несколько переосмыслила дизайн системы. Некоторые основные изменения.</p>
<h3>Кнопка Пуск и панель задач</h3>
<p>Так, кнопка Пуск помещена в центр экрана, что, по мысли MS, позволит проще и быстрее найти то, что нужно.</p>
<img src="./pics/windows11_1.png" alt="Windows 11 release Start menu" />
<p>Причем меню Пуск использует облачные сервисы и Microsoft 365 для отображения последних файлов, с которыми работал пользователь вне зависимости от 
операционной системы (даже если он работал с файлами на Android или iOS).</p>
<h3>Кастомизация расположения окон</h3>
<p>В Windows 11 появились новые способы расположения окон: Snap Layouts, Snap Groups и Desktops, которые позволяют 
проще работать с несколькими приложениями и оптимизировать расположение окон на экране.</p>
<img src="./pics/windows11_2.png" alt="Snap Layouts, Snap Groups и Desktops в Windows 11" />
<h3>Microsoft Teams</h3>
<p>В Windows 11 по умолчанию интегрировано в панель задач приложение <span class="b">Microsoft Teams</span>, которое по сути представляет мессенджер и позволяет 
обмениваться текстовыми, голосовыми и видео-сообщениями.</p>
<img src="./pics/windows11_3.png" alt="Microsoft Teams в Windows 11" />
<h3>Персонализированная лента новостей Widgets</h3>
<p>В Windows 11 появилась новая персонализированная лента новостей - <span class="b">Widgets</span>, которая использует AI и Microsoft Edge.</p>
<img src="./pics/windows11_4.png" alt="Widgets в Windows 11" />
<h3>Обновленный Microsoft Store</h3>
<p>Обновлен магазин приложений Microsoft Store. Как отмечают в Microsoft, он стал быстрее и проще в использовании. Причем, как отмечается, 
Microsoft Store будет открыт для самых разных приложений вне зависимости от их технологии, будь то Win32, Progressive Web App (PWA) или 
Universal Windows App (UWP) или приложения, которые используют другие фреймворки.</p>
<h3>Android-приложения на Windows</h3>
<p>Windows 11 позволит запускать Android-приложения. Позже в этом году у пользователей появится возможность поиска Android-приложений в 
Microsoft Store и их загрузки через Amazon Appstore благодаря технологии Intel Bridge.</p>
<h3>Обновление до Windows 11</h3>
<p>Обновление до Windows 11 будет доступно бесплатно для всех совместимых ПК с Windows 10. Для проверки, совместим ли компьютер 
с Windows 11, необходимо загрузить приложение <a href="https://aka.ms/GetPCHealthCheckApp" rel="nofollow">PC Health Check</a>.</p>
<p>Базовые минимальные требования к аппаратным ресурсам:</p>
<ul>
<li><p><span class="b">Процессор</span>: 1 ГГц и выше, два и более ядра, совместимый 64-битный процессор или  System on a Chip (SoC)</p></li>
<li><p><span class="b">Оперативная память</span>: 4 ГБ</p></li>
<li><p><span class="b">Жесткий диск</span>: 64 ГБ и выше</p></li>
<li><p><span class="b">Видеокарта</span>: совестимая с DirectX 12 / WDDM 2.x</p></li>
</ul>
<p>Обновление для всех совместимых ПК с Windows 10 будет доступно 
позже. А пока со следующей недели через программу Windows Insider будут доступны для тестирования ранние билды Windows 11.</p>
<p>На новых устройствах Windows 11 появится в продаже позже в этом году.</p>
<p>Вкратце про нововведения в новой ОС можно посмотреть в небольшом ролике</p>
<iframe width="560" height="315" src="https://www.youtube.com/embed/Uh9643c2P6k" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
<p>Более подробно про релиз Windows 11 можно прочитать в анонсе Microsoft на странице: <a href="https://blogs.windows.com/windowsexperience/2021/06/24/introducing-windows-11/" rel="nofollow">https://blogs.windows.com/windowsexperience/2021/06/24/introducing-windows-11/</a></p>
<h3>Новые возможности для разработчиков</h3>
<p>Новый инструмент <span class="b">PWABuilder3</span> позволяет создавать PWA-приложения из веб-приложений.</p>
<p>В Windows 11 включена среда выполнения <span class="b">WebView2</span>, которая упрощает создание гибридных веб-приложений.</p>
<p>Тулкит <span class="b">Project Reunion</span>, который упрощает интеграцию 
возможностей Windows 11 в разрабатываемое приложение, был переименован в <span class="b">Windows App SDK</span> и вышел в стабильный релиз - 
Windows App SDK 0.8 Stable (в NuGet и Visual Studio Marketplace по прежнему именуется как "Project Reunion"). 
В этом релизе имеются обновления для WinUI3 и поддержка для Visual Studio 16.10. Версия <span class="b">Windows App SDK 1.0</span> выйдет 
позже в этом году.</p>
<p>Для разработчиков игр стал доступен новый инструмент - <span class="b">Game Development Kit (GDK)</span>. Его можно найти на github: 
<a href="https://github.com/microsoft/gdk" rel="nofollow">https://github.com/microsoft/gdk</a>. 
GDK содержит базовые инструменты, библиотеки и документацию, необходимую для создания игр.</p>


