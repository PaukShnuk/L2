<p>Некоторые основные анонсы с проходящей конференции Microsoft Build 2021 (проходит 25-27 мая).</p>
<h3>.NET 6 Preview 4</h3>
<p>Вышла новая preview-версия .NET 6 - .NET 6 Preview 4, полноценный релиз которого ожидается в ноябре. Какие изменения произошли в новой версии?</p>
<ul>
<li><p>Функциональность перезагрузки приложения при изменения кода <span class="b">Hot Reload</span> теперь работает для многих типов приложений, 
в частности, для приложений WPF, Windows Forms, WinUI, ASP.NET, консольных приложений, а также поддерживается для других фреймворков, которые работают 
поверх CoreCLR. В настоящее время идет работа по добавления поддержики для приложений под WebAssembly, iOS и Android, которые работают поверх среды Mono.</p>
</li><li><p>Также добавлена поддержка серилизации с помощью System.Text.Json для типа <span class="b">IAsyncEnumerable</span>:</p>
<pre class="brush:c#;">
using System;
using System.Collections.Generic;
using System.IO;
using System.Text.Json;

static async IAsyncEnumerable&lt;int&gt; PrintNumbers(int n)
{
    for (int i = 0; i &lt; n; i++) yield return i;
}

using Stream stream = Console.OpenStandardOutput();
var data = new { Data = PrintNumbers(3) };
await JsonSerializer.SerializeAsync(stream, data); // выводит {"Data":[0,1,2]}
</pre></li>
<li><p>В <code>System.Text.Json</code> добавлен новый API для работы с json-объектами:</p>
<pre class="brush:c#;">
// Парсинг объекта JSON
    JsonNode jNode = JsonNode.Parse("{"MyProperty":42}");
    int value = (int)jNode["MyProperty"];
    Debug.Assert(value == 42);
    // или
    value = jNode["MyProperty"].GetValue&lt;int&gt;();
    Debug.Assert(value == 42);

    // Парсинг массива в JSON
    jNode = JsonNode.Parse("[10,11,12]");
    value = (int)jNode[1];
    Debug.Assert(value == 11);
    // или так
    value = jNode[1].GetValue&lt;int&gt;();
    Debug.Assert(value == 11);

    // Создаем новый объект JsonObject
    var jObject = new JsonObject
    {
        ["MyChildObject"] = new JsonObject
        {
            ["MyProperty"] = "Hello",
            ["MyArray"] = new JsonArray(10, 11, 12)
        }
    };

    // получаем JSON из объекта JsonObject
    string json = jObject.ToJsonString();
    Console.WriteLine(json); // {"MyChildObject":{"MyProperty":"Hello","MyArray":[10,11,12]}}
</pre></li>
<li><p>Некоторые добавления в <code>System.Linq</code>. Так, для типа <code>Enumerable</code> добавлена поддержка параметров типа <span class="b">Range</span> и <span class="b">Index</span>. Например, 
метод <code>Enumerable.ElementAt</code> позволяет получить элемент по индексу с конца последовательности</p>
<pre class="brush:c#;">Enumerable.Range(1, 10).ElementAt(^2); // возвращает 9</pre>

<p>Метод <code>Enumerable.Take()</code> теперь принимает параметр типа Range:</p>
<pre class="brush:c#;">
source.Take(..3) вместо source.Take(3)
source.Take(3..) вместо source.Skip(3)
source.Take(2..7) вместо source.Take(7).Skip(2)
source.Take(^3..) вместо source.TakeLast(3)
source.Take(..^3) вместо source.SkipLast(3)
source.Take(^7..^3) вместо source.TakeLast(7).SkipLast(3).
</pre>
<p>Также добавлен ряд новых методов: <code>DistinctBy/UnionBy/IntersectBy/ExceptBy/MaxBy/MinBy</code></p>
<p>Методы <code>FirstOrDefault/LastOrDefault/SingleOrDefault</code> теперь поддерживают установку значения по умолчанию, которое возвращается, если коллекция пуста:</p>
<pre class="brush:c#;">Enumerable.Empty&lt;int&gt;().SingleOrDefault(-1); // возвращает -1</pre></li>
<li><p>Переписан тип <span class="b">FileStream</span>, благодаря чему повысилась его производительность</p></li>
<li><p>Некоторые изменения при работе с датами и временем. В частности, добавлены новые структуры <span class="b">DateOnly</span> и <span class="b">TimeOnly</span> 
для представления только даты и только времени соответственно</p></li>
<li><p>Улучшена работа компилятора RyuJIT</p></li>
<li><p>Улучшения при публикации однофайловых приложений, в частности, добавлена поддержка сжатия.</p></li>
</ul>
<p>Подробнее про анонс <span class="b">.NET 6 Preview 4</span> можно прочитать здесь: 
<a href="https://devblogs.microsoft.com/dotnet/announcing-net-6-preview-4/" rel="nofollow">https://devblogs.microsoft.com/dotnet/announcing-net-6-preview-4/</a></p>
<h3>ASP.NET Core</h3>
<p>Основные изменения для ASP.NET Core в .NET 6 Preview 4</p>
<ul>
<li><p>Добавлена возможность для создания простейшего готового проекта с минимальным API.</p>
<img src="./pics/new-min-api.gif" alt="Min API in ASP.NET Core" /></li>
<li><p>Для метода <span class="b">Map()</spann> добавлены новые версии. Если раньше надо было писать так:</p>
<pre class="brush:c#;">
app.MapGet("/", async httpContext =&gt;
{
    await httpContext.Response.WriteAsync("Hello World!");
});
</pre>
<p>То теперь можно сократить определение метода:</p>
<pre class="brush:c#;">
app.MapGet("/", (Func&lt;string&gt;)(() =&gt; "Hello World!"));
</pre></li>
<li><p>Добавлен новый API для хостинга приложения:</p>
<pre class="brush:c#;">
var app = WebApplication.Create(args);

app.MapGet("/", (Func&lt;string&gt;)(() =&gt; "Hello World!"));

app.Run();
</pre>
</li>

<li><p>Добавлена поддержка асинхронных стримов:</p>
<pre class="brush:c#;">
public IActionResult GetProducts()
{
    return Ok(dbContext.Products);
}
</pre>
</li>

<li><p>Добавлен новый middleware для логгирования данных HTTP-запроса (заголовки, тело запроса, ответ на запрос):</p>
<pre class="brush:c#;">
public void Configure(IApplicationBuilder app, IWebHostEnvironment env)
{
    app.UseHttpLogging();
}
</pre>
<p>Этот middleware можно настроить в методе <code>ConfigureServices</code></p>
<pre class="brush:c#;">
public void ConfigureServices(IServiceCollection services)
{
    services.AppHttpLogging(logging =&gt;
    {
        logging.LoggingFields = HttpLoggingFields.All;
        logging.RequestHeaders.Add("My-Request-Header");
        logging.ResponseHeaders.Add("My-Response-Header");
        logging.MediaTypeOptions.AddText("application/javascript");
        logging.RequestBodyLogLimit = 4096;
        logging.ResponseBodyLogLimit = 4096;
    });
}
</pre>
</li>
<li><p>Для запуска приложений в Visual Studio теперь используется Kestrel вместо IIS</p></li>
<li><p>Улучшены шаблоны одностраничных приложений с Angular и React </p></li>
<li><p>Blazor WebAssembly теперь поддерживает AOT-компиляцию</p></li>
<li><p>Приложения .NET MAUI теперь могут использовать компоненты Blazor для создания интерфейса</p></li>
</ul>
<p>Про остальные новвоведения в ASP.NET Core в .NET 6 Preview 4 можно прочитать здесь: 
<a href="https://devblogs.microsoft.com/aspnet/asp-net-core-updates-in-net-6-preview-4/" rel="nofollow">https://devblogs.microsoft.com/aspnet/asp-net-core-updates-in-net-6-preview-4/</a></p>
<h3>.NET MAUI Preview 4</h3>
<p>Новый элемент <span class ="b">BlazorWebView</span> позволяет хостировать в приложении .NET MAUI компоненты Blazor:</p>
<pre class="brush:xml;">
&lt;BlazorWebView 
    HostPage="wwwroot/index.html"
    Services="{StaticResource Services}"&gt;
    &lt;BlazorWebView.RootComponent&gt;
        &lt;RootComponent 
            Selector="#app"
            ComponentType="{x:Type local:Main}"
        /&gt;
    &lt;/BlazorWebView.RootComponent&gt;
&lt;/BlazorWebView&gt;
</pre>
<p>Также добавлена поддержка единого начального экрана для мобильных приложений с помощью элемента <span class="b">MauiSplashScreen</span>.</p>
<p>Кроме того, упрощена работа с ресурсами приложения.</p>
<p>Подробнее про .NET MAUI Preview 4: 
<a href="https://devblogs.microsoft.com/dotnet/announcing-net-maui-preview-4/" rel="nofollow">https://devblogs.microsoft.com/dotnet/announcing-net-maui-preview-4/</a></p>
