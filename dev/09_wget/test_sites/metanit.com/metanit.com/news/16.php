<p>3 марта 2021 года компания Google анонсировала новую версию кроссплатформенного фреймворка Flutter - <span class="b">Flutter 2</span>. 
Напомню, что Flutter представляет кроссплатформенный фреймворк для создания приложений для разных платформ, который уже получил широкое распространение. 
По оценкам Google только в Play Store уже более 150 000 приложений на Flutter, в том числе такие популярные как WeChat, Grab, Yandex Go, Nubank, Sonos, Fastic, Betterment и realtor.com. 
Свыше 15 000 пакетов создано для Flutter и Dart в том числе такими компаиями, как Amazon, Microsoft, Adobe, Alibaba и eBay. Что принес данный релиз?</p>
<p>До недавнего времени Flutter 
полноценно поддерживал создание приложений для мобильных платформ - Android и iOS. И вот теперь с выходом Flutter 2 также добавлена поддержка для 
создания веб-приложений - возможность, которая на протяжении последнего года была в состоянии бета-версии.</p>
<img src="./pics/flutter2_1.png" alt="Релиз Flutter 2 Web" />
<h3>Web</h3>
<p>Flutter 2 предоставляет три сценария для создания веб-приложений:</p>
<ul>
<li><p>Так называемые прогрессивные веб-приложения (Progressive web apps или PWA), которые объединяют 
возможности обычных веб-приложений и досктопных приложений.</p></li>
<li><p>Одностраничные приложения (Single page apps или SPA)</p></li>
<li><p>Разширение существующих мобильных приложений Flutter на веб с использованием общей базы исходного кода.</p></li>
</ul>
<p>Как отмечают в Google, особое внимание было уделено производительности и улучшению рендеринга, и к стандартному HTML-рендереру был добавлен новый CanvasKit-рендерер. 
HTML-рендерер оптимизирован для создания приложений меньшего размера и широкой совместимости. В то время как CanvasKit-рендерер применяет WebAssembly 
и WebGL для рендернига команд Skia на элементе canvas в браузере. Соответственно у разработчиков есть выбор, какой именно рендерер использовать.</p>
<p>При этом также был добавлен ряд виджетов, которые применяются специально для веб-приложений, например, виджет Link.</p>
<img src="./pics/flutter2_2.png" alt="Релиз Flutter 2 Web CanvasKit renderer" />
<h3>Desktop</h3>
<p>Поддержка создания приложений для настольных систем Windows, MacOS, Linux по прежнему находится с состоянии превью, однако теперь разработчики получили 
возможность через стабильный канал опробовать в деле поддержку для Desktop.</p>
<p>Для добавления поддержки для разработки для десктопа, в консоли необходимо выполнить соответствующую команду для определенной ОС:</p>
<div class="console">
<pre class="consoletext">
 flutter config --enable-windows-desktop

 flutter config --enable-macos-desktop

 flutter config --enable-linux-desktop
</pre>
</div>
<p>Как сообщается, стабильный релиз для Desktopа выйдет позже в этом году.</p>
<h3>Google Mobile Ads Beta</h3>
<p>Также вышла бета-версия Google Mobile Ads - нового инструмента, который позволяет встраивать в приложения нативную рекламу в дополнение к уже существующим форматам.</p>
<img src="./pics/flutter2_3.png" alt="Google Mobile Ads for Flutter" />
<h3></h3>
<p>Кроме того, был добавлен еще ряд функциональностей, в частности, новые виджеты AutocompleteCore и ScaffoldMessenger, расширены возможности некоторых существующих виджетов, улучшены средства разработки и т.д.</p>
<p>Более подробно про анонс Flutter 2 можно прочитать в официальном блоге Google на странице
<a href="https://medium.com/flutter/whats-new-in-flutter-2-0-fe8e95ecc65" rel="nofollow">https://medium.com/flutter/whats-new-in-flutter-2-0-fe8e95ecc65</a>.</p>
<h3>Dart 2.12</h3>
<p>Вместе с Flutter вышла новая подверсия языка Dart - <span class="b">Dart 2.12</span>, основными новвоведениями которой стали:</p>
<ul>
<li><p>Sound null safety. Теперь все типы по умолчанию non-nullable (то есть не допускают значение null). Соответственно Flutter 2 тоже применяет null safety по умолчанию.</p>
<li><p>Подсистема Dart FFI теперь перешла в состояние релиза. Она позволяет задействовать код библиотек на языке C. То есть мы можем написать библиотеку на Си и вызывать 
ее, обащаться к ее коду из программы на Dart</p>
</ul>

