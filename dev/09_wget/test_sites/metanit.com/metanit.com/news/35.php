<p>Недавно компания Advanced Software выпустила отчет по касательно модернизации ИТ-инфраструктуры. 
Сам отчет доступен по ссылке <a href="https://modernsystems.oneadvanced.com/modernization2021/" rel="nofollow">https://modernsystems.oneadvanced.com/modernization2021/</a>
</p><p>Согласно отчету, 9 из 10 (89%) представителей крупного бизнеса испытывают беспокойство по поводу недостатка кадров с должной квалификацией и навыками для поддержании и управлении старыми (legacy) ИТ-системами. Особенно на фоне того, что старые кадры, обладающие должными знаниями, постепенно выходят на пенсию, а курсов обучения для новых кадров нет. 
</p><p>Многие подобные legacy-системы были созданы более несколько десятилетий назад и нередко базируются на таких языках программирования как COBOL или ассемблерах и до сих пор широко применяются в больших компаниях для обработки данных и ведения бизнес-процессов.
</p><p>В частности, 75% из 400 опрошенных организаций используют COBOL в качестве языка в своих мейнфремах. Дальше следуют ассемблер с долей 66%, ADS/Online (40%), CA Gen (37%), CA Telon (24%) и PL/1 (15%). В реальности типичный мейнфрейм часто использует комбинацию языков, например, CA Gen и CA Telon с Assembler или Natural и PL1. 
</p><p>К слову, система System 6 внутренней налоговой службы казначейства США, которая хранит данные налогоплательщиков, написана на ассемблере и COBOL. А ассемблеры в целом остаются достаточно востребованными в банковском секторе, поскольку в силу низкоуровневой природы позволяют выполнять операции оцень быстро.
</p><p>Тим Джонс (Tim Jones), управляющий директор в Advanced, в беседе с онлайн-изданием <a href="https://www.techrepublic.com/article/these-old-programming-languages-are-still-critical-to-big-companies-but-nobody-wants-to-learn-them/" rel="nofollow">https://www.techrepublic.com/</a> отмечает, что все сложнее найти разработчиков, которые работают с процедурными языками наподобие COBOL. По его словам, ситуация усугубляется из-за того, что большинство университетов больше не предоставляют курсы по поддержке мейнфремов, поскольку никто больше не хочет использовать процедурные языки типа COBOL для новых проектов. 
И некоторые компании вынуждены открывать внутренние курсы для подготовки подобных специалистов.</p><p>Тим Джонс также добавляет, что для некоторых может быть непонятно, почему предприятия до сих пор поддерживают подобные устаревшие системы, но причина проста: эти системы работают стабильно и надежно. А повсеместная цифровизация, необходимость не только поддержания, но и адаптации систем к новым условиям и запросам, особенно с условиях пандемии, только усугубила проблему.
</p>


