<p>Вместе с <span class="b">Visual Studio 2022 Preview 1</span> 17 июня вышла новая Preview-версия .NET 6 - <span class="b">.NET 6 Preview 5</span> c 
с обновлениями для ASP.NET Core, Entity Framework Core и .NET MAUI.</p> 
<p>Чтобы использовать .NET 6 Preview 5 c обновлениями ASP.NET Core, Entity Framework Core и .NET MAUI, можно установить новую среду 
<span class="b">Visual Studio 2022 Preview 1</span>, которая уже по умолчанию включает эти компоненты. Либо использовать 
Visual Studio 2019 version 16.11 Preview 2 и Visual Studio for Mac 8.9.</p>
<p>Рассмотрим вкратце некоторые основные изменения в .NET 6 Preview 5, ASP.NET Core, Entity Framework Core и .NET MAUI</p>
<h3>Что нового в .NET 6 Preview 5?</h3>
<ul>
<li><p>Добавленая новая функциональность - <span class="b">SDK workloads</span> (Рабочие нагрузки SDK), которая позволяет добавлять поддержку для новых типов приложений без увеличения размера SDK. Эта функциональность 
поддерживает две команды <code>list</code> и <code>update</code>:</p>
<ul>
<li><p><code>dotnet workload list</code> позволяет вывести список установленных рабочих нагрузок</p></li>
</code> позволяет вывести список установленных рабочих нагрузок</p></li>
<li><p><code>dotnet workload update</code> позволяет обновить установленные рабочие нагрузки</p></li>
</ul>
</li>

<li><p>Добавлен инструментарий <span class="b">Package Validation</span>, который позволяет разработчикам пакетов NuGet, проверить публикуемые пакеты 
на корректность.</p></li>
<li><p>Добавлено больше анализаторов Roslyn</p></li>
<li><p>В Windows Forms теперь можно глобально установить для приложения шрифт с помощью метода <code>Application.SetDefaultFont()</code></p></li>
<li><p>В <span class="b"> IHostBuilder</span> добавлен новый API - <code>ConfigureHostOptions</code>, который позволяет упростить настройку приложения. 
Пример применения:</p>
<pre class="brush:c#;">
using HostBuilder host = new()
    .ConfigureHostOptions(o =&gt;
    {
        o.ShutdownTimeout = TimeSpan.FromMinutes(10);
    })
    .Build();

host.Run();
</pre></li>

<li><p>Для устранения проблемы с регистрацией сервиса <span class="b">IAsyncDisposable</span> добавлен новый API - <code>CreateAsyncScope</code>. 
Пример применения:</p>
<pre class="brush:c#;">
await using (var scope = provider.CreateAsyncScope())
{
    var foo = scope.ServiceProvider.GetRequiredService&lt;Foo&gt;();
}
</pre></li>
<li><p>Улучшена производительность класса <span class="b">JsonSerializer</span></p></li>

<li><p>WebSocket стали поддерживать сжатие:</p>
<pre class="brush:c#;">
var cws = new ClientWebSocket();
cws.Options.DangerousDeflateOptions = new WebSocketDeflateOptions()
{
    ClientMaxWindowBits = 10,
    ServerMaxWindowBits = 10
};
</pre></li>

<li><p>Добавлена поддержка <span class="b">SOCKS</span> - реализации прокси-сервера, которая 
может обрабатывать любой трафик по протоколам TCP и UDP. Соответственно добавлена поддержка схем сокетов Socks4, Socks4a и Socks5. 
Благодаря этому, например, можно тестировать внешние подключения через SSH или подключения к сети Tor network.
А класс <code>WebProxy</code> теперь может принимать эти схемы сокетов:</p>
<pre class="brush:c#;">
var cws = new ClientWebSocket();
var handler = new HttpClientHandler
{
    Proxy = new WebProxy("socks5://127.0.0.1", 9050)
};
var httpClient = new HttpClient(handler);
</pre></li>
</ul>
<p>Подобнее про новвоведения в .NET 6 Preview 5 можно прочитать в официальном блоге Microsoft на странице <a href="https://devblogs.microsoft.com/dotnet/announcing-net-6-preview-5/" rel="nofollow">https://devblogs.microsoft.com/dotnet/announcing-net-6-preview-5/</a></p>

<h3>Что нового в ASP.NET Core в .NET 6 Preview 5</h3>
<ul>
<li><p>Обновлен функционал команды <code>dotnet watch</code> в .NET Hot Reload</p></li>
<li><p>Шаблоны проектов ASP.NET Core SPA обновлены до версий Angular 11 и React 17 (прим. ред.: месяц назад вышел Angular 12)</p></li>
<li><p>В SVG-элементах <code>foreignObject</code> можно использовать синтаксис Razor</p>
<pre class="brush:xml;">
&lt;svg width="200" height="200" xmlns="http://www.w3.org/2000/svg"&gt;
    &lt;rect x="0" y="0" rx="10" ry="10" width="200" height="200" stroke="black" fill="none" /&gt;
    &lt;foreignObject x="20" y="20" width="160" height="160"&gt;
        &lt;p&gt;@message&lt;/p&gt;
    &lt;/foreignObject&gt;
&lt;/svg&gt;

@code {
    string message = "Hello World!";
}
</pre>
</li>
<li><p>В компонентах Blazor для параметров типов <code>Action</code> и <code>RenderFragment</code> можно указывать значение <code>null</code></p></li>
<li><p>Уменьшен размер загружаемой среды выполнения Blazor WebAssembly </p></li>
<li><p>Для сервера Kestrel добавлены подкатегории сообщений для улучшения фильтрации логов</p></li>
<li><p>Добавлен новый API - <code>System.Net.Http.HeaderNames</code> для упрощения получения и установки заголовками HTTP-запрсоа и ответа:</p>
<pre class="brush:c#;">
app.Use(async (context, next) =&gt;
{
    var hostHeader = context.Request.Headers.Host;
    app.Logger.LogInformation("Host header: {host}", hostHeader);
    context.Response.Headers.XPoweredBy = "ASP.NET Core 6.0-preview5";
    await next.Invoke(context);
    var dateHeader = context.Response.Headers.Date;
    app.Logger.LogInformation("Response date: {date}", dateHeader);
});
</pre>
</li>
</ul>
<p>Более подробно про новвоведения ASP.NET Core в NET 6 Preview 1 в блоге Microsoft: <a href="https://devblogs.microsoft.com/aspnet/asp-net-core-updates-in-net-6-preview-5/" rel="nofollow">https://devblogs.microsoft.com/aspnet/asp-net-core-updates-in-net-6-preview-5/</a></p>

<h3>Что нового в Entity Framework Core в .NET 6 Preview 1</h3>
<p>В Entity Framework Core в .NET 6 Preview 1 добавлена новая функциональность - <span class="b">компилируемые модели</span> (compiled models). 
Они значительно увеличили производитльность. Так, время запуска приложения по секам Microsoft уменьшилось 
чуть ли не в 10 раз. По мере того, как компилируемые модели увеличиваются за счет новых свойств и связей, начальное время запуска остается постоянным</p>
<img src="./pics/efcompiledstartuptime.png" alt="Entity Framework Core в .NET 6 Preview 1" />
<p>В то же время компилируемые модели поддерживают не все возможности, доступные для стандартных моделей.</p>
<p>Более подробно про изменения в EF Core в блоге Microsoft: <a href="https://devblogs.microsoft.com/dotnet/announcing-entity-framework-core-6-0-preview-5-compiled-models/" rel="nofollow">https://devblogs.microsoft.com/dotnet/announcing-entity-framework-core-6-0-preview-5-compiled-models/</a></p>

<h3>Что нового в .NET MAUI Preview 5</h3>
<ul>
<li><p>Добавлено новое событие <code>AttachedHandler</code> для управления прикрепленными визуальными элементами</p></li>
<li><p>Для некоторых элементов управления свойства и события портированы из архитектуры рендереров Xamarin.Forms в обработчики событий</p></li>
<li><p>Обновлен шаблон проекта - теперь он включает два проекта: собственно проект .NET MAUI, который нацелен на разные системы, и проект WinUI.</p>
<img src="./pics/maui-preview-5-solution.png" alt=".NET 6 MAUI in Visual Studio" />
<p>Для поддержи WinUI в настоящий момент необходимо установить расширение Project Reunion 0.8 (Preview)</p>
</li>
</ul>
<p>Более подробно про .NET MAUI Preview 5 можно прочитать в анонсе Microsoft на странице: <a href="https://devblogs.microsoft.com/dotnet/announcing-net-maui-preview-5/" rel="nofollow">https://devblogs.microsoft.com/dotnet/announcing-net-maui-preview-5/</a></p>


