<p>9 декабря 2021 года компания Google выпустила большую группу обновлений в виде новой версии языка Dart (Dart 2.15), новой версии 
кроссплатформенного фреймворка Flutter - Flutter 2.8 и, кроме того, аннонсировал новый движок для создания игр - <span class="b">Flame</span>, который работает повех Flutter.</p>
<h3>Dart 2.15</h3>
<p>Что нового в новой подверсии? Прежде всего были внесены изменения в систему isolates - специальных сущностей, которые изолированны друг от друга и 
которые позволяют выполнять отдельные куски кода параллельно. В новой версии была переосмыслена система изолятов и введена концепция групп изолятов 
(isolate groups). В рамках одной группы изоляты могут использовать совместно различные структуры данных, которые представляют запущенную программу. 
Это повышает поизводительность - запуск нового изолята в рамках существующей группы происходит в 100 быстрее, поскольку не надо заново инициализировать структуры 
данных. а потребленее памяти уменьшилось в 10–100 раз. Также переделан механизм обмена сообщениями между изолятами - обмен сообщениями малого и среднего размера теперь происходит 
в восемь раз быстрее.</p>
<p>Также в язык была добавлена поддержка <code>tear-off</code> конструктора:</p>
<pre class="brush:dart;">
class Greeter {
  final String name;
  Greeter(this.name);
  
  void greet(String who) {
    print('$name says: Hello $who!');
  }
}void main() {
  final m = Greeter('Tom');
  final g = m.greet; // g хранит указать на функцию  m.greet.
  g('Leaf'); // вызывает функцию m.greet и выводит на консоль "Tom says: Hello Leaf!"
}
</pre>
<p>Переменная <code>g</code> здесь редставляет указать на функцию - такие указатели еще называются <code>tear-off</code>. И начиная с версии dart 2.15 теперь можно определять 
подобные указатели на конструкторы:</p>
<pre class="brush:dart;">
class FruitWidget extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return Column(
        children: ['Apple', 'Orange'].map(Text.new).toList());
  }
}
</pre>
<p>Здесь <code>Text.new</code> представляет ссылку на конструктор по умолчанию. Также можно ссылаться на вторичные конструкторы.</p>
<p>Также в новой версии оптимизирована функциональность перечислений. Например, к ним добавлено свойство <code>name</code>, которое позволяет получить текстовое 
представление константы перечисления:</p>
<pre class="brush:dart;">
enum MyEnum {
  one, two, three
}void main() {
  print(MyEnum.one.name);  // one
}
</pre>
<p>Также можно получить словарь (карту) из перечислений и их текстовых наименований:</p>
<pre class="brush:dart;">
final map = MyEnum.values.asNameMap(); 
print(map['three'] == MyEnum.three);  // true
</pre>
<p>Кроме того, в Dart 2.15 добавлена поддержка сжатых указателей (compressed pointers), 
которые позволяют в 64-битном SDK потреблять меньше памяти, если необходимо использовать только 32-битные адреса в памяти.</p>
<p>Также пакет инструментов <span class="b">DevTools</span>, предназначенный для отладки и отслеживания производительности приложений на Dart, по умолчанию 
включен в Dart SDK.</p>
<p>Подробнее по выход Dart 2.15 можно прочитать в официальном блоге на mediume - <a href="https://medium.com/dartlang/dart-2-15-7e7a598e508a" rel="nofollow">https://medium.com/dartlang/dart-2-15-7e7a598e508a</a></p>
<h3>Flutter 2.8</h3>
<p>Flutter представляет кроссплатформенный фреймворк для создания приложений для разных платформ - мобильных, дестопных и веб-приложений, 
которые могут использовать один и тот же код на языке Dart. Основные изменения лежат прежде всего в сфере производительности - новая версия должна работать быстрее и при этом потреблять меньше памяти, прежде всего на мобильных платформах и в веб-приложениях./p>
<p>Также обновлен виджет <span class="b">WebView</span> - его новая версия 3.0 использует по умолчанию гибридный режим и имеет экспериментальную 
поддержку для веб-приложений.</p>  
<p>Кроме того, сообщается, что в ближайшее будущем будет упразднен канал <span class="b">dev</span>. Кто ранее пользовался этим каналом, могут перейти на канал 
<code>stable</code>, <code>beta</code> или <code>master</code>.</p>
<p>Более подробно про анонс Flutter 2.8 можно прочитать в официальном блоге Google на странице
<a href="https://medium.com/flutter/whats-new-in-flutter-2-8-d085b763d181" rel="nofollow">https://medium.com/flutter/whats-new-in-flutter-2-8-d085b763d181</a>.</p>
<h3>Flame</h3>
<p>Также Google выпустил в релиз версию 1.0 нового игрового движка - <span class="b">Flame</span>. Flame представляет 
модульный движок для создания 2D-игр, который работает поверх фреймворка Flutter. 
Flame предоставляет возможности для более быстрого создания игр: игровой цикл, базовые примитивы, анимированные спрайты и изображения, функциональность определения 
столкновений, камеру, систему эффеектов, поддержку жестов и ввода.</p>
<p>Поскольку Flame исповедует модульный подход, то при необходимости его можно расширять с помощью дополнительных пакетов и интегрировать с дугими 
библиотеками, например, с Rive (для анимации), Forge2D (физический движок в стиле Box2D), Tiled (редактор фрагментов карты), 
Fire Atlas (редактор анимации и спрайтов).</p>
<p>Официальная страница движка - <a href="https://flame-engine.org/" rel="nofollow">https://flame-engine.org/</a>.</p>

