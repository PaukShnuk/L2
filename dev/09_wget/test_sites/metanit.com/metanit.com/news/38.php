<h3>Visual Studio 2022 Preview 3 и Visual Studio 2019 v16.11</h3>
<p>10 августа Microsoft выпустил новую Preview-версию VS 2022 - Visual Studio 2022 Preview 3. Часть изменений пришлась на усовершенствование пользовательского интерфейса, в частности, оптимизацию темной темы.</p>
<p>Также улучшены возможности по удаленному тестированию, в том числе в Linux-контейнерах, WSL и с использованием SSH-соединений.</p>
<p>Также добавлены новые типы проектов языков JavaScript и TypeScript, которые позволяют создавать приложения с использованием Angular, Vue и React</p>
<p>Подробнее про выход VS 2022 Preview 3 можно найти в блоге Microsoft на 
<a href="https://devblogs.microsoft.com/visualstudio/visual-studio-2022-preview-3-now-available/" rel="nofollow">https://devblogs.microsoft.com/visualstudio/visual-studio-2022-preview-3-now-available/</a></p>
<p>Также вышла новая подверсия текущей VS - Visual Studio 2019 v16.11</p>
<h3>.NET 6 Preview 7</h3>
<p>Также 10 августа Microsoft выпустил последнюю Preview-версию .NET 6 - .NET 6 Preview 7. Далее ожидается выход двух выпусков Release Candidate (RC), 
которые уже можно будет полноценно использовать для создания стабильных приложений, и, наконец, финальный релиз в начале ноября 2022 года.</p>
<p>Некоторые основные изменения в этой Preview-версии:</p>
<li><p>Базовый шаблон проекта консольного проекта использует top-level statements. В частности,</p>
<pre class="brush:c#;">
Console.WriteLine("Hello, World!");
</pre>
<p>Вместо</p>
<pre class="brush:c#;">
using System;

namespace Company.ConsoleApplication1
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Hello, World!");
        }
    }
}
</pre>
<p>Подобные изменения затронули и другие типы проектов, в частности, для ASP NET</p>
</li>
<li><p>Для ряда проектов (консольный, библиотека классов, ASP.NET) теперь по умолчанию в настройках проекта включена опция <span class="b">nullable reference types</span>:</p>
<pre class="brush:xml;">
&lt;Project Sdk="Microsoft.NET.Sdk"&gt;

  &lt;PropertyGroup&gt;
    &lt;OutputType&gt;Exe&lt;/OutputType&gt;
    &lt;TargetFramework&gt;net6.0&lt;/TargetFramework&gt;
    &lt;Nullable&gt;enable&lt;/Nullable&gt;
  &lt;/PropertyGroup&gt;

&lt;/Project&gt;
</pre>
</li>
<li><p>Обновилось Reflection API, которое теперь учитывает nullable reference types</p></li>
<li><p>Добавлен <span class="b">Native memory allocation API</span>. Этот API представляет функциональность, эквивалентную функциям <span class="b">malloc, free, realloc</span> и 
<span class="b">calloc</span> в языке C и позволяет управлять выделением памяти.</p></li>
<li><p>Обновилось API System.Text.Json</p></li>
</ul>
<p>Более подробно про все новвоведения и изменения можно прочитать в официальном блоге по ссылке 
<a href="https://devblogs.microsoft.com/dotnet/announcing-net-6-preview-7/" rel="nofollow">https://devblogs.microsoft.com/dotnet/announcing-net-6-preview-7/</a></p>
<h3>Релиз ASP NET 6 Preview 7</h3>
<p>Вместе с .NET 6 Preview 7 вышли обновления для ASP NET. Некоторые основные обновления в ASP NET 6 Preview 7:</p>
<ul>
<li><p>Применение <code>Minimal API</code> в шаблонах проетов</p></li>
<li><p>Добавление интерфейса <span class="b">IResult</span> для создания классов ответа HTTP</p></li>
<li><p>Добавлена поддержка объектов Request, Response и User для минимальных действий</p></li>
<li><p>Добавлена передача данных из строки запроса параметрам компонентов Blazor</p></li>
<li><p>Замена текущего адреса URI в истории браузера в Blazor</p></li>
<li><p>Новое свойство DynamicComponent.Instance для доступа к динамически создаваемым компонентам</p></li>
<li><p>Добавлена поддержка в Blazor стриминга из кода JavaScript в .NET</p></li>
<li><p>Добавлена возможность загрузки больших файлов Blazor</p></li>
<li><p>Добавлена возможность изменения HTML-элемента <code>&lt;head&gt;</code> из компонентов Blazor</p></li>
<li><p>Добавлена поддержка атрибута <code>multiple</code> HTML-элемента <code>&lt;select&gt;</code> в Blazor</p></li>
<li><p>Добавлена поддержка для протокола HTTP/3 в Kestrel</p></li>
<li><p>Поддержка QUIC перенесена в общий фреймворк</p></li>
<li><p>Добавлена поддержка для символов кодировки, отличной от  ASCII, в заголовках ответа в Kestrel</p></li>
<li><p>Добавлена логгер W3CLogger</p></li>
<li><p>Добавлена возможность настройки истечения срока аутентификации в SignalR</p></li>
</ul>
<p>Более подробно про обновления в ASP NET 6 Preview 7 можно найти в официальном блоге по ссылке 
<a href="https://devblogs.microsoft.com/aspnet/asp-net-core-updates-in-net-6-preview-7/" rel="nofollow">https://devblogs.microsoft.com/aspnet/asp-net-core-updates-in-net-6-preview-7/</a></p>

<h3>Релиз .NET MAUI Preview 7</h3>
<p>Также вышло обновления для .NET MAUI - .NET MAUI Preview 7. Некоторые основные обновления в .NET MAUI Preview 7:</p>
<ul>
<li><p>Обновлен функционал панелей компоновки. В частности, теперь о умолчанию применяются компоненты Grid, FlexLayout, StackLayout 
(HorizontalStackLayout, VerticalStackLayout). А AbsoluteLayout и RelativeLayout оставлены только для сомвместимости и 
</p></li>
<li><p>Теперь на всех платформах по умолчанию применяется масшабирование шрифта</p></li>
<li><p>Добавлена поддержка эффектов</p></li>
<li><p>Улучшена привязка тем</p></li>
<li><p>Добавлена поддержка передачи при навигации в Shell комплексных объектов</p></li>
<li><p>Добавлен функционал событий жизненного цикла окна</p></li>
<li><p>Обновлен функционал событий межстраничной навигации</p></li>
</ul>
<p>Более подробно про обновления в MAUI Preview 7 можно найти в официальном блоге по ссылке 
<a href="https://devblogs.microsoft.com/dotnet/announcing-net-maui-preview-7/" rel="nofollow">https://devblogs.microsoft.com/dotnet/announcing-net-maui-preview-7/</a></p>

