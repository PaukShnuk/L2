<p>8 ноября компания Microsoft выпустила финальную версию фреймворка .NET 6.0 с C# 10 и F# 6, а также Visual Studio 2022. 
.NET 6 ознаменовался значительным увеличением поизводительности по сравнению с предыдущими версиями фреймворка и большим количеством нового функционала. 
Кроме того, это LTS-версия, которая будет поддерживаться в течении следующих трех лет.</p>

<p>.NET 6.0 представляет унифицированную платформу, которая позволяет создавать десктопные  и мобильные и веб-приложения, а также приложения для IoT и облака.</p>
<img src="./pics/dotnet6.1.png" alt=".NET 6 и C# 10" />

<p>Некоторые основные изменения в этой версии:</p>
<ul>
<li><p>Благодаря внедрению <span class="b">Dynamic Profile-guided Optimization (PGO)</span> удалось повысить производительность. 
Dynamic PGO базируется на механизме уровневой компиляции Tiered Compilation, которая позволяет произвести на старте быструю базовую компиляцию методов 
("уровень 0"), а когда метод потребует, произвести его перекомпиляцию с рядом оптимизаций ("уровень 1")</p></li>
<li><p>Полностью переписан API для работы с файлами, в частности, класс <span class="b">FileStream</span>. Это привело к значительному увеличению производительности: 
увеличилась скорость выполнения и значительно уменьшилось выделение памяти.</p></li>
<li><p>Более быстрая проверка интерфейсов и использование их в преобразованиях типов. По данным Microsoft, производительность увеличилась 
на 16% – 38%.</p></li>
<li><p>Добавлены генераторы кода для API <span class="b">System.Text.Json</span>, что опять же повысило производительность и уменьшило выделение памяти. 
Кроме того, <span class="b">JsonSerializer</span> поддерживает <span class="b">IAsyncEnumerable</span>:</p>
<pre class="brush:c#;">
using System;
using System.Collections.Generic;
using System.IO;
using System.Text.Json;

static async IAsyncEnumerable&lt;int&gt; PrintNumbers(int n)
{
    for (int i = 0; i &lt; n; i++) yield return i;
}

using Stream stream = Console.OpenStandardOutput();
var data = new { Data = PrintNumbers(3) };
await JsonSerializer.SerializeAsync(stream, data); // {"Data":[0,1,2]}
</pre>
</li>

<li><p>Релиз .NET 6.0 также ознаменовался выходом новой версии языка C# - C# 10. Некоторые из его новвоведений:</p>
<ul>
<li><p>Глобальные пространства имен. Достаточно подключить пространство имен в один файл кода с помощью директивы <span class="b">global using</span>, 
и оно будет доступно во всех файлах проекта.</p>
<pre class="brush:c#;">

    global using System;
    global using static System.Console;
    global using Env = System.Environment;

</pre>
</li>
<li><p>Пространства имен уровня файла. Так, вместо </p>
<pre class="brush:c#;">
namespace MyNamespace
{
    class MyClass { ... }
}
</pre>
<p>Можно писать</p>
<pre class="brush:c#;">
namespace MyNamespace;

class MyClass { ... } // Not indented
</pre>
</li>
<li><p>Теперь можно создавать объекты record в виде структур (раньше все record представляли классы)</p>
<pre class="brush:c#;">
public record struct Person
{
    public string FirstName { get; init; }
    public string LastName { get; init; }
}
</pre>
</li>
</ul>

</li>
<li><p>Также вместе с фреймворком вышла новая версия F# - F# 6</p></li>
<li><p>.NET 6.0 позволяет использовать Hot Reload для динамческой перезагрузки приложения при изменении кода</p></li>
<li><p>Это первая версия фреймворка, которая поддерживает архитектуру Apple Silicon (Arm64) и значительно усовершенствована для работы Windows Arm64.</p></li>
<li><p>Некоторые изменения затронули Windows Forms, в частности, возможность установки шрифта для приложения, минимизация кода и ряд других</p></li>
<li><p>Добавлен ряд новых API:</p>
<ul>
<li><p>Новые API в <span class="b">System.Math</span>, например, <code>SinCos</code> для одновременного вычисления синуса и косинуса и ряд других</p></li>
<li><p>API для сжатия для WebSocket</p></li>
<li><p>Поддержка прокси для Socks4, Socks4a и Socks5</p>
<pre class="brush:c#;">
var handler = new HttpClientHandler
{
    Proxy = new WebProxy("socks5://127.0.0.1", 9050)
};
var httpClient = new HttpClient(handler);
</pre></li>
<li><p>В <span class="b">Microsoft.Extensions.Hosting</span> добавлено <span class="b">ConfigureHostOptions API</span></p>
<pre class="brush:c#;">
using HostBuilder host = new()
    .ConfigureHostOptions(o =&gt;
    {
        o.ShutdownTimeout = TimeSpan.FromMinutes(10);
    })
    .Build();

host.Run();
</pre>
</li>
<li><p>В <span class="b">Microsoft.Extensions.DependencyInjection</span> добавлено <span class="b">CreateAsyncScope API</span> 
для использования сервисами <code>IAsyncDisposable</code></p>
<pre class="brush:c#;">
await using (var scope = provider.CreateAsyncScope())
{
    var foo = scope.ServiceProvider.GetRequiredService&lt;Foo&gt;();
}
</pre>
</li>
<li><p>В <span class="b">System.Linq</span> методы <code>Enumerable.ElementAt</code> и <code>Enumerable.Take</code> 
имеют поддержку Index и Range</p>
<pre class="brush:c#;">
Enumerable.Range(1, 10).ElementAt(^2); // 9

source.Take(..3) вместо source.Take(3)
source.Take(3..) вместо source.Skip(3)
source.Take(2..7) вместо source.Take(7).Skip(2)
source.Take(^3..) вместо source.TakeLast(3)
source.Take(..^3) вместо source.SkipLast(3)
source.Take(^7..^3) вместо source.TakeLast(7).SkipLast(3).
</pre>
<p>Также добавлены новые методы <span class="b">DistinctBy/UnionBy/IntersectBy/ExceptBy/MaxBy/MinBy</span></p>
<p>А в методах <span class="b">FirstOrDefault/LastOrDefault/SingleOrDefault</span> теперь можно установить значение по умолчанию:</p>
<pre class="brush:c#;">
Enumerable.Empty&lt;int&gt;().SingleOrDefault(-1); // -1
</pre>
</li>
</ul>
<li><p>Очередь на основе приоритетов - <span class="b">PriorityQueue</span></p>
<pre class="brush:c#;">
// очередь из строк, числа устанавливают приоритеты
var pq = new PriorityQueue&lt;string, int&gt;();

// получение элементов очереди
pq.Enqueue("A", 3);
pq.Enqueue("B", 1);
pq.Enqueue("C", 2);
pq.Enqueue("D", 3);

pq.Dequeue(); //  "B"
pq.Dequeue(); // "C"
pq.Dequeue(); // либо "A", либо "D"
</pre>
</li>

<li><p>Новые структуры для представления дат и времени: <span class="b">DateOnly</span> и <span class="b">TimeOnly</span></p>
</li>
</li>
</ul>

<p>Отмечается, что .NET 6 поддерживается в Visual Studio 2022 и Visual Studio 2022 for Mac. Однако НЕ поддерживается в 
Visual Studio 2019 и Visual Studio for Mac 8. Кроме того, .NET 6 поддерживается в Visual Studio Code с помощью расширения для C#.</p>

