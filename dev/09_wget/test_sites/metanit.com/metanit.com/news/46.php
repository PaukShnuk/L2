<p>Аналитики из компании Veracode проанализировали различные приложения на предмет безопасности и уязвимостей и результаты анализа зафиксировали в отчете 
<a href="https://www.veracode.com/state-of-software-security-report" rel="nofollow" target="_blank">State of Software Security</a>. 
При этом ставилась задача определить, являются ли некоторые языки более подверженными уязвимостям при использовании сторонних библиотек, чем другие, 
и есть ли какой-то прогресс в плане безопасности в последние годы. Некоторые итоги данного исследования.</p>
<p>Прежде всего отмечается тенденция к использованию одного языка программирования в приложении. Так, в 2018 году около 20% приложений использовали одновременно несколько языков программирования. 
В 2021 году это число составило менее чем 5%.</p>
<p>Повышается безопасность сторонних библиотек, они становятся все менее уязвимыми. Если 2017 году 35% библиотек имели уязвимости, то в 2021 году
году это количество снизилось до 10%.</p>
<p>Уязвимости стале быстрее закрываться. В 2017 году требовалось бы более трех лет, чтобы закрыть 50% уязвимостей с момента их обнаружения. 
Сейчас же для этого требуется чуть более года. В то же время, 77% уязвимостей остаются открытыми в течение трех месяцев.</p>
<h3>Языки програмирования и сторонние библиотеки</h3>
<p>Одним из узких мест безопасности обычно являются сторонние библиотеки. Среди языков программирования в этом плане выделяется Java - приложения на этом языке больше всего применяют сторонние библиотеки. 
Причем примение сторонних библиотек в последние годы только росло. Так, по оценкам около 97% приложений на Java используют open-source-библиотеки</p>
<p>Применение сторонних библиотек в приложениях на .NET в середине 2020 года превысило 50%, что совпало с выходом .NET 5</p>
<p>Что касается JavaScript и Python, то тут отмечается сильная дифференциация - приложения либо почти целиком полагаются на сторонние библиотеки, 
либо, наоборот, используются преимущественно код собственной разработки.</p>
<p>В протиположность Java, приложения на языках PHP и C++ преимущественно используют код свобственной разработки</p>
<h3>Наиболее уязвимые языки</h3>
<p>Исследование показало, что в среднем около 12,5% библиотек на Java имеют наибольшее количество уязвимостей. Далее следуют Ruby (около 10%) и 
Python (около 5%).</p>
<p>PHP, JavaScript и .NET имеют наименьшую распространенность уязвимых библиотек среди проанализированных языков — около 3% для каждого языка.</p>
<p>При этом аналитики Veracode отмечает заметный прогресс, в частности, для библиотек Java, JavaScript и Python. Так, в 2017 году количество уязвимых 
библиотек Java составляло примерно 25%, Python — около 20%, а JavaScript — примерно 10%.</p>

<h3>Типы уязвимостей</h3>
<p>Кроме того, аналитики Veracode исследовали приложения на предмет различных уязвимостей, применяя различные методологии: статический анализ, динамический анализ и 
SCA (software composition analysis или анализ композиции программного обеспечения). Результаты анализа на картинке ниже</p>
