<p>Элемент Listbox в tkinter представляет список объектов. Определим простой список:</p>
<pre class="brush:py;">
from tkinter import *

languages = ["Python", "JavaScript", "C#", "Java"]

root = Tk()
root.title("GUI на Python")
root.geometry("300x280")

languages_listbox = Listbox()

for language in languages:
    languages_listbox.insert(END, language)

languages_listbox.pack()

root.mainloop()
</pre>
<p>Для наполнения listboxa элементами в цикле for пробегаемся по всем элементам списка languages и добавляем каждый элемент в listbox с помощью метода 
<span class="b">insert()</span>. В качестве первого аргумента в insert передается индекс вставки элемента. Но если мы хотим последовательно добавить элементы, 
то вместо индекса можно использовать значение END.</p>
<img src="./pics/9.19.png" alt="Listbox в tkinter и Python" />
<p>Для настройки виджета Listbox мы можем указать в его конструкторе следующие параметры:</p>
<ul>
<li><p><span class="b">bg</span>: фоновый цвет</p></li>
<li><p><span class="b">bd</span>: толщина границы вокруг элемента</p></li>
<li><p><span class="b">cursor</span>: курсор при наведении на Listbox</p></li>
<li><p><span class="b">font</span>: настройки шрифта</p></li>
<li><p><span class="b">fg</span>: цвет текста</p></li>
<li><p><span class="b">height</span>: высота элемента в строках. По умолчанию отображает 10 строк</p></li>
<li><p><span class="b">highlightcolor</span>: цвет элемента, когда он получает фокус</p></li>
<li><p><span class="b">highlightthickness</span>: толщина границы элемента, когда он находится в фокусе</p></li>
<li><p><span class="b">relief</span>: устанавливает стиль элемента, по умолчанию имеет значение SUNKEN</p></li>
<li><p><span class="b">selectbackground</span>: фоновый цвет для выделенного элемента</p></li>
<li><p><span class="b">selectmode</span>: определяет, сколько элементов могут быть выделены. Может принимать следующие значения: BROWSE, SINGLE, MULTIPLE, EXTENDED. 
Например, если необходимо включить множественное выделение элементов, то можно использовать значения MULTIPLE или EXTENDED.</p></li>
<li><p><span class="b">width</span>: устанавливает ширину элемента в символах. По умолчанию ширина - 20 символов</p></li>
<li><p><span class="b">xscrollcommand</span>: задает горизонтальную прокрутку</p></li>
<li><p><span class="b">yscrollcommand</span>: устанавливает вертикальную прокрутку</p></li>
</ul>
<p>Некоторую сложность при использовании Listbox представляет создание прокрутки. Рассмотрим, как это сделать:</p>
<pre class="brush:py;">
from tkinter import *

languages = ["Python", "JavaScript", "C#", "Java", "C/C++", "Swift",
             "PHP", "Visual Basic.NET", "F#", "Ruby", "Rust", "R", "Go",
             "T-SQL", "PL-SQL", "Typescript"]

root = Tk()
root.title("GUI на Python")

scrollbar = Scrollbar(root)
scrollbar.pack(side=RIGHT, fill=Y)

languages_listbox = Listbox(yscrollcommand=scrollbar.set, width=40)

for language in languages:
    languages_listbox.insert(END, language)

languages_listbox.pack(side=LEFT, fill=BOTH)
scrollbar.config(command=languages_listbox.yview)

root.mainloop()
</pre>
<p>Для создания прокрутки создается элемент <span class="b">Scrollbar</span>. И так как необходимо прокручивать listbox по вертикали, то у него задается 
параметр <code>yscrollcommand=scrollbar.set</code></p>
<p>В конце scrollbar ассоциируется с функцией, которую надо выполнять при прокрутке. В данном случае это метод <span class="b">yview</span> элемента listbox. 
В итоге мы сможем прокручивать элементы по вертикали:</p>
<img src="./pics/9.20.png" alt="Scrollbar и Listbox в tkinter и Python" />
<h3>Основные методы Listbox</h3>
<p>Listbox имеет ряд методов для управления поведением элемента и его содержимым. Некоторые из них:</p>
<ul>
<li><p><span class="b">curselection()</span>: возвращает набор индексов выделенных элементов</p></li>
<li><p><span class="b">delete(first, last = None)</span>: удаляет элементы с индексами из диапазона [first, last]. 
Если второй параметр опущен, то удаляет только один элемент по индексу first.</p></li>
<li><p><span class="b">get(first, last = None)</span>: возвращает кортеж, который содержит текст элементов 
с индексами из дипазона [first, last]. Если второй параметр опущен, возвращается только текст элемента с индексом first.</p></li>
<li><p><span class="b">insert(index, element)<span>: вставляет элемент по определенному индексу</p></li>
<li><p><span class="b">size()</span>: возвращает количество элементов</p></li>
</ul>
<p>Для рассмотрения этих методов напишем небольшой скрипт по управлению данными:</p>
<pre class="brush:py;">
from tkinter import *


# удаление выделенного элемента
def delete():
    selection = languages_listbox.curselection()
    # мы можем получить удаляемый элемент по индексу
    # selected_language = languages_listbox.get(selection[0])
    languages_listbox.delete(selection[0])


# добавление нового элемента
def add():
    new_language = language_entry.get()
    languages_listbox.insert(0, new_language)

root = Tk()
root.title("GUI на Python")

# текстовое поле и кнопка для добавления в список
language_entry = Entry(width=40)
language_entry.grid(column=0, row=0, padx=6, pady=6)
add_button = Button(text="Добавить", command=add).grid(column=1, row=0, padx=6, pady=6)

# создаем список
languages_listbox = Listbox()
languages_listbox.grid(row=1, column=0, columnspan=2, sticky=W+E, padx=5, pady=5)

# добавляем в список начальные элементы
languages_listbox.insert(END, "Python")
languages_listbox.insert(END, "C#")

delete_button = Button(text="Удалить", command=delete).grid(row=2, column=1, padx=5, pady=5)

root.mainloop()
</pre>
<p>Для манипулирования элемента списка здесь определено две кнопки. Первая кнопка вызывает функцию <code>add()</code>, которая получает введенное в текстовое поле значение и добавляет 
его на первое место в списке с помощью метода <span class="b">insert()</span>.</p>
<p>Вторая кнопка по нажатию удаляет выделенный элемент. Для этого мы сначала получаем выделенные индексы через метод <span class="b">curselection()</span>. 
Так как в нашем случае выделяется только один элемент, то получаем его индекс через выражение <code>selection[0]</code>. И этот индекс передаем в метод 
<span class="b">delete()</span> для удаления.</p>
