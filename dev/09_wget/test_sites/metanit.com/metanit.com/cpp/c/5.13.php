<p>Язык программирования Си допускает использование функций, которые имеют нефиксированное количество параметров. Более того может быть неизвестным не только количество, но 
и типы параметров. То есть точное определение параметров становится известным только во время вызова функции.</p>
<p>Для определения параметров неопределенной длины в таких функциях используется многоточие:</p>
<pre class="brush:c;">тип имя_функции(обязательные параметры, ...)</pre>
<p>При этом надо учитывать, что функция должна иметь как минимум один обязательный параметр.</p>
<p>Например, определим функцию, которая вычисляет сумму чисел, количество чисел нефиксировано:</p>
<pre class="brush:c;">
#include &lt;stdio.h&gt;

int sum(int n, ...)
{
	int result = 0;
	// получаем указатель на параметр n
	for(int *ptr = &n; n&gt;0; n--)
	{
		result+= *(++ptr);
	}
	return result;
}

int main(void)
{	
	printf("%d \n", sum(4, 1, 2, 3, 4));
	printf("%d \n", sum(5, 12, 21, 13, 4, 5));
	return 0;
}
</pre>
<p>При работе с параметрами следует учитывать, что для языка Си, как правило, меньшее значение адреса у первого параметра, а 
остальные размещаются дальше подряд (Но это характерно НЕ для всех компиляторов). Поэтому мы можем получить адрес первого параметра и указателем пробежаться по адресам, которые идут после адреса первого параметра.</p>
<p>Первый и обязательный параметр функции sum - n - указывает на количество необязательных параметров. В цикле устанавливаем указатель ptr на адрес 
параметра n и последовательно перемещаем его. С помощью операции разыменования <code>*ptr</code> после перемещения указателя на один элемент вперед получаем значение и выполняем сложение с 
переменной result.</p>
<p>В то же время нельзя не отметить недостаток данного решения: все параметры представляют один и тот же тип. Кроме того, мы можем при наборе кода ошибиться со значением первого 
параметра, тогда результаты функции будут непредсказуемыми. Третий недостаток - компилятор может вносить свои коррективы в размещение параметров, соответственно результат опять может быть непредсказуемым.</p>
<p>И для решения этих проблем и упрощения работы с переменным количеством параметров неопределенных типов в языке Си в стандартом заголовочном файле <span class="b">stdarg.h</span> 
определены специальные макрокоманды:</p>
<pre class="brush:c;">
va_start();
va_arg();
va_end();
</pre>
<p>Все эти макросы используют специальный тип данных <span class="b">va_list</span>, который также определен в stdarg.h и который позволяет обрабатывать списки параметров с нефиксированным количеством.</p>
<p>Макрос <span class="b">va_start</span> имеет следующее определение:</p>
<pre class="brush:c;">void va_start(va_list param, последний_явный_параметр);</pre>
<p>Первый параметр макроса - param связывает объект va_list с первым необязательным параметром. Для его определения в качестве второго параметра в макрос передается последний обязательный параметр функции. Таким образом, используя 
последний обязательный параметр, мы можем нацелить объект va_list на адрес первого необязательного параметра. То есть фактически va_list выступает в данной роли как указатель.</p>
<p>Макрос <span class="b">va_arg</span> имеет следующее определение:</p>
<pre class="brush:c;">type va_arg(va_list param, type);</pre>
<p>Этот макрос позволяет получить значение параметра типа <code>type</code>, а также переместить указатель va_list на следующий необязательный параметр.</p>
<p>Макрос <span class="b"></span> позволяет выйти из функции с переменным списком параметров. Она имеет следующее определение:</p>
<pre class="brush:c;">void va_end(va_list param);</pre>
<p>В качестве параметра она принимает указатель va_start, который ранее был задействован в макросах va_start и va_arg.</p>
<p>Перепишем предыдущий пример с использованием этих макрокоманд:</p>
<pre class="brush:c;">
#include &lt;stdio.h&gt;
#include &lt;stdarg.h&gt;

int sum(int n, ...)
{
	int result = 0;
	va_list factor;			//указатель va_list
	va_start(factor, n);	// устанавливаем указатель
	for(int i=0;i&lt;n; i++)
	{
		result += va_arg(factor, int);	// получаем значение текущего параметра типа int
	}
	va_end(factor);	// завершаем обработку параметров
	return result;
}

int main(void)
{	
	printf("%d \n", sum(4, 1, 2, 3, 4));
	printf("%d \n", sum(5, 12, 21, 13, 4, 5));
	return 0;
}
</pre>
<p>В функции <code>sum()</code> вначале определяется указатель <code>va_list factor;</code>.</p>
<p>Далее связываем этот указатель с первым необязательным параметром: <code>va_start(factor, n);</code>.</p>
<p>В цикле пробегаемся по всем необязательным параметрам и их значение прибавляем к переменной result: <code>result += va_arg(factor, int);</code></p>
<p>В конце завершаем обработку параметров: <code>va_end(factor);</code>.</p>
<p>Результат этой программы будет тот же, что и в предыдущем случае. Но здесь опять же нам надо передавать количество необязательных параметров в качестве первого параметра функции sum. 
И, кроме того, мы точно знаем, что необязательные параметры имеют тип int.</p>
<p>Но стоит отметить, что используемые нами функции ввода-вывода <span class="b">printf()</span> и <span class="b">scanf()</span> то же имеют неопределенное 
число параметров, но их типы также неопределены:</p>
<pre class="brush:c;">
int printf(const char* format, ...);
int scanf(const char* format, ...);
</pre>
<p>Для идентификации типов аргументов параметр format использует спецификаторы %d, %c и так далее. Например, определим собстенную функцию, которая 
будет выводит текст на экран, принимая параметры разных типов:</p>
<pre class="brush:c;">
#include &lt;stdio.h&gt;
#include &lt;stdarg.h&gt;

void display(char* format, ...)
{
	int d; 
	double f;
	va_list factor;			// указатель на необязательный параметр
	va_start(factor, format);	// устанавливаем указатель
	
	for(char *c = format;*c; c++)
	{
		if(*c!='%')
		{
			printf("%c", *c);
			continue;
		}
		switch(*++c)
		{
			case 'd': 
				d = va_arg(factor, int);
				printf("%d", d);
				break;
			case 'f': 
				f = va_arg(factor, double);
				printf("%.2lf", f);
				break;
			default:
				printf("%c", *c);
		}
	}
	va_end(factor);
}

int main(void)
{	
	display("Age:%d	\t Weight:%f", 24, 68.4);
	return 0;
}
</pre>
<p>Для упрощения примера здесь взяты только два спецификатора: d (для типа int) и f (для типа double). В самой функции display с помощью 
указателя <code>char *c</code> пробегаемся по всем символам переданной строки format, пока этот указатель не станет указывать на нулевой символ (<code>*c!='\0'</code>). Если символ не равен знаку %, то выводим этот символ. Иначе смотрим, какой символ идет после 
знака % - d или f. В зависимости от этого получаем либо объект int, либо объект double.</p>
