<p>Для ввода данных в консоли может использоваться функция <span class="b">scanf()</span>. Эта функция определена в заголовочном файле <span class="b">stdio.h</span> 
(там же, где и функция printf) и имеет следующее формальное определение:</p>
<pre class="brush:c;">scanf(форматная_строка, аргументы)</pre>
<p>И форматная_строка, и аргументы для функции scanf обязательны.</p>
<p>Форматная_строка содержит спецификации преобразования, которые определяют вводимые данные. Общий вид спецификаций преобразования:</p>
<pre class="brush:c;">% * ширина_поля модификатор спецификатор</pre>
<p>Из этих элементов обязательны только два: знак процента % и спецификатор.</p>
<p>Спецификатор определяет тип вводимых данных:</p>
<ul>
<li><p><span class="b">%c</span>: считывает один символ</p></li>
<li><p><span class="b">%d</span>: считывает десятичное целое число</p></li>
<li><p><span class="b">%i</span>: считывает целое число в любой системе (десятичной, шестнадцатеричной, восьмеричной)</p></li>
<li><p><span class="b">%u</span>: считывает положительное целое число</p></li>
<li><p><span class="b">%e</span>: считывает число с плавающей точкой в экспоненциальной форме</p></li>
<li><p><span class="b">%E</span>: считывает число с плавающей точкой в экспоненциальной форме с заглавным символом экспоненты</p></li>
<li><p><span class="b">%f</span>: считывает число с плавающей точкой</p></li>
<li><p><span class="b">%F</span>: считывает число с плавающей точкой</p></li>
<li><p><span class="b">%g</span>: считывает число с плавающей точкой</p></li>
<li><p><span class="b">%G</span>: считывает число с плавающей точкой</p></li>
<li><p><span class="b">%o</span>: считывает восьмеричное число</p></li>
<li><p><span class="b">%x</span>: считывает шестнадцатеричное число</p></li>
<li><p><span class="b">%X</span>: считывает шестнадцатеричное число</p></li>
<li><p><span class="b">%s</span>: считывает строку</p></li>
<li><p><span class="b">%%</span>: считывает символ процента</p></li>
</ul>
<p>Символ звездочки * в спецификации преобразования позволяет пропустить при вводе водимые символы для типа, указанного через спецификатор.</p>
<p>Ширина_поля представляет целое положительное число, которое позволяет определить, какое количество байтов будет учитываться при вводе.</p>
<p>Модификаторы позволяют конкретизировать тип данных. В частности, есть следующие модификаторы:</p>
<ul>
<li><p><span class="b">h</span>: для ввода значений типа <span class="b">short int</span> (<code>%hd</code>)</p></li>
<li><p><span class="b">l</span>: для ввода значений типа <span class="b">long int</span> (<code>%ld</code>) или <span class="b">double</span> (<code>%lf</code>, <code>%le</code>)</p></li>
<li><p><span class="b">L</span>: для ввода значений типа <span class="b">long double</span> (<code>%Lf</code>, <code>%Le</code>)</p></li>
</ul>
<p>В качестве аргументов в функцию <span class="b">scanf()</span> передаются адреса переменной, которая будет получать введенное значение. 
Для получения адреса переменной перед ее именем ставится знак амперсанда <span class="b">&amp;</span>. Например, если переменная называется <code>age</code>, 
то ее адрес мы можем получить с помощью выражения <code>&amp;age</code>.</p>
<p>Например, введем с консоли числовое значение:</p>
<pre class="brush:c;">
#include &lt;stdio.h&gt;

int main(void)
{
	int age;
	printf("Input your age:");
	scanf("%d", &age);
	
	printf("age = %d", age);
	return 0;
}
</pre>
<p>Здесь вводится значение для переменной age, которая представляет тип <span class="b">int</span>, поэтому в форматную строку в функции scanf 
передается спецификатор <span class="b">%d</span>. Здесь не используется ни символ звездочки, ни ширина поля, ни модификаторы. Вторым параметром идет 
адрес переменной age - <code>&amp;age</code>.</p>
<p>После ввода значения мы можем его использовать, например, вывести на консоль:</p>
<div class="console">
<pre class="consoletext">
Input your age:44
age = 44
</pre>
</div>
<p>Аналогичен будет ввод данных других типов:</p>
<pre class="brush:c;">
#include &lt;stdio.h&gt;

int main(void)
{
	int age;
	char name[10];
	// вводим возраст
	printf("Your age: ");
	scanf("%d", &age);
	// вводим имя
	printf("Your name: ");
	scanf("%10s", &name);
	
	printf("Name: %s \t Age = %d",name,  age);
	return 0;
}
</pre>
<p>Здесь для имени выделяется 10 символов. Теоретически мы можем ввести и большее количество символов, но чтобы только 10 из них учитывались, 
в строку форматирования передается ширина поля, которая представляет 10 символов - <code>%10s</code>. Возможный консольный вывод:</p>
<div class="console">
<pre class="consoletext">
Your age: 24
Your name: Tom
Name: Tom 	 Age = 24
</pre>
</div>
<p>Можно сразу вводить несколько значений. В этом случае в качестве разделителя используется пробел:</p>
<pre class="brush:c;">
#include &lt;stdio.h&gt;

int main(void)
{
	int age;
	char name[10];
	float weight;
	scanf("%d %s %f", &age, &name, &weight);
	
	printf("Name: %s \t Age = %d \t Weight = %f",name,  age, weight);
	return 0;
}
</pre>
<p>Консольный ввод-вывод:</p>
<div class="console">
<pre class="consoletext">
23 Tom 55.34
Name: Tom	Age = 23	Weight = 55.340000
</pre>
</div>
