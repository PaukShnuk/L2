<h3>Добавление элементов в вектор</h3>
<p>Для добавления элементов в вектор применяется функция <span class="b">push_back()</span>, в который передается добавляемый элемент:</p>
<pre class="brush:cpp;">
#include &lt;iostream&gt;
#include &lt;vector&gt;
 
int main()
{
	std::vector&lt;int&gt; numbers;		// пустой вектор
	numbers.push_back(5);
	numbers.push_back(3);
	numbers.push_back(10);
	for(int n : numbers)
		cout &lt;&lt; n &lt;&lt; "\t";		// 5	3	10
	
    std::cout &lt;&lt; std::endl;
    return 0;
}
</pre>
<p>Векторы являются динамическими структурами в отличие от массивов, где мы скованы его заданым размером. Поэтому мы можем 
динамически добавлять в вектор новые данные.</p>
<p>Функция <span class="b">emplace_back()</span> выполняет аналогичную задачу - добавляет элемент в конец контейнера:</p>
<pre class="brush:cpp;">
std::vector&lt;int&gt; numbers1 = { 1, 2, 3, 4, 5 };
numbers1.emplace_back(8);	// numbers1 = { 1, 2, 3, 4, 5, 8 };
</pre>
<h3>Добавление элементов на определенную позицию</h3>
<p>Ряд функций позволяет добавлять элементы на определенную позицию.</p>
<ul>
<li><p><span class="b">emplace(pos, value)</span>: вставляет элемент value на позицию, на которую указывает итератор pos</p></li>
<li><p><span class="b">insert(pos, value)</span>: вставляет элемент value на позицию, на которую указывает итератор pos, аналогично функции emplace</p></li>
<li><p><span class="b">insert(pos, n, value)</span>: вставляет n элементов value начиная с позиции, на которую указывает итератор pos</p></li>
<li><p><span class="b">insert(pos, begin, end)</span>: вставляет начиная с позиции, на которую указывает итератор pos, элементы из другого контейнера из 
диапазона между итераторами begin и end</p></li>
<li><p><span class="b">insert(pos, values)</span>: вставляет список значений начиная с позиции, на которую указывает итератор pos</p></li>
</ul>
<p>Функция emplace:</p>
<pre class="brush:cpp;">
std::vector&lt;int&gt; numbers = { 1, 2, 3, 4, 5 };
auto iter = numbers.cbegin();	// константный итератор указывает на первый элемент
numbers.emplace(iter + 2, 8); // добавляем после второго элемента  numbers = { 1, 2, 8, 3, 4, 5};
</pre>
<p>Функция insert:</p>
<pre class="brush:cpp;">
std::vector&lt;int&gt; numbers1 = { 1, 2, 3, 4, 5 };
auto iter1 = numbers1.cbegin();	// константный итератор указывает на первый элемент
numbers1.insert(iter1 + 2, 8); // добавляем после второго элемента  
//numbers1 = { 1, 2, 8, 3, 4, 5};

std::vector&lt;int&gt; numbers2 = { 1, 2, 3, 4, 5 };
auto iter2 = numbers2.cbegin();	// константный итератор указывает на первый элемент
numbers2.insert(iter2 + 1, 3, 4); // добавляем после первого элемента три четверки  
//numbers2 = { 1, 4, 4, 4, 2, 3, 4, 5};

std::vector&lt;int&gt; values = { 10, 20, 30, 40, 50 };
std::vector&lt;int&gt; numbers3 = { 1, 2, 3, 4, 5 };
auto iter3 = numbers3.cbegin();	// константный итератор указывает на первый элемент
// добавляем после первого элемента три первых элемента из вектора values
numbers3.insert(iter3 + 1, values.begin(), values.begin() + 3);
//numbers3 = { 1, 10, 20, 30, 2, 3, 4, 5};

std::vector&lt;int&gt; numbers4 = { 1, 2, 3, 4, 5 };
auto iter4 = numbers4.cend();	// константный итератор указывает на позицию за последним элементом
// добавляем в конец вектора numbers4 элементы из списка { 21, 22, 23 }
numbers4.insert(iter4, { 21, 22, 23 });
//numbers4 = { 1, 2, 3, 4, 5, 21, 22, 23};
</pre>
<h3>Удаление элементов</h3>
<p>Если необходимо удалить все элементы вектора, то можно использовать функцию <span class="b">clear</span>:</p>
<pre class="brush:cpp;">
std::vector&lt;int&gt; v = { 1,2,3,4 };
v.clear();
</pre>
<p>Функция <span class="b">pop_back()</span> удаляет последний элемент вектора:</p>
<pre class="brush:cpp;">
std::vector&lt;int&gt; v = { 1,2,3,4 };
v.pop_back();		// v = { 1,2,3 }
</pre>
<p>Если нужно удалить элемент из середины или начала контейнера, применяется функция <span class="b">erase()</span>, которая имеет следующие формы:</p>
<ul>
<li><p><span class="b">erase(p)</span>: удаляет элемент, на который указывает итератор p. Возвращает итератор на элемент, следующий после удаленного, 
или на конец контейнера, если удален последний элемент</p></li>
<li><p><span class="b">erase(begin, end)</span>: удаляет элементы из диапазона, на начало и конец которого указывают итераторы begin и end. 
Возвращает итератор на элемент, следующий после последнего удаленного, или на конец контейнера, если удален последний элемент</p></li>
</ul>
<p>Применение функции:</p>
<pre class="brush:cpp;">
std::vector&lt;int&gt; numbers1 = { 1, 2, 3, 4, 5, 6 };
auto iter = numbers1.cbegin(); // указатель на первый элемент
numbers1.erase(iter + 2);	// удаляем третий элемент
// numbers1 = { 1, 2, 4, 5, 6 }

std::vector&lt;int&gt; numbers2 = { 1, 2, 3, 4, 5, 6 };
auto begin = numbers2.cbegin(); // указатель на первый элемент
auto end = numbers2.cend();		// указатель на последний элемент
numbers2.erase(begin + 2, end - 1);	// удаляем с третьего элемента до последнего
// numbers2 = {1, 2, 6}
</pre>
<h3>Размер вектора</h3>
<p>С помощью функции <span class="b">size()</span> можно узнать размер вектора, а с помощью функции <span class="b">empty()</span> 
проверить, путой ли вектор:</p>
<pre class="brush:cpp;">
#include &lt;iostream&gt;
#include &lt;vector&gt;
using std::vector;
using std::cout;
using std::endl;
 
int main()
{
	vector&lt;int&gt; numbers = {1, 2, 3};
	if(numbers.empty())
		cout &lt;&lt; "Vector is empty" &lt;&lt; endl;
	else
		cout &lt;&lt; "Vector has size " &lt;&lt; numbers.size() &lt;&lt; endl;
	
    return 0;
}
</pre>
<p>С помощью функции <span class="b">resize()</span> можно изменить размер вектора. Эта функция имеет две формы:</p>
<ul>
<li><p><span class="b">resize(n)</span>: оставляет в векторе n первых элементов. Если вектор содержит больше элементов, то его размер усекается до n элементов. 
Если размер вектора меньше n, то добавляются недостающие элементы и инициализируются значением по умолчанию</p></li>
<li><p><span class="b">resize(n, value)</span>: также оставляет в векторе n первых элементов. Если размер вектора меньше n, то добавляются 
недостающие элементы со значением value</p></li>
</ul>
<p>Применение функции:</p>
<pre class="brush:cpp;">
std::vector&lt;int&gt; numbers1 = { 1, 2, 3, 4, 5, 6 };
numbers1.resize(4);	// оставляем первые четыре элемента - numbers1 = {1, 2, 3, 4}

numbers1.resize(6, 8);	 // numbers1 = {1, 2, 3, 4, 8, 8}
</pre>
<p>Важно учитывать, что применение функции resize может сделать некорректными все итераторы, указатели и ссылки на элементы.</p>
<h3>Изменение элементов вектора</h3>
<p>Функция <span class="b">assign()</span> позволяет заменить все элементы вектора определенным набором:</p>
<pre class="brush:cpp;">
std::vector&lt;std::string&gt; names = { "Tom", "Bob", "Kate"};
names.assign(4, "Sam");	// numbers = {"Sam", "Sam", "Sam", "Sam"}
</pre>
<p>В данном случае элементы вектора заменяются набором из четырех строк "Sam".</p>
<p>Еще одна функция - <span class="b">swap()</span> обменивает значения двух контейнеров:</p>
<pre class="brush:cpp;">
std::vector&lt;std::string&gt; clangs = { "C++", "C#", "Java" };
std::vector&lt;std::string&gt; ilangs = { "JavaScript", "Python", "PHP"};
clangs.swap(ilangs);	// clangs = { "JavaScript", "Python", "PHP"};
for(std::string n : clangs)
	std::cout &lt;&lt; n &lt;&lt; "\t";
std::cout &lt;&lt; std::endl;
</pre>
<h3>Сравнение векторов</h3>
<p>Векторы можно сравнивать. Сравнение контейнеров осуществляется на основании сравнения пар элементов на тех же позициях. 
Векторы равны, если они содержат одинаковые элементы на тех же позициях. Иначе они не равны:</p>
<pre class="brush:cpp;">
std::vector&lt;int&gt; v1 = {1, 2, 3};
std::vector&lt;int&gt; v2 = {1, 2, 3};
std::vector&lt;int&gt; v3 = {3, 2, 1};
	
bool v1v2 = v1 == v2; 	// true
bool v1v3 = v1 != v3;	// true
bool v2v3 = v2 == v3;	// false
</pre>
