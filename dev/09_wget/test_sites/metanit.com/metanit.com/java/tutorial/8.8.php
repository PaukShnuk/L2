<p>Класс <span class="b">Phaser</span> позволяет синхронизировать потоки, представляющие отдельную фазу или стадию выполнения общего действия. 
Phaser определяет объект синхронизации, который ждет, пока не завершится определенная фаза. Затем Phaser переходит к следующей стадии или фазе и снова ожидает ее завершения.</p>
<p>Для создания объекта Phaser используется один из конструкторов:</p>
<pre class="brush:java;">
Phaser()
Phaser(int parties)
Phaser(Phaser parent)
Phaser(Phaser parent, int parties)
</pre>
<p>Параметр <code>parties</code> указывает на количество участников (грубо говоря, потоков), которые должны выполнять все фазы действия. Первый конструктор создает объект Phaser 
без каких-либо участников. Второй конструктор регистрирует передаваемое в конструктор количество участников. Третий и четвертый конструкторы также устанавливают родительский объект Phaser.</p>
<p>Основные методы класса Phaser:</p>
<ul>
<li><p><span class="b">int register()</span>: регистрирует участника, который выполняет фазы, и возвращает номер текущей фазы - обычно фаза 0</p></li>
<li><p><span class="b">int arrive()</span>: сообщает, что участник завершил фазу, и возвращает номер текущей фазы</p></li>
<li><p><span class="b">int arriveAndAwaitAdvance()</span>: аналогичен методу arrive, только при этом заставляет phaser ожидать завершения фазы 
всеми остальными участниками</p></li>
<li><p><span class="b">int arriveAndDeregister()</span>: сообщает о завершении всех фаз участником и снимает его с регистрации. Возвращает номер текущей фазы или отрицательное число, 
если синхронизатор Phaser завершил свою работу</p></li>
<li><p><span class="b">int getPhase()</span>: возвращает номер текущей фазы</p></li>
</ul>
<p>При работае с классом Phaser обычно сначала создается его объект. Далее нам надо зарегистрировать всех участников. Для регистрации 
для каждоого участника вызывается метод <code>register()</code>, либо можно обойтись и без этого метода, передав нужное количество участников в конструктор Phaser.</p>
<p>Затем каждый участник выполняет некоторый набор действий, составляющих фазу. А синхронизатор Phaser ждет, пока все участники не завершат выполнение фазы. Чтобы сообщить синхронизатору, 
что фаза завершена, участник должен вызвать метод <code>arrive()</code> или <code>arriveAndAwaitAdvance()</code>. После этого синхронизатор переходит к следующей фазе.</p>
<p>Применим Phaser в приложении:</p>
<pre class="brush:java;">
import java.util.concurrent.Phaser;

public class Program {

    public static void main(String[] args) {
        
        Phaser phaser = new Phaser(1);
        new Thread(new PhaseThread(phaser, "PhaseThread 1")).start();
        new Thread(new PhaseThread(phaser, "PhaseThread 2")).start();
        
        // ждем завершения фазы 0
        int phase = phaser.getPhase();
        phaser.arriveAndAwaitAdvance();
        System.out.println("Фаза " + phase + " завершена");
        // ждем завершения фазы 1
        phase = phaser.getPhase();
        phaser.arriveAndAwaitAdvance();
        System.out.println("Фаза " + phase + " завершена");
        
        // ждем завершения фазы 2
        phase = phaser.getPhase();
        phaser.arriveAndAwaitAdvance();
        System.out.println("Фаза " + phase + " завершена");
        
        phaser.arriveAndDeregister();
    }
}

class PhaseThread implements Runnable{
    
    Phaser phaser;
    String name;

    PhaseThread(Phaser p, String n){
        
        this.phaser=p;
        this.name=n;
        phaser.register();
    }
    public void run(){
        
        System.out.println(name + " выполняет фазу " + phaser.getPhase());
        phaser.arriveAndAwaitAdvance(); // сообщаем, что первая фаза достигнута
        
        System.out.println(name + " выполняет фазу " + phaser.getPhase());
        phaser.arriveAndAwaitAdvance(); // сообщаем, что вторая фаза достигнута

        System.out.println(name + " выполняет фазу " + phaser.getPhase());
        phaser.arriveAndDeregister(); // сообщаем о завершении фаз и удаляем с регистрации объекты 
    }
}
</pre>
<p>Итак, здесь у нас фазы выполняются тремя участниками - главным потоком и двумя потоками PhaseThread. Поэтому при создании объекта Phaser ему передается 
число 1 - главный поток, а в конструкторе PhaseThread вызывается метод <code>register()</code>. Мы в принципе могли бы не использовать метод register, 
но тогда нам надо было бы указать <code>Phaser phaser = new Phaser(3)</code>, так как у нас три участника.</p>
<p>Фаза для каждого участника представляет минимальный примитивный набор действий: для потоков PhaseThread это вывод сообщения, а для главного потока - подсчет текущей фазы с помощью метода 
<code>getPhase()</code>. При этом отсчет фаз начинается с нуля. Каждый участник завершает выполнение фазы вызовом метода 
<code>phaser.arriveAndAwaitAdvance()</code>. При вызове этого метода пока последний участник не завершит выполнение текущей фазы, все остальные 
участники блокируются.</p>
<p>После завершения выполнения последней фазы происходит отмена регистрации всех участников с помощью метода <code>arriveAndDeregister()</code>.</p>
<p>В итоге работа программы даст следующий вывод:</p>
<div class="console">
<pre class="consoletext">
PhaseThread 1 выполняет фазу 0
PhaseThread 2 выполняет фазу 0
PhaseThread 1 выполняет фазу 1
PhaseThread 2 выполняет фазу 1
Фаза 0 завершена
Фаза 1 завершена
PhaseThread 1 выполняет фазу 2
PhaseThread 2 выполняет фазу 2
Фаза 2 завершена
</pre>
</div>
<p>В данном случае получается немного путанный вывод. Так, сообщения о выполнении фазы 1 выводится после сообщения об окончании фазы 0. Что связано с многопоточностью - 
фазы завершились, но в одном потоке еще не выведено сообщение о завершении, тогда как другие потоки уже начали выполнение следующей фазы. В любом случае все это происходит уже после завершения фазы.</p>
<p>Но чтобы было более наглядно, мы можем использовать sleep в потоках:</p>
<pre class="brush:java;">
public void run(){
        
        System.out.println(name + " выполняет фазу " + phaser.getPhase());
        phaser.arriveAndAwaitAdvance(); // сообщаем, что первая фаза достигнута
        try{
            Thread.sleep(200);
        }
        catch(InterruptedException ex){
            System.out.println(ex.getMessage());
        }
        
        System.out.println(name + " выполняет фазу " + phaser.getPhase());
        phaser.arriveAndAwaitAdvance(); // сообщаем, что вторая фаза достигнута
        try{
            Thread.sleep(200);
        }
        catch(InterruptedException ex){
            System.out.println(ex.getMessage());
        }
        System.out.println(name + " выполняет фазу " + phaser.getPhase());
        phaser.arriveAndDeregister(); // сообщаем о завершении фаз и удаляем с регистрации объекты 
    }
</pre>
<p>И в этом случае вывод будет более привычным, хотя на работу фаз это никак не повлияет.</p>
