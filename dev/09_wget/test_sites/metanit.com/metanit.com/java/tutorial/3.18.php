<p>Начиная с версии Java 16 в язык была добавлена новая функциональность - <span class="b">Records</span> (на русском нередко называют "записями"). 
<span class="b">Records</span> представляют классы, которые предназначены для создания контейнеров неизменяемых данных. Кроме того, records позволяют упростить разработку, 
сократив объем кода.</p>
<p>Для определения классов record применяется ключевое слово <span class="b">record</span>, после которого идет название и далее в круглых 
скобках список полей record:</p>
<pre class="brush:java;">record название (поле1, поле2,...полеN){
	// тело record
}
</pre>
<p>Рассмотрим следующий пример</p>
<pre class="brush:java;">
import java.util.Objects;

public class Program{ 
      
    public static void main (String args[]){
          
		Person tom = new Person("Tom", 36);
        System.out.println(tom.toString());
    }
}

class Person {
    private final String name;
    private final int age;

    Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    String name() { return name; }
    int age() { return age; }

    public boolean equals(Object o) {
        if (!(o instanceof Person)) return false;
        Person other = (Person) o;
        return other.name == name && other.age == age;
    }

    public int hashCode() {
        return Objects.hash(name, age);
    }

    public String toString() {
        return String.format("Person[name=%s, age=%d]", name, age);
    }
}
</pre>
<p>Здесь определен класс Person, который определяет две константы - name и age:</p>
<pre class="brush:java;">
private final String name;
private final int age;
</pre>
<p>Их значения устанавливаются в конструкторе. Больше никак их установить мы не можем. Таким образом, после создания объекта Person они будут 
хранить неизменяемые данные.</p>
<p>Для получения значений name и age предусмотрены одноименные методы:</p>
<pre class="brush:java;">
String name() { return name; }
int age() { return age; }
</pre>
<p>Кроме того, здесь переопределены унаследованные от класса Object методы <code>equals()</code>, <code>hashCode()</code> и <code>toString()</code>.</p>
<p>В методе <code>main</code> создаем один объект класса Person и выводит на консоль его текстовое представление:</p>
<pre class="brush:java;">
Person tom = new Person("Tom", 36);
 System.out.println(tom.toString());
</pre>
<p>В итоге консоль нам выведет:</p>
<div class="console"><pre class="consoletext">Person[name=Tom, age=36]</pre></div>
<p>Теперь посмотрим, что нам предлагают <span class="b">Records</span> - определим record, которая будет полностью аналогична вышеопределенному классу:</p>
<pre class="brush:java;">
public class Program{ 
      
    public static void main (String args[]){
          
		Person tom = new Person("Tom", 36);
        System.out.println(tom.toString());
    }
}
record Person(String name, int age) { }
</pre>
<p>Records определяются с помощью ключевого слова <span class="b">record</span>, за которым следует название записи. Дальше идет список полей записи. То есть в данном случае 
определяется два поля - name и age. Причем по умолчанию все они будут приватными и иметь модификатор <span class="b">final</span>.</p> 
<p>Также будет создаваться конструктор с двумя параметрами name и age. А каждого поля автоматически будет создаваться одноименный общедоступный метод для получения значения это поля. Например, для поля <code>name</code> создается 
метод <code>name()</code>, который возвращает значение поля name.</p>
<p>И также автоматически будут создаваться методы <code>equals, hashCode и toString</code>. Вообщем, данная record будет полностью аналогична вышеопределенному классу, но при этом содержит гораздо меньше 
кода.</p>
<p>При необходимости мы можем вызывать все имеющиеся методы:</p>
<pre class="brush:java;">
public class Program{ 
      
    public static void main (String args[]){
          
		Person tom = new Person("Tom",  36);
		
        System.out.println(tom.name());		// Tom
        System.out.println(tom.age());		// 36
        System.out.println(tom.hashCode());
		
		Person bob = new Person("Bob", 21);
		Person tomas = new Person("Tom", 36);
		System.out.println(tom.equals(bob));	// false
		System.out.println(tom.equals(tomas));	// true
    }
}
record Person(String name, int age){ }
</pre>
<h3>Конструктор record</h3>
<p>В примере выше применялась форма record:</p>
<pre class="brush:java;">
record Person(String name, int age) { }
</pre>
<p>которая фактически создавала конструктор:</p>
<pre class="brush:java;">
Person(String name, int age) { 
	this.name = name;
	this.age = age;
}
</pre>
<p>Этот конструктор называется <span class="b">каноническим</span>. Он принимает параметры, которые называются также как и поля record, и передает полям значения соответствующих параметров.</p>
<p>Тем не менее при необходимости мы можем изменить логику конструктора. Так, мы можем переопределить логику конструктора. Например, что если при создании объекта 
будет передан невалидный возраст? Предусмотрим эту ситуацию, переопределив логику конструктора:</p>
<pre class="brush:java;">
public class Program{ 
      
    public static void main (String args[]){
          
		Person tom = new Person("Tom", -116);
        System.out.println(tom.toString());
    }
}
record Person(String name, int age) { 

	Person{
		
		if(age&lt;1 || age &gt; 110){
			age = 18;
		}
	}
}
</pre>
<p>В данном случае если передано невалидное значение, то применяем некоторое значение по умолчанию (число 18). В итоге фактически мы получим 
конструктор со следующим действием:</p>
<pre class="brush:java;">
Person(String name, int age) { 
	if(age&lt;1 || age &gt; 110){
		age = 18;
	}
	this.name = name;
	this.age = age;
}
</pre>
<p>В итоге программа выведет на консоль следующее:</p>
<div class="console"><pre class="consoletext">Person[name=Tom, age=18]</pre></div>
<p>Мы можем полностью переопределить канонический конструктор:</p>
<pre class="brush:java;">
public class Program{ 
      
    public static void main (String args[]){
          
		Person tom = new Person("Tom",  36);
        System.out.println(tom.toString());
        System.out.println(tom.name());
    }
}
record Person(String name, int age) { 

	Person(String name, int age){
		
		if(age &lt; 0 || age &gt; 120) age = 18;
		
		this.name = name;
		this.age = age;
	}
}
</pre>
<p>Также мы можем определять какие-то другие конструкторы, но все они должны вызывать канонический конструктор:</p>
<pre class="brush:java;">
public class Program{ 
      
    public static void main (String args[]){
          
		Person tom = new Person("Tom", "Smith",  36);
        System.out.println(tom.toString());
    }
}
record Person(String name, int age) { 

	Person(String firstName, String lastName, int age){
		
		this(firstName + " " + lastName, age);
		
	}
}
</pre>
<p>Здесь определен конструктор, который условно принимает имя, фамилию и возраст пользователя. Этот конструктор вызывает канонический конструктор, 
передая ему значения для полей name и age: <code>this(firstName + " " + lastName, age)</code>.</p>
<p>Консольный вывод программы:</p>
<div class="console"><pre class="consoletext">Person[name=Tom Smith, age=36]</pre></div>
<h3>Переопределение методов</h3>
<p>Также мы можем переопределить методы, которые имеет record по умолчанию. А это методы <code>equals()</code>, <code>hashCode()</code> и 
<code>toString()</code> и методы, которые называются также, как и поля записи, и которые возвращают значения соответствующих полей.</p>
<p>Например, перепопределим для записи Person методы <code>toString()</code> и <code>name()</code>:</p>
<pre class="brush:java;">
public class Program{ 
      
    public static void main (String args[]){
          
		Person tom = new Person("Tom", 36);
        System.out.println(tom.toString());
        System.out.println(tom.name());
    }
}
record Person(String name, int age) { 

	public String name() { return "Mister " + name; }
	
    public String toString() {
        return String.format("Person %s, Age: %d", name, age);
    }
}
</pre>
<p>Консольный вывод программ:</p>
<div class="console"><pre class="consoletext">
Person Tom, Age: 36
Mister Tom
</pre></div>
<h3>Ограничения records</h3>
<p>Следует учитывать, что мы не можем наследовать запись record от других классов. Также нельзя наследовать классы от records. Однако классы record 
могут реализовать интерфейсы. Кроме того, классы record не могут быть абстрактными.</p>
<p>В record нельзя явным образом определять нестатические поля и инициализаторы. Но можно определять статические переменные и инициализаторы, 
также как статические и нестатические методы:</p>
<pre class="brush:java;">
record Person(String name, int age){ 

	static int minAge;
	static{
        minAge = 18;
        System.out.println("Static initializer");
    }
}
</pre>

