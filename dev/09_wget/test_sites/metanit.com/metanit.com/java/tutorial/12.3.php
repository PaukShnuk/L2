<p>Класс <span class="b">LocalDate</span> из пакета <span class="b">java.time</span> предназначен для работы с датами. Функционал этого класса 
позволяет создавать даты и изменять их, добавляя и отнимая необходимое количество дней/месяцев/лет.</p>
<p>Основные методы LocalDate:</p>
<ul>
<li><p><code>static LocalDate now()</code>: возвращает объект, который представляет текущую дату</p></li>
<li><p><code>static LocalDate of(int year, int month, int day)</code>: возвращает объект, который представляет дату с определенными годом, месяцем и днем</p></li>
<li><p><code>int getYear()</code>: возвращает год даты</p></li>
<li><p><code>int getMonthValue()</code>: возвращает месяц</p></li>
<li><p><code>int getDayOfMonth()</code>: возвращает день месяца (значение от 1 до 31)</p></li>
<li><p><code>int getDayOfYear()</code>: возвращает номер дня года (значение от 1 до 365)</p></li>
<li><p><code>DayOfWeek getDayOfWeek()</code>: возвращает день недели в виде значения перечисления <span class="b">DayOfWeek</span></p></li>
<li><p><code>LocalDate plusDays(int n)</code>: добавляет к дате некоторое количество дней</p></li>
<li><p><code>LocalDate plusWeeks(int n)</code>: добавляет к дате некоторое количество недель</p></li>
<li><p><code>LocalDate plusMonths(int n)</code>: добавляет к дате некоторое количество месяцев</p></li>
<li><p><code>LocalDate plusYears(int n)</code>: добавляет к дате некоторое количество лет</p></li>
<li><p><code>LocalDate minusDays(int n)</code>: отнимает от даты некоторое количество дней</p></li>
<li><p><code>LocalDate minusMonths(int n)</code>: отнимает от даты некоторое количество месяцев</p></li>
<li><p><code>LocalDate minusWeeks(int n)</code>: отнимает от даты некоторое количество недель</p></li>
<li><p><code>LocalDate minusYears(int n)</code>: отнимает от даты некоторое количество лет</p></li>
</ul>
<p>Получение информации о текущей дате:</p>
<pre class="brush:java;">
import java.time.DayOfWeek;
import java.time.LocalDate;

public class Program {
  
    public static void main(String[] args) {
        
        LocalDate date = LocalDate.now(); // получаем текущую дату
        int year = date.getYear();
        int month = date.getMonthValue();
        int dayOfMonth = date.getDayOfMonth();
        DayOfWeek dayOfWeek = date.getDayOfWeek();
        System.out.println(date);
        System.out.println(dayOfWeek);
        System.out.printf("%d.%d.%d \n", dayOfMonth, month, year);
    }
}
</pre>
<p>Консольный вывод:</p>
<div class="console">
<pre class="consoletext">
2018-11-11
SUNDAY
11.11.2018 
</pre>
</div>
<p>Создание и получение информации по случайной дате:</p>
<pre class="brush:java;">
LocalDate date = LocalDate.of(1914, 12, 31);
System.out.println(date);
</pre>
<p>Операции с датами - добавление и удаление:</p>
<pre class="brush:java;">
LocalDate date = LocalDate.of(1914, 7, 28);
        
date = date.plusYears(4);
date = date.plusMonths(3);
date = date.plusDays(14);
System.out.println(date);   // 1918-11-11
        
date = date.minusMonths(10);
date = date.minusDays(3);
System.out.println(date);   // 1918-01-08
</pre>
