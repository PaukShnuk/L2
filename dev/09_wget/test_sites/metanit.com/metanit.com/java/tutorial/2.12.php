<p>Одной из основных особенностей Java является то, что данный язык является строго типизированным. А это значит, что каждая переменная и константа 
представляет определенный тип и данный тип строго определен. Тип данных определяет диапазон значений, которые может хранить переменная или константа.</p>
<p>Итак, рассмотрим систему встроенных базовых типов данных, которая используется для создания переменных в Java. А она представлена следующими типами.</p>
<ul>
<li><p><span class="b">boolean</span>: хранит значение <code>true</code> или <code>false</code></p>
<pre class="brush:java;">
boolean isActive = false;
boolean isAlive = true;
</pre></li>
<li><p><span class="b">byte</span>: хранит целое число от <code>-128</code> до <code>127</code> и занимает 1 байт</p>
<pre class="brush:java;">
byte a = 3;
byte b = 8;
</pre>
</li>
<li><p><span class="b">short</span>: хранит целое число от <code>-32768</code> до <code>32767</code> и занимает 2 байта</p>
<pre class="brush:java;">
short a = 3;
short b = 8;
</pre>
</li>
<li><p><span class="b">int</span>: хранит целое число от <code>-2147483648</code> до <code>2147483647</code> и занимает 4 байта</p>
<pre class="brush:java;">
int a = 4;
int b = 9;
</pre>
</li>
<li><p><span class="b">long</span>: хранит целое число от <code>–9 223 372 036 854 775 808</code> до <code>9 223 372 036 854 775 807</code> и занимает 
8 байт</p>
<pre class="brush:java;">
long a = 5;
long b = 10;
</pre>
</li>
<li><p><span class="b">double</span>: хранит число с плавающей точкой от <code>±4.9*10<sup>-324</sup></code> до <code>±1.8*10<sup>308</sup></code> и 
занимает 8 байт</code></p>
<pre class="brush:java;">
double x = 8.5;
double y = 2.7;
</pre>
<p>В качестве разделителя целой и дробной части в дробных литералах используется точка.</p>
</li>
<li><p><span class="b">float</span>: хранит число с плавающей точкой от <code>-3.4*10<sup>38</sup></code> до <code>3.4*10<sup>38</sup></code> и 
занимает 4 байта</p>
<pre class="brush:java;">
float x = 8.5F;
float y = 2.7F;
</pre>
</li>
<li><p><span class="b">char</span>: хранит одиночный символ в кодировке UTF-16 и занимает 2 байта, поэтому диапазон хранимых значений от 
<code>0</code> до <code>65535</code></p></li>
</ul>
<p>При этом переменная может принимать только те значения, которые соответствуют ее типу. Если переменная представляет целочисленный тип, то она не может хранить дробные числа.</p>
<h3>Целые числа</h3>
<p>Все целочисленные литералы, например, числа 10, 4, -5, воспринимаются как значения типа <span class="b">int</span>, однако мы можем присваивать целочисленные литералы 
другим целочисленным типам: <span class="b">byte</span>, <span class="b">long</span>, <span class="b">short</span>. В этом случае Java автоматически осуществляет соответствующие 
преобразования:</p>
<pre class="brush:java;">
byte a = 1;
short b = 2;
long c = 2121;
</pre>
<p>Однако если мы захотим присвоить переменной типа long очень большое число, которое выходит за пределы допустимых значений для типа int, 
то мы столкнемся с ошибкой во время компиляции:</p>
<pre class="brush:java;">long num = 2147483649;</pre>
<p>Здесь число 2147483649 является допустимым для типа long, но выходит за предельные значения для типа int. И так как все целочисленные 
значения по умолчанию расцениваются как значения типа int, то компилятор укажет нам на ошибку. Чтобы решить проблему, надо добавить к числу суффикс 
<span class="b">l</span> или <span class="b">L</span>, который указывает, что число представляет тип long:</p>
<pre class="brush:java;">long num = 2147483649L;</pre>
<p>Как правило, значения для целочисленных переменных задаются в десятичной системе счисления, однако мы можем применять и другие системы счисления. 
Например:</p>
<pre class="brush:java;">
int num111 = 0x6F; // 16-тиричная система, число 111
int num8 = 010; // 8-ричная система, число 8
int num13 = 0b1101; // 2-ичная система, число 13
</pre>
<p>Для задания шестнадцатеричного значения после символов <span class="b">0x</span> указывается число в шестнадцатеричном формате. 
Таким же образом восьмеричное значение указывается после символа <span class="b">0</span>, а двоичное значение - после символов <span class="b">0b</span>.</p>
<p>Также целые числа поддерживают разделение разрядов числа с помощью знака подчеркивания:</p>
<pre class="brush:java;">
int x = 123_456;
int y = 234_567__789;
System.out.println(x); 	// 123456
System.out.println(y); 	// 234567789
</pre>
<h4>Числа с плавающей точкой</h4>
<p>При присвоении переменной типа float дробного литерала с плавающей точкой, например, 3.1, 4.5 и т.д., Java автоматически рассматривает этот литерал как значение типа <code>double</code>. 
И чтобы указать, что данное значение должно рассматриваться как <code>float</code>, нам надо использовать суффикс f:</p>
<pre class="brush:java;">
float fl = 30.6f;
double db = 30.6;
</pre>
<p>И хотя в данном случае обе переменных имеют практически одно значения, но эти значения будут по-разному рассматриваться и будут занимать разное место в памяти.</p>
<h3>Символы и строки</h3>
<p>В качестве значения переменная символьного типа получает одиночный символ, заключенный в одинарные кавычки: <code>char ch='e';</code>. 
Кроме того, переменной символьного типа также можно присвоить целочисленное значение от <code>0</code> до <code>65535</code>. В этом случае 
переменная опять же будет хранить символ, а целочисленное значение будет указывать на номер символа в таблице символов Unicode (UTF-16). Например:</p>
<pre class="brush:java;">
char ch=102; // символ 'f'
System.out.println(ch);
</pre>
<p>Еще одной формой задания символьных переменных является шестнадцатеричная форма: переменная получает значение в шестнадцатеричной форме, 
которое следует после символов "\u". Например, <code>char ch='\u0066';</code> опять же будет хранить символ 'f'.</p>
<p>Символьные переменные не стоит путать со строковыми, 'a' не идентично "a". Строковые переменные представляют объект <code>String</code>, который 
в отличие от char или int не является примитивным типом в Java:</p>
<pre class="brush:java;">
String hello = "Hello...";
System.out.println(hello);
</pre>
<p>Кроме собствено символов, которые представляют буквы, цифры, знаки препинания, прочие символы, есть специальные наборы символов, которые 
называют управляющими последовательностями. Например, самая популярная последовательность - "\n". Она выполняет перенос на следующую строку. Например:</p>
<pre class="brush:java;">
String text = "Hello \nworld";
System.out.println(text);
</pre>
<p>Результат выполнения данного кода:</p>
<div class="console">
<pre class="consoletext">
Hello 
world
</pre>
</div>
<p>В данном случае последовательность \n будет сигналом, что необходимо сделать перевод на следующую строку.</p>
<p>Начиная с версии 15 Java поддерживает тестовые блоки (text blocks) - многострочный текст, облеченный в тройные кавычки. Рассмотрим, в чем 
их практическая польза. Например, выведем большой многострочный текст:</p>
<pre class="brush:java;">
String text = "Вот мысль, которой весь я предан,\n"+
                "Итог всего, что ум скопил.\n"+
                "Лишь тот, кем бой за жизнь изведан,\n"+
                "Жизнь и свободу заслужил.";
System.out.println(text);
</pre>
<p>С помощью операции + мы можем присоединить к одному тексту другой, причем продолжение текста может располагаться на следующей строке. 
Чтобы при выводе текста происходил перенос на следующую строку, применяется последовательность \n.</p>
<p>Результат выполнения данного кода:</p>
<div class="console">
<pre class="consoletext">
Вот мысль, которой весь я предан,
Итог всего, что ум скопил.
Лишь тот, кем бой за жизнь изведан,
Жизнь и свободу заслужил.
</pre>
</div>
<p>Текстовые блоки, которые появились в JDK15, позволяют упростить написание многострочного текста:</p>
<pre class="brush:java;">
String text = """
                Вот мысль, которой весь я предан,
                Итог всего, что ум скопил.
                Лишь тот, кем бой за жизнь изведан,
                Жизнь и свободу заслужил.
                """;
System.out.println(text);
</pre>
<p>Весь текстовый блок оборачивается в тройные кавычки, при этом не надо использовать соединение строк или последовательность \n для их переноса. Результат 
выполнения программы будет тем же, что и в примере выше.</p>
