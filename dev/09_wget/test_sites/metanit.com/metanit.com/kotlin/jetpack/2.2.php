<p>Контейнер <span class="b">Column</span> позволяет выстроить вложенные компоненты в столбик. Функция <span class="b">Column</span> принимает четыре параметра:</p>
<pre class="brush:kt;">
@Composable
inline fun Column(
    modifier: Modifier = Modifier,
    verticalArrangement: Arrangement.Vertical = Arrangement.Top,
    horizontalAlignment: Alignment.Horizontal = Alignment.Start,
    content: ColumnScope.() -&gt; Unit
): @Composable Unit
</pre>
<ul>
<li><p><span class="b">modifier</span>: объект <span class="b">Modifier</span>, который позволяет настроить внешний вид и поведение компонента</p></li>
<li><p><span class="b">verticalArrangement</span>: объект <span class="b">Arrangement.Vertical</span>, который устанавливает выравнивание компонента по вертикали. 
По умолчанию имеет значение <code>Arrangement.Top</code> (расположение в верху)</p></li>
<li><p><span class="b">horizontalAlignment</span>: объект <span class="b">Alignment.Horizontal</span>, который устанавливает выравнивание компонента по 
горизонтали. По умолчанию имеет значение <code>Alignment.Start</code> (расположение в начале - слева для языков с левосторонним письмом и справа для языков с правосторонним письмом)</p></li>
<li><p><span class="b">content</span>: объект интерфейса <span class="b">BoxScope</span>, который представляет вложенное содержимое</p></li>
</ul>
<p>Так, изменим код <span class="b">MainActivity.kt</span> следующим образом:</p>
<pre class="brush:kt;">
package com.example.helloapp

import android.os.Bundle
import androidx.activity.ComponentActivity
import androidx.activity.compose.setContent
import androidx.compose.foundation.layout.Column
import androidx.compose.material.Text
import androidx.compose.ui.text.TextStyle
import androidx.compose.ui.unit.sp

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            Column {
                Text("Hello", style = TextStyle(fontSize = 22.sp))
                Text("World", style = TextStyle(fontSize = 22.sp))
                Text("from", style = TextStyle(fontSize = 22.sp))
                Text("Jetpack Compose", style = TextStyle(fontSize = 22.sp))
            }
        }
    }
}
</pre>
