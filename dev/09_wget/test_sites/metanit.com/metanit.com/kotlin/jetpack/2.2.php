<h3>Установка веса компонентов</h3>
<p>Контейнер <span class="b">Column</span> позволяет назначить вложенным компонентам высоту в соответствии с их весом. Для указания веса 
применяется модификатор <span class="b">ColumnScope.weight</span>. Стоит учитывать, что если контейнер <span class="b">Column</span> обеспечивает вертикальную прокрутку или располагается в контейере, который предполагает вертикальную 
прокрутку, то веса компонентов игнорируются, поскольку общее пространство по вертикали условно бесконечно.</p>
<pre class="brush:kt;">
package com.example.helloapp

import android.os.Bundle
import androidx.activity.ComponentActivity
import androidx.activity.compose.setContent
import androidx.compose.foundation.background
import androidx.compose.foundation.layout.*
import androidx.compose.ui.Modifier
import androidx.compose.ui.graphics.Color

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            Column()
            {
                Box(modifier = Modifier.background(Color.Red).fillMaxWidth().weight(1f))
                Box(modifier = Modifier.background(Color.Yellow).fillMaxWidth().weight(3f))
                Box(modifier = Modifier.background(Color.Green).fillMaxWidth().weight(2f))
            }
        }
    }
}
</pre>
<img src="./pics/2.6.png" alt="ColumnScope weight и вес компонента в Column в Jetpack Compose и Kotlin и Android" />
<p>Обратите внимание, как передается вес:</p>
<pre class="brush:kt;">Modifier.background(Color.Red).fillMaxWidth().weight(1f)</pre>
<p>В качестве веса в функцию <code>weight()</code> фактически передается доля пространства в единицах <span class="b">f</span>. 
Так, первый элемент Box имеет вес <code>1f</code>, второй - <code>3f</code>, третий - <code>2f</code>. Соовокупный вес всех комонентов, таким образом, будет 
1f + 3f + 2f = 6f. И в итоге получится, что первый элемент получит от пространства 1f/6f часть или одну шестую. Второй элемент - 3f/6f или половину простанства, 
а третий - 2f/6f или одну третью пространства. Таким образом будет распределено пространство по вертикали между вложенными элементами.</p>
<p>Однако веса управляют только распределением пространства по вертикали, то есть для установки высоты. Ширина же устанавливается либо исходя из явно 
указанных значений ширины, либо исходя из вложенного содержимого. В данном случае по умолчанию вложенные элементы Box получат нулевую ширину, поэтому, 
чтобы их бло видно, в данном случае растягивает элементы по всей ширине с помощью модификатора <code>fillMaxWidth()</code>.</p>
<p>Стоит отметить, что модификатор <span class="b">weight()</span> может принимать второй параметр - булевое значение, которое указывает, будет ли 
ему выделяться пространство в соответствии с его весом. Если этот параметр равен <code>false</code>, то его вес не учитывается:</p>
<pre class="brush:kt;">Box(modifier = Modifier.background(Color.Yellow).fillMaxWidth().weight(3f, fill=false))</pre>
<h4>Сочетание весов и точных размеров</h4>
<p>Если у компонента не указан вес, то контейер сначала запрашивает его предпочтительную 
высоту. Затем оставшее простанство распределяется между компонентами, для которых указан вес, в соответствии с их весом.</p>
<pre class="brush:kt;">
package com.example.helloapp

import android.os.Bundle
import androidx.activity.ComponentActivity
import androidx.activity.compose.setContent
import androidx.compose.foundation.background
import androidx.compose.foundation.layout.*
import androidx.compose.ui.Modifier
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.unit.dp

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            Column()
            {
                Box(modifier = Modifier.background(Color.Red).fillMaxWidth().weight(1f))
                Box(modifier = Modifier.background(Color.Yellow).fillMaxWidth().height(150.dp))
                Box(modifier = Modifier.background(Color.Green).fillMaxWidth().weight(2f))
            }
        }
    }
}
</pre>
<img src="./pics/2.7.png" alt="вес компонента в Column в Jetpack Compose и Kotlin и Android" />
<p>Здесь для второго элемента Box установлена высота в 150 единиц, а для остальных установлены веса - 1f и 2f. Таким образом, получится, что от всей длины контейера Column (а он будет растягиваться на весь экран) второй элемент получит высоту в 
150 единиц. Оставшееся пространство затем будет распределено между первым и третим элементами в соответствии с их весами.</p>
<h3>Установка размеров Column</h3>
<p>Если ни у одного вложенного компонента НЕ указан вес, <span class="b">Column</span> занимает то пространство, которое необходимо, 
чтобы вместить вложенные компоненты. Если подобный размер контейера нежелателен, то можно задать конкретный размер с помощью модификатора 
<span class="b">Modifier.height</span>:</p>
<pre class="brush:kt;">Column(modifier = Modifier.height(550.dp))</pre>
<p>С помощью модификатора <span class="b">Modifier.fillMaxHeight</span> можно растянуть контейер по всей длине экрана.</p>
<pre class="brush:kt;">Column(modifier = Modifier.fillMaxSize())</pre>
<p>Если как минимум для одного вложенного компонента указан вес, то в модификаторе <span class="b">Modifier.fillMaxHeight</span> нет смысла. 
Однако в этот случае по прежнему можно использовать модификаторы <span class="b">Modifier.height</span> и <span class="b">Modifier.size</span> 
для ограничения длины контейера.</p>
<h3>Позиционирование по вертикали и verticalArrangement</h3>
<p>Если высота контейера <span class="b">Column</span> больше суммы высот его вложенных компонентов, то для позиционирования этих 
компонентов может применяться параметр <span class="b">verticalArrangement</span>, который может принимать следующие значения:</p>
<ul>
<li><p><span class="b">Arrangement.Center</span>: расположение по центру</p></li>
<li><p><span class="b">Arrangement.Bottom</span>: расположение внизу</p></li>
<li><p><span class="b">Arrangement.Top</span>:  расположение вверху</p></li>
<li><p><span class="b">Arrangement.SpaceAround</span>: компоненты равномерно распределяются по всей высоте с равномерными отступами между элементами, 
при этом отступы между первым и последним элементами и границами контейнера равен половине отступов между элементами</p></li>
<li><p><span class="b">Arrangement.SpaceBetween</span>: компоненты равномерно распределяются по всей высоте с равномерными отступами между элементами, 
при этом первый и последний элементы прижимаются к границам контейнера</p></li>
<li><p><span class="b">Arrangement.SpaceEvenly</span>: компоненты равномерно распределяются по всей высоте с равномерными отступами между элементами, 
при этом отступы между первым и последним элементами и границами контейнера равны отступам между элементами</p></li>
</ul>
<img src="./pics/column_arrangement_visualization.gif" alt="Расположение компонентов в Column в Jetpack Compose и Kotlin" />
<p>Например:</p>
<pre class="brush:kt;">
package com.example.helloapp

import android.os.Bundle
import androidx.activity.ComponentActivity
import androidx.activity.compose.setContent
import androidx.compose.foundation.background
import androidx.compose.foundation.layout.*
import androidx.compose.ui.Modifier
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.unit.dp

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            Column(modifier = Modifier.fillMaxSize(), verticalArrangement = Arrangement.SpaceBetween )
            {
                Box(modifier = Modifier.background(Color.Red).fillMaxWidth().height(100.dp))
                Box(modifier = Modifier.background(Color.Yellow).fillMaxWidth().height(100.dp))
                Box(modifier = Modifier.background(Color.Green).fillMaxWidth().height(100.dp))
            }
        }
    }
}
</pre>
