<p>Для создания объекта необходимо вызвать конструктор класса. По умолчанию компилятор создает конструктор, который не принимает параметров и 
который мы можем использовать. Но также мы можем определять свои собственные конструкторы. Для определения конструкторов применяется ключевое слово <span class="b">constructor</span>.</p>
<p>Классы в Kotlin могут иметь один <span class="b">первичный конструктор</span> (primary constructor) и один или несколько <span class="b">вторичных конструкторов</span> (secondary constructor).</p>
<h3>Первичный конструктор</h3>
<p>Первичный конструктор является частью заголовка класса и определяется сразу после имени класса:</p>
<pre class="brush:kt;">
class Person constructor(_name: String){
   
}
</pre>
<p>Конструкторы, как и обычные функции, могут иметь параметры. Так, в данном случае конструктор имеет параметр <code>_name</code>, который представляет тип String. 
Через параметры конструктора мы можем передать извне данные и использовать их для инициализации объекта. При этом первичный конструктор 
в отличие от функций не определяет никаких действий, он только может принимать данные извне через параметры.</p>
<p>Если первичный конструктор не имеет никаких аннотаций или модификаторов доступа, как в данном случае, то ключевое слово 
<code>constructor</code> можно опустить:</p>
<pre class="brush:kt;">
class Person(_name: String){
   
}
</pre>
<h3>Инициализатор</h3>
<p>Что делать с полученными через конструктор данными? Мы их можем использовать для инициализации свойств класса. Для этого применяются блоки инициализаторов:</p>
<pre class="brush:kt;">
class Person(_name: String){
    val name: String
    init{
        name = _name
    }
}
</pre>
<p>В классе Person определено свойство name, которое хранит имя человека. Чтобы передать эту свойству значение параметра <code>_name</code> из 
первичного конструктора, применяется блок инициализатора. Блок инициализатора определяется после ключевого слова <span class="b">init</span>.</p>
<p>Цель инициализатора состоит в инициализации объекта при его создании. Стоит отметить, что здесь свойству <code>name</code> не задается начальное значение, 
потому это свойство в любом случае будет инициализировано в блоке инициализатора, и при создании объекта оно в любом случае получит значение.</p>
<p>Теперь мы можем использовать первичный конструктор класса для создания объекта:</p>
<pre class="brush:kt;">
fun main() {
    val tom = Person("Tom")
    val bob = Person("Bob")
    val alice = Person("Alice")
	
    println(tom.name)   // Tom
    println(bob.name)   // Bob
    println(alice.name) // Alice
}

class Person(_name: String){
    val name: String
    init{
        name = _name
    }
}
</pre>
<p>Важно учитывать, что если мы определили первичный конструктор, то мы не можем использовать конструктор по умолчанию, который генерируется компилятором. 
Для создания объекта обязательно надо использовать первичный конструктор, если он определен в классе.</p>
<p>Стоит отметить, что в классе может быть определено одновременно несколько блоков инициализатора.</p>
<p>Также стоит отметить, что в данном случае в инициализаторе нет смысла, так как параметры первичного конструктора можно нарямую передавать свойствам:</p>
<pre class="brush:kt;">
class Person(_name: String){
	
    val name: String = _name
}
</pre>
<h3>Первичный конструктор и свойства</h3>
<p>Первичный конструктор также может использоваться для определения свойств:</p>
<pre class="brush:kt;">
fun main() {

    val bob: Person = Person("Bob", 23)

    println("Name: ${bob.name}  Age: ${bob.age}")
}

class Person(val name: String, var age: Int){

}
</pre>
<p>Свойства определяются как и параметры, при этом их определение начинается с ключевого слова <span class="b">val</span> (если их не планируется изменять) 
и <span class="b">var</span> (если свойства должны быть изменяемыми). И в этом случае нам уже необязательно 
явным образом определять эти свойства в теле класса, так как их уже определяет конструктор. И при вызове конструктора этим свойствам автоматически передаются значения: <code>Person("Bob", 23)</code></p>
<h3>Вторичные конструкторы</h3>
<p>Класс также может определять вторичные конструкторы. Они применяются в основном, чтобы определить дополнительные параметры, через которые можно передавать данные для инициализации 
объекта.</p>
<p>Вторичные конструкторы определяются в теле класса. Если для класса определен первичный конструктор, то вторичный конструктор должен вызывать первичный с помощью ключевого слова 
<span class="b">this</span>:</p>
<pre class="brush:kt;">
class Person(_name: String){
    val name: String = _name
    var age: Int = 0
	
    constructor(_name: String, _age: Int) : this(_name){
        age = _age
    }
}
</pre>
<p>Здесь в классе Person определен первичный конструктор, который принимает значение для установки свойства <code>name</code>:</p>
<pre class="brush:kt;">class Person(_name: String)</pre>
<p>И также добавлен вторичный конструктор. Он принимает два параметра: _name и _age. С помощью ключевого слова this вызывается первичный конструктор, поэтому через этот вызов необходимо 
передать значения для параметров первичного конструктора. В частности, в первичный конструктор передается значение параметра _name. 
В самом вторичном конструкторе устанавливается значение свойства age.</p>
<pre class="brush:kt;">
constructor(_name: String, _age: Int) : this(_name){
	age = _age
}
</pre>
<p>Таким образом, при вызове вторичного конструктора вначале вызывается первичный конструктор, срабатывает блок инициализатора, который устанавливает свойство name. 
Затем выполняются собственно действия вторичного конструктора, который устанавливает свойство age.</p>
<p>Используем данную модификацию класса Person:</p>
<pre class="brush:kt;">
fun main() {

    val tom: Person = Person("Tom")
    val bob: Person = Person("Bob", 45)
    
    println("Name: ${tom.name}  Age: ${tom.age}")
    println("Name: ${bob.name}  Age: ${bob.age}")
}

class Person(_name: String){
    val name: String = _name
    var age: Int = 0
	
    constructor(_name: String, _age: Int) : this(_name){
        age = _age
    }
}
</pre>
<p>В функции main создаются два объекта Person. Для создания объекта tom применяется первичный конструктор, который принимает один параметр. 
Для создания объекта bob применяется вторичный конструктор с двумя параметрами.</p>
<p>Консольный вывод программы:</p>
<div class="console">
<pre class="consoletext">
Name: Tom  Age: 0
Name: Bob  Age: 45
</pre>
</div>
<p>При необходимости мы можем определять и больше вторичных конструкторов:</p>
<pre class="brush:kt;">
fun main() {

    val tom = Person("Tom")
    val bob = Person("Bob", 41)
    val sam = Person("Sam", 32, "JetBtains")

    println("Name: ${tom.name}  Age: ${tom.age}  Company: ${tom.company}")
    println("Name: ${bob.name}  Age: ${bob.age}  Company: ${bob.company}")
    println("Name: ${sam.name}  Age: ${sam.age}  Company: ${sam.company}")
}

class Person(_name: String){
    val name = _name
    var age: Int = 0
    var company: String = "Undefined"

    constructor(_name: String, _age: Int) : this(_name){
        age = _age
    }

    constructor(_name: String, _age: Int, _comp: String) : this(_name, _age){
         company = _comp
    }
}
</pre>
<p>Здесь в класс Person добавлено новое свойство - <code>company</code>, которое описывает компании, в которой работает человек. И также добавлен еще один конструктор, 
который принимает три параметра:</p>
<pre class="brush:kt;">
constructor(_name: String, _age: Int, _comp: String) : this(_name, _age){
	company = _comp
}
</pre>
<p>Чтобы не дублировать код установки свойств name и age, этот вторичный конструктор передает установку этих свойств другому вторичному конструктору, который принимает два параметра, 
через вызов <code>this(_name, _age)</code>. То есть данный вызов по сути будет вызывать первый вторичный конструктор с двумя параметрами.</p>
<p>Консольный вывод программы:</p>
<div class="console">
<pre class="consoletext">
Name: Tom  Age: 0  Company: Undefined
Name: Bob  Age: 41  Company: Undefined
Name: Sam  Age: 32  Company: JetBtains
</pre>
</div>
