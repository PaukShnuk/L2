<p>Делегирование представляет паттерн объектно-ориентированного программирования, который позволяет одному объекту делегировать/перенаправить все запросы другому объекту. 
В определенной степени делегирование может выступать альтернативой наследованию. И преимуществом Kotlin в данном случае состоит в том, что Kotlin нативно поддерживает 
данный паттерн, предоставляя необходимый инструментарий.</p>
<p>Формальный синтаксис:</p>
<pre class="brush:kt;">
interface Base {
    fun someFun()
}

class BaseImpl() : Base {
    override fun someFun() { }
}

class Derived(someBase: Base) : Base by someBase
</pre>
<p>Есть некоторый интерфейс - Base, который определяет некоторый функционал. Есть его реализация в виде класса BaseImpl.</p>
<p>И есть еще один класс - Derived, который также применяет интерфейс Base. Причем после указания применяемого интерфейса идет ключевое слово 
<span class="b">by</span>, а после него - объект, которому будут делегироваться вызовы.<p>
<pre class="brush:kt;">class Derived(someBase: Base) : Base by someBase</pre>
<p>То есть в данной схеме класс Derived будет делегировать вызовы объекту <code>someBase</code>, который представляет интерфейс Base и передается через первичный конструктор. 
При этом Derived может не реализовать интерфейс Base или реализовать неполностью - какие-то отдельные свойства и функции.</p>
<p>Например, рассмотрим следующие классы:</p>
<pre class="brush:kt;">
interface Messenger{
    fun send(message: String)
}
class InstantMessenger(val programName: String) : Messenger{

    override fun send(message: String){
        println("Message `$message` has been sent")
    }
}
class SmartPhone(val name: String, m: Messenger): Messenger by  m
</pre>
<p>Здесь определен интерфейс <code>Messenger</code>, который представляет условно программу для отправки сообщений. Для условной отправки сообщений 
определена функция <code>send()</code>.</p>
<p>Также есть класс <code>InstantMessenger</code> - программа мгновенных сообщений или проще говоря мессенджер, который применяет интерфейс Messenger, 
реализуя его функцию <code>send()</code></p>
<p>Далее определен класс <code>SmartPhone</code>, который представляет смартфон и также применяет интерфейс Messenger, но не реализует его. Вместо этого он 
принимает через первичный конструктор объект Messenger и делегирует ему обращение к функции <code>send()</code>.</p>
<p>Применим классы:</p>
<pre class="brush:kt;">
fun main() {
    val telegram = InstantMessenger("Telegram")
    val pixel = SmartPhone("Pixel 5", telegram)
    pixel.send("Hello Kotlin")
    pixel.send("Learn Kotlin on Metanit.com")
}
</pre>
<p>Здесь создан объект pixel, который представляет класс SmartPhone. Поскольку SmartPhone применяет интерфейс Messenger, то мы можем вызвать у объекта pixel 
функцию <code>send()</code> для отправки условного сообщения. Однако сам класс SmartPhone НЕ реализует функцию send - само выполнение этой функции 
делегируется объекту <code>telegram</code>, который в реальности выполняет отправку сообщения. Соответственно при выполнении программы мы увидим следующий консольный вывод:</p>
<div class="console">
<pre class="consoletext">
Message `Hello Kotlin` has been sent
Message `Learn Kotlin on Metanit.com` has been sent
</pre>
</div>
<h3>Множественное делегирование</h3>
<p>Подобным образом один  объект может делегировать выполнение различных функций разным объектам. Например:</p>
<pre class="brush:kt;">
fun main() {
    val telegram = InstantMessenger("Telegram")
    val photoCamera = PhotoCamera()
    val pixel = SmartPhone("Pixel 5", telegram, photoCamera)
    pixel.send("Hello Kotlin")
    pixel.takePhoto()
}

interface Messenger{
    fun send(message: String)
}
class InstantMessenger(val programName: String) : Messenger{
    override fun send(message: String) = println("Send message: `$message`")
}
interface PhotoDevice{
    fun takePhoto()
}
class PhotoCamera: PhotoDevice{
    override fun takePhoto() = println("Take a photo")
}
class SmartPhone(val name: String, m: Messenger, p: PhotoDevice)
    : Messenger by  m, PhotoDevice by p
</pre>
<p>Здесь класс SmartPhone также реализует интерфейс PhotoDevice, который предоставляет функцию <code>takePhoto()</code> для съемки фото. Но выполнение этой функции он делегирует 
параметру <code>p</code>, который представляет интерфейс PhotoDevice и в роли которого выступает объект PhotoCamera.</p>
<h3>Переопределение функций</h3>
<p>Класс может переопределять часть функций интерфейса, в этом случае выполнение этих функций не делегируется. Например:</p>
<pre class="brush:kt;">
fun main() {
    val telegram = InstantMessenger("Telegram")
    val pixel = SmartPhone("Pixel 5", telegram)
    pixel.sendTextMessage()
    pixel.sendVideoMessage()
}

interface Messenger{
    fun sendTextMessage()
    fun sendVideoMessage()
}
class InstantMessenger(val programName: String) : Messenger{
    override fun sendTextMessage() = println("Send text message")
    override fun sendVideoMessage() = println("Send video message")
}
class SmartPhone(val name: String, m: Messenger) : Messenger by  m{
    override fun sendTextMessage() = println("Send sms")
}
</pre>
<p>В данном случае класс SmartPhone реализует функцию <code>sendTextMessage()</code>, поэтому ее выполнение не делегируется. Консольный вывод программы:</p>
<div class="console">
<pre class="consoletext">
Send sms
Send video message
</pre>
</div>
<h3>Делегирование свойств</h3>
<p>По аналогии с функциями объект может делегировать обращение к свойствам:</p>
<pre class="brush:kt;">
fun main() {
    val telegram = InstantMessenger("Telegram")
    val pixel = SmartPhone("Pixel 5", telegram)
    println(pixel.programName)  // Telegram
}
interface Messenger{
    val programName: String
}
class InstantMessenger(override val programName: String) : Messenger
class SmartPhone(val name: String, m: Messenger) : Messenger by  m
</pre>
<p>Здесь при интерфейс Messenger определяет свойство <code>programName</code> - название программы отправки. Класс SmartPhone не реализует это свойство, поэтому обращение к 
этому свойству делегируется объекту <code>m</code>.</p>
<p>Если бы класс SmartPhone сам реализовал это свойство, то делегирования бы не было:</p>
 <pre class="brush:kt;">
 fun main() {
    val telegram = InstantMessenger("Telegram")
    val pixel = SmartPhone("Pixel 5", telegram)
    println(pixel.programName)  // Default Messenger
}
interface Messenger{
    val programName: String
}
class InstantMessenger(override val programName: String) : Messenger
class SmartPhone(val name: String, m: Messenger) : Messenger by  m{
    override val programName = "Default Messenger"
}
</pre>
