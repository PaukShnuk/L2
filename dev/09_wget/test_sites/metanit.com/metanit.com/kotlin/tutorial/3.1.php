<p>Одним из строительных блоков программы являются функции. Функция определяет некоторое действие. В Kotlin функция объявляется с 
помощью ключевого слова <span class="b">fun</span>, после которого идет название функции. Затем после названия в скобках указывается список параметров. 
Если функция возвращает какое-либо значение, то после списка параметров через запятую можно указать тип возвращаемого значения. И далее в фигурных скобках идет 
тело функции.</p>
<pre class="brush:kt;">
fun имя_функции (параметры) : возвращаемый_тип{
	выполняемые инструкции
}
</pre>
<p>Параметры необязательны.</p>
<p>Например, определим и вызовем функцию, которая просто выводит некоторую строку на консоль:</p>
<pre class="brush:kt;">
fun main() {

    hello() // вызов функции hello
    hello() // вызов функции hello
    hello() // вызов функции hello
}
// определение функции hello
fun hello(){
    println("Hello")
}
</pre>
<p>Функции можно определять в файле вне других функций или классов, сами по себе, как например, определяется функция main. Такие 
функции еще называют функциями верхнего уровня (top-level functions).</p>
<p>Здесь кроме главной функции main также определена функция hello, которая не принимает никаких параметров и ничего не возвращает. Она просто выводит строку на консоль.</p>
<p>Функция hello (и любая другая определенная функция, кроме main) сама по себе не выполняется. Чтобы ее выполнить, ее надо вызвать. 
Для вызова функции указывается ее имя (в данном случае "hello"), после которого идут пустые скобки.</p>
<p>Таким образом, если необходимо в разных частях программы выполнить одни и те же действия, то можно эти действия вынести в функцию, и затем вызывать эту функцию.</p>
<h3>Предача параметров</h3>
<p>Через параметры функция может получать некоторые значения извне. Параметры указываются после имени функции в скобках через запятую в формате 
<code>имя_параметра : тип_параметра</code>. Например, определим функцию, которая вычисляет факториал числа:</p>
<pre class="brush:kt;">
fun main() {

    showMessage("Hello Kotlin")
    showMessage("Привет Kotlin")
    showMessage("Salut Kotlin")
}

fun showMessage(message: String){
    println(message)
}
</pre>
<p>Функция <code>showMessage()</code> принимает один параметр типа <code>String</code>. Поэтому при вызове функции в скобках необходимо передать значение 
для этого параметра: <code>showMessage("Hello Kotlin")</code>. Причем это значение должно представлять тип String, то есть строку. Значения, которые передаются параметрам функции, 
еще назвают аргументами.</p>
<p>Консольный вывод программы:</p>
<div class="console">
<pre class="consoletext">
Hello Kotlin
Привет Kotlin
Salut Kotlin
</pre>
</div>
<p>Другой пример - функция, которая выводит данные о пользователе на консоль:</p>
<pre class="brush:kt;">
fun main() {

    displayUser("Tom", 23)
    displayUser("Alice", 19)
    displayUser("Kate", 25)
}
fun displayUser(name: String, age: Int){
    println("Name: $name   Age: $age")
}
</pre>
<p>Функция <code>displayUser()</code> принимает два параметра - name и age. При вызове функции в скобках ей передаются значения для этих параметров. 
При этом значения передаются параметрам по позиции и должны соответствовать параметрам по типу. Так как вначале идет параметр типа <code>String</code>, 
а потом параметр типа <code>Int</code>, то при вызове функции в скобках вначале передается строка, а потом число.</p>
<h3>Аргументы по умолчанию</h3>
<p>В примере выше при вызове функций showMessage и displayUser мы обязательно должны предоставить для каждого их параметра какое-то определенное значение, которое соответствует 
типу параметра. Мы не можем, к примеру, вызвать функцию displayUser, не передав ей аргументы для параметров, это будет ошибка:</p>
<pre class="brush:kt;">
displayUser()
</pre>
<p>Однако мы можем определить какие-то параметры функции как необязательные и установить для них значения по умолчанию:</p>
<pre class="brush:kt;">
fun displayUser(name: String, age: Int = 18, position: String="unemployed"){
    println("Name: $name   Age: $age  Position: $position")
}

fun main() {

    displayUser("Tom", 23, "Manager")
    displayUser("Alice", 21)
    displayUser("Kate")
}
</pre>
<p>В данном случае функция displayUser имеет три параметра для передачи имени, возраста и должности. 
Для первого параметр name значение по умолчанию не установлено, поэтому для него значение по-прежнему обязательно передавать значение. 
Два последующих - age и position являются необязательными, и для них установлено значение по умолчанию. Если для этих параметров не передаются значения, тогда параметры используют 
значения по умолчанию. Поэтому для этих параметров в принципе нам необязательно передавать аргументы. Но если для какого-то параметра определено значение по умолчанию, 
то для всех последующих параметров тоже должно быть установлено значение по умолчанию.</p>
<p>Консольный вывод программы</p>
<div class="console">
<pre class="consoletext">
Name: Tom   Age: 23  Position: Manager
Name: Alice   Age: 21  Position: unemployed
Name: Kate   Age: 18  Position: unemployed
</pre>
</div>
<h3>Именованные аргументы</h3>
<p>По умолчанию значения передаются параметрам по позиции: первое значение - первому параметру, второе значение - второму параметру и так далее. 
Однако, используя именованные аргументы, мы можем переопределить порядок их передачи параметрам:</p>
<pre class="brush:kt;">
fun main() {

    displayUser("Tom", position="Manager", age=28)
    displayUser(age=21, name="Alice")
    displayUser("Kate", position="Middle Developer")
}
</pre>
<p>При вызове функции в скобках мы можем указать название параметра и с помощью знака равно передать ему нужное значение.</p>
<p>При этом, как видно из последнего случае, необязательно все аргументы передавать по имени. Часть аргументов могут передаваться параметрам по позиции. 
Но если какой-то аргумент передан по имени, то остальные аргументы после него также должны передаваться по имени соответствующих параметров.</p>
<p>Также если до обязательного параметра функции идут необязательные параметры, то для обязательного параметра значение передается по имени:</p>
<pre class="brush:kt;">
fun displayUser(age: Int = 18, name: String){
    println("Name: $name   Age: $age")
}
fun main() {

    displayUser(name="Tom", age=28)
    displayUser(name="Kate")
}
</pre>
<h3>Изменение параметров</h3>
<p>По умолчанию все параметры функции равносильны val-переменным, поэтому их значение нельзя изменить. Например, в случае следующей функции при компиляции мы получим ошибку:</p>
<pre class="brush:kt;">
fun double(n: Int){
    n = n * 2   // !Ошибка - значение параметра нельзя изменить
    println("Значение в функции double: $n")
}
</pre>
<p>Однако если параметр предствляет какой-то сложный объект, то можно изменять отдельные значения в этом объекте. Например, возьмем функцию, которая в качестве параметра 
принимает массив:</p>
<pre class="brush:kt;">
fun double(numbers: IntArray){
    numbers[0] = numbers[0] * 2
    println("Значение в функции double: ${numbers[0]}")
}

fun main() {

    var nums = intArrayOf(4, 5, 6)
    double(nums)
    println("Значение в функции main: ${nums[0]}")
}
</pre>
<p>Здесь функция <code>double</code> принимает числовой массив и увеличивает значение его первого элемента в два раза. Причем изменение элемента массива внутри функции приведет к 
тому, что также будет изменено значение элемента в том массиве, который передается в качестве аргумента в функцию, так как этот один и тот же массив. Консольный вывод:</p>
<div class="console">
<pre class="consoletext">
Значение в функции double: 8
Значение в функции main: 8
</pre>
</div>
