<p>Для хранения состояния объекта в типах F# могут использоваться поля. Есть несколько способов определения полей.</p>
<h3>let-привязка значений в классах</h3>
<p>Как и в целом в модулях, в F# внутри классов с помощью оператора <span class="b">let</span> можно определять значения, которые будут хранить 
некоторые данные. Но стоит учитывать, что такие значения доступны только внутри своего класса:</p>
<pre class="brush:f#;">
type Person (name, surname, age) = 

    let fullName = $"{name} {surname}"                      // приватное поле
    member _.Print() = printfn $"Name: {fullName}  Age: { age }"
 
let tom = Person("Tom", "Smith", 37)

tom.Print()			// Name: Tom Smith  Age: 37
</pre>
<p>Здесь определено приватное поле <code>fullName</code>, которое хранит полное имя пользователя на основе его имени и фамилии. Вне класса к этому полю мы обратиться не можем.</p>
<p>При этом подобные поля также могут изменять свои данные при определении с оператором <span class="b">mutable</span>:</p>
<pre class="brush:f#;">
type Person (name, surname, _age) = 

    let fullName = $"{name} {surname}"
    let mutable age = _age              // изменяемое поле age

    member _.Print() = printfn $"Name: {fullName}  Age: { age }"
    member _.Grow() =  age &lt;- age + 1
 
let tom = Person("Tom", "Smith", 37)
tom.Print()     // Name: Tom Smith  Age: 37

tom.Grow()
tom.Print()     // Name: Tom Smith  Age: 38
</pre>
<p>Здесь определено изменяемое поле <code>age</code>, начальное значение которого равно значению параметра конструктора <code>_age</code>. Далее 
мы можем изменять значение этого поля на единицу с помощью метода <code>Grow()</code>. Консольный вывод:</p>
<div class="console"><pre class="consoletext">
Name: Tom Smith  Age: 37
Name: Tom Smith  Age: 38
</pre></div>
<h3>Определение полей с помощью оператора val</h3>
<p>Другой способ определения полей в классе представляет оператор <span class="b">val</span>. Формальный синтаксис его применения:</p>
<pre class="brush:f#;">
val [ mutable ] [ модификатор_доступа ] имя_поля : тип_данных
</pre>
<p>В отличие от let-полей val-поля могут иметь модификатор доступа, соответственно поля с модификаторами <code>public/internal</code> могут быть доступны вне 
класса. По умолчанию val-поля без модификаторов доступны вне класса.</p>
<h3>val-поля в классе с первичным конструктором</h3>
<p>Может показаться, что val-поля гораздо проще и удобнее использовать, чем let-поля, но на самом деле их применение обставлено рядом ограничений. 
Например, определим пару таких полей:</p>
<pre class="brush:f#;">
type Person(name, surname, _age) = 

    [&lt;DefaultValue&gt;] val mutable fullName: string
    [&lt;DefaultValue&gt;] val mutable age: int

    member this.Print() = printfn $"Name: {this.fullName}  Age: { this.age }"
    member this.Grow() =  this.age &lt;- this.age + 1
 
let tom = Person("Tom", "Smith", 37)
tom.Print()     // Name:  Age: 0

tom.Grow()
tom.Print()     // Name:  Age: 1

tom.age &lt;- tom.age + 4
tom.Print()     // Name:  Age: 5
</pre>
<p>Здесь определены два поля: <code>fullName</code> и <code>age</code>. При определении полей мы НЕ можем им тут же присвоить некоторое значение, например:</p>
<pre class="brush:f#;">
val mutable fullName: string = $"{name} {surname}"		// это работать не будет
val mutable age: int = 6		// это работать не будет
</pre>
<p>Для инициализации полей нужно использовать другие способы.</p>
<p>Следующее ограничение: если класс имеет первичный конструктор (как в случае выше), то val-поля должны иметь атрибут <span class="b">[&lt;DefaultValue&gt;]</span>. Этот 
атрибут устанавливает для таких полей значение по умолчанию. Так, для числовых данных это число 0, а для типа <code>string</code> (как и классов) это специальное значение <code>null</code>, 
которое указывает на отсутствие значения.</p>
<p>Еще одно ограничение - в этом случае val-поля должны быть изменяемыми, то есть определены с оператором <span class="b">mutable</span>.</p>
<p>В итоге при создании объекта Person его поле <code>fullName</code> фактически не будет иметь никакого значения (при выводе на консоль мы увидим пустую строку), 
а поле <code>age</code> будет иметь значение 0. Консольный вывод программы:</p>
<div class="console"><pre class="consoletext">
Name:  Age: 0
Name:  Age: 1
Name:  Age: 5
</pre></div>
<p>Но что если мы хотим все-таки присвоить этим полям некоторые значения, например, значения первичного конструктора? В этом случае мы можем определить специальный метод, 
который будет выполять подобную работу:</p>
<pre class="brush:f#;">
type Person(name, surname, _age) = 

    [&lt;DefaultValue&gt;] val mutable fullName: string
    [&lt;DefaultValue&gt;] val mutable age: int

    member this.SetValues() =  
        this.fullName &lt;- $"{name} {surname}"
        this.age &lt;- _age

    member this.Print() = printfn $"Name: {this.fullName}  Age: { this.age }"
    member this.Grow() =  this.age &lt;- this.age + 1
 
let tom = Person("Tom", "Smith", 37)
tom.SetValues()
tom.Print()     // Name: Tom Smith  Age: 37

tom.Grow()
tom.Print()     // Name: Tom Smith  Age: 38

tom.age &lt;- tom.age + 4
tom.Print()     // Name: Tom Smith  Age: 42
</pre>
<p>В данном случае определен метод <code>SetValues</code>, который передает val-полям значения параметров первичного конструктора. При этом значения для полей также можно передавать и через 
параметры метода или брать их из let-полей (при наличии таковых). Однако кроме определения нам надо еще вызывать этот метод в программе. Консольный вывод:</p>
<div class="console"><pre class="consoletext">
Name: Tom Smith Age: 37
Name: Tom Smith Age: 38
Name: Tom Smith Age: 42
</pre></div>
<h3>Классы без первичного конструктора</h3>
<p>В классах без первичного конструктора val-поля могут быть неизменяемыми, для них не надо определять атрибут <code>[&lt;DefaultValue&gt;]</code>, 
однако нам надо дополнительно с помощью конструктора определить логику их инициализации:</p>
<pre class="brush:f#;">
type Person = 

    val fullName: string 
    val mutable age: int

    new (name, surname, _age) = { fullName = $"{name} {surname}"; age = _age;}

    member this.Print() = printfn $"Name: {this.fullName}  Age: { this.age }"
    member this.Grow() =  this.age <- this.age + 1
 
let tom = Person("Tom", "Smith", 37)
tom.Print()     // Name: Tom Smith  Age: 37

tom.Grow()
tom.Print()     // Name: Tom Smith  Age: 38

tom.age &lt;- tom.age + 5
tom.Print()     // Name: Tom Smith  Age: 43
</pre>
<p>Здесь изменяемым полем является только поле <code>age</code>, а для инициализации полей определен специальный конструктор, который называется <span class="b">конструктором типа</span>:</p>
<pre class="brush:f#;">new (name, surname, _age) = { fullName = $"{name} {surname}"; age = _age;}</pre>
<p>Обратите внимание, как выглядит инициализация - тело конструктора обернуто в фигурные скобки, а присвоение значений полям отделяется точкой с запятой.</p>
<h4>Модификаторы доступа</h4>
<p>Если надо установить модификатор доступа, то он указывается перед именем поля и после операторов <code>let/mutable</code>:</p>
<pre class="brush:f#;">
val internal fullName: string 
val mutable private age: int
</pre>
