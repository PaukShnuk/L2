<p>При работе с сетью и http нередко могут происходить ошибки, например, в момент запроса сеть стала недоступна, неправильно указан адрес и соответственно не найден 
ресурс, либо доступ к ресурсу ограничен, либо другие ошибки. Перехват ошибок позволит выяснить проблему и каким-то образом обработать их, например, 
вывести пользователю сообщение об ошибке.</p>

<p>Для перехвата ошибок, которые могут возникнуть при выполнении запроса, можно использовать функцию <span class="b">catchError()</span>. Так, 
возьмем код сервиса из прошлой темы и добавим к нему обработку ошибок:</p>
<pre class="brush:ts;">
import {Injectable} from '@angular/core';
import {HttpClient} from '@angular/common/http';
import {User} from './user';
import {Observable, throwError} from 'rxjs';
import { map, catchError} from 'rxjs/operators';
 
@Injectable()
export class HttpService{
    
    errorMessage: String = "";
    constructor(private http: HttpClient){ }
        
    getUsers() : Observable&lt;User[]&gt; {
        return this.http.get('assets/usersP.json').pipe(map((data:any)=&gt;{
            let usersList = data["userList"];
            
            return usersList.map(function(user:any) : User {
                return new User(user.userName, user.userAge);
              });
        }),
        catchError(err =&gt; {  
            console.log(err); 
            this.errorMessage = err.message;
            return [];
        }))
    };
}
</pre>
<p>Прежде всего в сервисе определяется переменная <code>errorMessage</code>, которая будет хранит информацию об ошибке.</p>
<p>Для имитации ошибки в http-клиент передается заведомо несуществующий адрес "usersP.json". Для обработки ошибок в метод 
<code>pipe()</code> передается в качестве второго параметра функция для обработки ошибок. В качестве подобной функции здесь применяется функция <span class="b">catchError()</span>:</p>
<pre class="brush:c#;">
catchError(err =&gt; {  
            console.log(err); 
            this.errorMessage = err.message;
            return [];
        })
</pre>
<p>В качестве параметра функция <code>catchError()</code> принимает функцию, в которую в качестве параметра передается объект ошибки, возникшей 
при выполнении запроса. Таким образом, в этой функции мы можем получить ошибку и обработать ее.</p>
<p>Ошибка собственно представляет объект, из которого мы можем получить ряд данных. В частности, 
свойство <span class="b">message</span> позволяет получить сообщение об ошибке, а свойство <span class="b">status</span> - статусный код ответа.</p>
<p>Так, в данном случае вся обработка заключается в том, что этот объект выводится на консоль, а свойству errorMessage сервиса 
передается сообщение об ошибке (если запрос прошел успешно, то этому свойству присваивается пустая строка).</p>
<p>Стоит отметить, что в функции обработки ошибки нам все равно надо вернуть объект Observable. Для этого мы возвращаем пустой массив:</p>
<pre class="brush:c#;">return [];</pre>
<p>Далее будет создан объект <span class="b">Observable&gt;User[]&lt;</span>, который будет содержать пустой массив объектов User.<p>

<p>Например, используем сервис и для этого изменим код компонента AppComponent:</p>
<pre class="brush:ts;">
import { Component, OnInit} from '@angular/core';
import { HttpService} from './http.service';
import {User} from './user';
   
@Component({
    selector: 'my-app',
    template: `&lt;div&gt;{{this.httpService.errorMessage}}&lt;/div&gt;
                &lt;ul&gt;
                &lt;li *ngFor="let user of users"&gt;
                &lt;p&gt;Имя пользователя: {{user?.name}}&lt;/p&gt;
                &lt;p&gt;Возраст пользователя: {{user?.age}}&lt;/p&gt;
                &lt;/li&gt;
            &lt;/ul&gt;`,
    providers: [HttpService]
})
export class AppComponent implements OnInit { 
   
    users: User[]=[];
    error:any;
    constructor(public httpService: HttpService){}
      
    ngOnInit(){
          
        this.httpService.getUsers().subscribe( data=&gt;this.users=data);
    }
}
</pre>
<p>С помощью метода <code>subscribe()</code> компонент может получить из сервиса массив объектов User. Если возникнет ошибка, то это будет пустой массив.</p>
<p>Для получения информации об ошибке компонент обращается к свойству <code>errorMessage</code> сервиса и выводит его значение в размеке html.</p> 
<p>И например, при обащении к несуществующему файлу json мы получим следующую ошибку:</p>
<img src="./pics/6.4.png" alt="Передача ошибки в компонент в Angular 13" />
<p>Это самая примитивная обработка ошибка, которая демонстрирует общий принцип. Естественно в сервисе мы можем определять какое-то другое сообщение об ошибке или как-то иначе обрабатывать ошибку.</p>

