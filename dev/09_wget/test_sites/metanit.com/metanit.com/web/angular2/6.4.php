<p>В прошлых темах были рассмотрены простейшие запросы к файлу json, который содержал некоторые данные. Однако нередко возникает необходимость обращаться с более сложными запросами 
к удаленному ресурсу, передавая ему некоторые параметры. Вначале рассмотрим, как передать параметры в get-запросе.</p>
<h3>Определение приложения на сервере</h3>
<p>Рассмотрим простейшую задачу. Допустим, на стороне сервера действует некоторый обработчик или скрипт, который принимает в качестве параметра два числа и вычисляет их сумму.</p>
<p>В качестве стороны сервера мы могли бы взять разные технологии: PHP, ASP.NET и т.д. В данном случае для тестирования определим небольшое приложение на 
Node.js, так как данная технология представляет наиболее простейший способ для создания простейшего тестового приложения, да и сам Node.js 
установлен у всех разработчиков Angular. Приложение будет интуитивно понятным, более же подробно с данной технологией можно ознакомиться в 
<a href="//metanit.com/web/nodejs/">руководстве по Node.js</a></p>
<p>Определим для приложения Node.js новый каталог где-нибудь на жестком диске. Далее в этом каталоге определим файл <span class="b">package.json</span>:</p>
<pre class="brush:js;">
{
  "name": "expressapp",
  "version": "1.0.0",
  "dependencies": {
    "express": "^4.16.4"
  }
}
</pre>
<p>Наше приложение будет использовать фреймворк Express для обработки запросов. Установим пакеты командой <code>npm install</code>.</p>
<p>Далее определим в папке приложения файл <span class="b">app.js</span> - главный файл приложения, который будет содержать следующий код:</p>
<pre class="brush:js;">
const express = require("express");
  
const app = express();

// обработчик по маршруту localhost:3000/sum
app.get("/sum", function(request, response){
      
	// получаем параметры из строки запроса и преобразуем в числа
    const number1 = parseInt(request.query.num1);
    const number2 = parseInt(request.query.num2);

	// вычисляем сумму
    const sum = number1 + number2;
	
	// настройка заголовков CORS для кроссдоменных запросов
	response.header('Access-Control-Allow-Origin', '*');
	response.header('Access-Control-Allow-Headers', 'Origin, X-Requested-With, Content-Type, Accept');
	response.header('Access-Control-Allow-Methods', 'GET, PATCH, PUT, POST, DELETE, OPTIONS');
	
	response.send({result: sum});
});
 
app.listen(3000);
</pre>
<p>Данное приложение будет принимать запросы по адресу "/sum" (<code>app.get("/sum")...</code>). Из строки запроса получаем два параметра num1 и num2 (названия параметров произвольное). 
Далее конвертируем значения параметров в числа и вычисляем суммму. И возвращаем сумму в виде объекта json <code>{result: sum}</code>.</p>
<p>Поскольку приложение Angular будет разворачиваться на одном домене, а приложение Node.js на другом, то нам необходимо будет настроить CORS - кроссдоменные запросы. 
Для этого с помощью метода <code>response.header</code> устанавливаются соответствующие заголовки.</p>
<p>В командой строке/консоли перейдем к папке приложения node.js и запустим скрипт с помощью команды <code>node app.js</code></p>
