<h3>Определение клиентского приложения Angular</h3>
<p>Для отправки запроса из Angular определим следующий сервис:</p>
<pre class="brush:ts;">
import {Injectable} from '@angular/core';
import {HttpClient} from '@angular/common/http';
  
@Injectable()
export class HttpService{
  
    constructor(private http: HttpClient){ }
     
    getSum(num1: number, num2: number){
        return this.http.get('http://localhost:3000/sum?num1=' + num1 + "&num2=" + num2);
    }
}
</pre>
<p>Поскольку данные передаются через запрос GET, то мы можем конкатенировать нужное число со строкой запроса.</p>
<p>И определим код компонента:</p>
<pre class="brush:ts;">
import { Component, OnInit} from '@angular/core';
import { HttpService} from './http.service';
   
@Component({
    selector: 'my-app',
    template: `&lt;div *ngIf="done"&gt;Сумма = {{sum}}&lt;/div&gt;
                &lt;div class="form-group"&gt;
                    &lt;label&gt;Введите первое число&lt;/label&gt;
                    &lt;input class="form-control" type="number" name="num1" [(ngModel)]="num1" /&gt;
                &lt;/div&gt;
                &lt;div class="form-group"&gt;
                    &lt;label&gt;Введите второе число&lt;/label&gt;
                    &lt;input class="form-control" type="number" name="num2" [(ngModel)]="num2" /&gt;
                &lt;/div&gt;
                &lt;div class="form-group"&gt;
                    &lt;button class="btn btn-default" (click)="submit()"&gt;Отправить&lt;/button&gt;
                &lt;/div&gt;`,
    providers: [HttpService]
})
export class AppComponent { 
   
    num1: number = 0;
    num2: number = 0;
    sum: number | undefined;
    done: boolean = false;
    constructor(private httpService: HttpService){}
    submit(){
        this.httpService.getSum(this.num1, this.num2).subscribe((data:any) =&gt; {
            this.sum=data.result; 
            this.done=true;
        });
    }
}
</pre>
<p>По нажатию на кнопку отправляем введенное число на сервер, и поверх формы ввода отображаем полученный результат:</p>
<img src="./pics/6.5.png" alt="Отправка данных в Angular" />
<h3>HttpParams</h3>
<p>Также для определения параметров в запросе можно использовать класс <span class="b">HttpParams</span>. В частности, изменим сервис HttpService 
следующим образом:</p>
<pre class="brush:ts;">
import {Injectable} from '@angular/core';
import {HttpClient, HttpParams} from '@angular/common/http';
  
@Injectable()
export class HttpService{
  
    constructor(private http: HttpClient){ }
     
    getSum(num1: number, num2: number){
        const params = new HttpParams()
        .set('num1', num1.toString())
        .set('num2', num2.toString());
        return this.http.get('http://localhost:3000/sum', {params});
    }
}
</pre>
<p>С помощью метода <span class="b">set()</span> объекта HttpParams устанавливаются параметры, и затем этот объект передается в запрос. В итоге результат будет тот 
же, что и в примере выше.</p>
