<p>Ранее рассматривалась тема <a href="https://metanit.com/web/typescript/2.4.php">type assertion</a>, где говорилось про преобразования типов. Те же способы преобразаний мы можем 
применять и при работе с классами и интерфейсами.</p>
<p>Так, рассмотрим следующую иерархию типов:</p>
<pre class="brush:ts;">
class Person {
 
    name: string;
    constructor(userName: string) {
 
        this.name = userName;
    }
}
 
class Employee extends Person {
 
    company: string;
    constructor(userName: string, company: string) {
 
        super(userName);
        this.company = company;
    }
}
</pre>
<p>Здесь класс Employee унаследован от класса Person. Поскольку объекты Employee в то же время являются и объектами Person, то при определении 
объектов мы можем написать так:</p>
<pre class="brush:ts;">
let tom : Person = new Employee("Tom", "Microsoft");
</pre>
<p>Соответственно везде, где в функцию в качестве параметра передается объект Person или возвращается из функции объект Person, мы вместо объекта Person можем 
передавать объект Employee:</p>
<pre class="brush:ts;">
class Person {
 
    name: string;
    constructor(userName: string) {
 
        this.name = userName;
    }
}
 
class Employee extends Person {
 
    company: string;
    constructor(userName: string, company: string) {
 
        super(userName);
        this.company = company;
    }
}

function printPerson(user: Person): void{
    console.log(`Person ${user.name}`);
}
 
function personFactory(userName: string): Person {
    return new Employee(userName, "не установлено");
}
 
let tom : Person = new Employee("Tom", "Microsoft");
printPerson(tom);

let bob = personFactory("Bob");
printPerson(bob);
</pre>
<p>Здесь продемонстрированы восходящиие преобразования, то есть преобразования от более конкретного типа к более общему - от призводного типа Employee к базовому типу Person. 
Они производятся неявно, и нам не надо писать какой-то дополнительный код.</p>
<p>Но есть и другой тип преобразований - нисходящие или от более общего типа к более конкретному. Например:</p>
<pre class="brush:ts;">
class Person {
 
    name: string;
    constructor(userName: string) {
 
        this.name = userName;
    }
}
 
class Employee extends Person {
 
    company: string;
    constructor(userName: string, company: string) {
 
        super(userName);
        this.company = company;
    }
}

let tom : Person = new Employee("Tom", "Microsoft");
console.log(tom.company);	// ошибка - в классе Person нет свойства company
</pre>
<p>Здесь переменная tom имеет тип Person, однако в реальности эта переменная указывает на объект типа Employee, так как для ее инициализации мы использовали 
конструктор типа Employee, который устанавливает свойство company. Однако попытка вывести значение свойства company у объекта <code>tom</code> завершится ошибкой, 
так как <code>tom</code> - это все таки переменная типа Person, в котором нет свойства <code>company</code>.</p>
<p>Чтобы решить эту ситуацию, нам надо явно преобразовать объект <code>tom</code> к типу <code>Employee</code>:</p>
<pre class="brush:ts;">
let tom : Person = new Employee("Tom", "Microsoft");

let tomEmployee: Employee = &lt;Employee&gt;tom; // преобразование к типу Employee
console.log(tomEmployee.company);
 
// или так
console.log((&lt;Employee&gt;tom).company);
</pre>
<p>Выражение <code>&lt;Тип&gt; переменная</code> позволяет преобразовать переменную к типу, который идет в угловых скобках.</p>
<p>Другой способ осуществить явное преобразование типов представляет применение оператора <span class="b">as</span>:</p>
<pre class="brush:ts;">
let tom : Person = new Employee("Tom", "Microsoft");

let tomEmployee: Employee = tom as Employee; // преобразование к типу Employee
console.log(tomEmployee.company);
 
// или так
console.log((tom as Employee).company);
</pre>
<h3>Интерфейсы в преобразаниях типов</h3>
<p>Все сказанное в отношении преобразования классов будет справедливо и для преобразования интерфейсов. В то же время есть некоторые особенности. Пусть у 
нас будет интерфейс IPerson, никак не связанный с классами Person и Employee и ими не реализуемый:</p>
<pre class="brush:ts;">
interface IPerson {
    name: string;
}
class Person {
 
    name: string;
    constructor(userName: string) {
 
        this.name = userName;
    }
}
 
class Employee extends Person {
 
    company: string;
    constructor(userName: string, company: string) {
 
        super(userName);
        this.company = company;
    }
}
function printPerson(user: IPerson): void {
    console.log(`IPerson ${user.name}`);
}

</pre>
<p>Функция getPersonName в качестве параметра принимает объект интерфейса IPerson:</p>
<pre class="brush:ts;">
let tom: Person = new Employee("Tom", "Microsoft");
printPerson(tom);
 
printPerson({ name: "Sam" });
//printPerson({ name: "Bob", company:"Microsoft" }); // ошибка
</pre>
<p>Ни класс Person, ни класс Employee не применяют интерфейс IPerson, однако мы можем их использовать, так как они имеют все те же свойства и методы, что интерфейс IPerson (в данном случае только 
свойство name).</p>
<p>Объект <code>{ name: "Sam" }</code> также является объектом интерфейса, так как он имеет свойство <code>name</code>. В то же время при передаче объекта 
<code>{ name: "Bob", company:"Microsoft" }</code> мы получим ошибку, так как он уже расширяет возможности IPerson, добавляя свойство <code>company</code> и напрямую интерфейсу IPerson не 
соответствует. Но даже в этом случае мы его можем вполне использовать, применив преобразование типов:</p>
<pre class="brush:ts;">
printPerson({ name: "Bob", company:"Microsoft" } as IPerson);
</pre>
<h3>Оператор instanceOf</h3>
<p>С помощью оператора <span class="b">instanceOf</span> можно проверить, принадлежит ли объект определенному классу:</p>
<pre class="brush:ts;">
let tom = new Employee("Tom", "Microsoft");
if (tom instanceof Person) {
    console.log("Tom is a Person");
}
else {
    console.log("Tom is not a Person");
}
</pre>
