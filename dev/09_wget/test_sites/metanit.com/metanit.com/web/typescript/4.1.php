<p>Для установки связи с внешними файлами скриптов javascript в TS служат декларативные или заголовочные файлы. Это файлы с расширением 
<span class="ii">.d.ts</span>, они описывают синтаксис и структуру функций и свойств, которые могут использоваться в программе, не предоставляя при этом конкретной реализации. 
Их действие во многом похоже на работу файлов с расширением <span class="ii">.h</span> в языках C/C++. Они выполняют своего рода роль оберток над библиотеками JavaScript.</p>
<p>Рассмотрим, как мы можем использовать заголовочные файлы. Иногда в программах на javascript используются глобальные переменные, которые должны быть видны для всех функций приложения. Например, пусть на веб-странице (или во 
внешнем подключаемом файле javascript) в коде js определена переменная:</p>
<pre class="brush:xml;">
&lt;!DOCTYPE html&gt;
&lt;html&gt;
&lt;head&gt;
    &lt;meta charset="utf-8" /&gt;
    &lt;title&gt;Metanit.com&lt;/title&gt;
&lt;/head&gt;
&lt;body&gt;
    &lt;h2&gt;Приложение на TypeScript&lt;/h2&gt;
	&lt;script&gt;
		let message = "Hello TypeScript!";
    &lt;/script&gt;
    &lt;script src="app.js"&gt;&lt;/script&gt;
&lt;/body&gt;
&lt;/html&gt;
</pre>
<p>В данном случае для простоты переменная определена веб-странице, хотя она также могла быть определена во внешнем подключенном js-файле.</p>
<p>И, допустим, мы хотим использовать эту переменную <code>message</code> в коде TypeScript в файле app.ts:</p>
<pre class="brush:ts;">
console.log(message);
</pre>
<p>При запуске приложения компилятор TS не сможет скомпилировать программу, так как для кода TS глобальная переменная пока не существует. 
В этом случае нам надо подключать определение глобальной переменной с помощью декларативных файлов. Для этого добавим в проект 
новый файл, который назовем <span class="b">globals.d.ts</span> и который будет иметь следующее содержимое:</p>
<pre class="brush:ts;">
declare let message: string;
</pre>
<p>С помощью ключевого слова <span class="b">declare</span> в программу на TS подключается определение глобальной переменной.</p>
<p>То есть у нас получится следующая структура проекта:</p>
<ul style="list-style-type:none;">
<li><p>app.ts</p></li>
<li><p>globals.d.ts</p></li>
<li><p>index.html</p></li>
</ul>
<h3>Компиляция</h3>
<p>Если мы компилируем, передавая компилятору в консоли название файла:</p>
<div class="console"><pre class="consoletext">tsc app.ts</pre></div>
<p>То в этом случае компилятор не найдет автоматически файл globals.d.ts. В этом случае нам надо в файле <span class="b">app.ts</span> явно указать расположение файла globals.d.ts с помощью директивы <span class="b">reference</span></p>
<pre class="brush:ts;">
/// &lt;reference path="globals.d.ts" /&gt;
console.log(message);
</pre>
<img src="./pics/4.png" alt="Заголовочные файлы в TypeScript" />
<p>Если же мы полагаемся на файл конфигурации tsconfig.json, просто выполняя команду</p>
<div class="console"><pre class="consoletext">tsc</pre></div>
<p>то директиву <code>/// &lt;reference path="globals.d.ts" /&gt;</code> можно не указывать.</p>
<p>Подобным образом мы можем подлюкчать другие компоненты кода JavaScript - функции, объекты, классы. Рассмотрим их подключение.</p>
<h3>Функции</h3>
<p>Пусть на веб-странице в коде js объявлены две следующие функции:</p>
<pre class="brush:js;">
let message = "Hello TypeScript!";
function hello(){
	console.log(message);
}
		
function sum(a, b){
	return a + b;
}
</pre>
<p>Функция <code>hello()</code> выводит значение переменной message на консоль, а функция <code>sum()</code> возвращает сумму двух чисел.</p>
<p>И, допустим, в коде TS мы хотим вызывать эти функции:</p>
<pre class="brush:ts;">
hello();

let result = sum(2, 5);
console.log(result);
</pre>
<p>В этом случае подключение в файле globals.d.ts выглядело бы так:</p>
<pre class="brush:ts;">
declare function hello(): void;
declare function sum(a: number, b: number): number;
</pre>
<h3>Подключение объектов</h3>
<p>Пусть в коде JavaScript есть следующий объект:</p>
<pre class="brush:js;">
const tom = {
	name: "Tom",
	age: 37,
	print(){
		console.log(`Name: ${this.name}  Age: ${this.age}`);
	}
}
</pre>
<p>Используем этот объект в коде typescript:</p>
<pre class="brush:ts;">tom.print();</pre>
<p>В этом случае определение объекта в файле <span class="b">globals.d.ts</span> выглядело бы так:</p>
<pre class="brush:ts;">declare const tom: {name: string, age: number, print: ()=> void};</pre>
<h3>Подключени сложных объектов</h3>
<p>Однако может возникнуть сложность с подключением более сложных объектов. Например, пусть есть такой объект javascript:</p>
<pre class="brush:js;">
var points = [{ X: 10, Y: 34 },
              { X: 24, Y: 65 },
               { X: 89, Y: 12 }];
</pre>
<p>Для данного массива объектов в файле <span class="b">globals.d.ts</span> мы можем определить соответствующий отдельному объекту интерфейс 
и подключить массив объектов некоторого интерфейса, который содержит два свойства X и Y:</p>
<pre class="brush:ts;">
interface IPoint {
    X: number;
    Y: number;
}
declare var points: IPoint[];
</pre>
<p>И в TS мы сможем использовать этот массив:</p>
<pre class="brush:ts;">
for (let point of points){
	console.log(`Точка с координатами X = ${point.X}  Y = ${point.Y}`);
}
</pre>
<p>Консольный вывод браузера:</p>
<div class="browser">
Точка с координатами X = 10  Y = 34
Точка с координатами X = 24  Y = 65
Точка с координатами X = 89  Y = 12
</div>
<h3>Подключение классов</h3>
<p>Рассмотрим последний пример - подключение в typescript классов, определенных в javascript. Пусть в коде JavaScript определен следующий класс Person:</p>
<pre class="brush:js;">
class Person{
	constructor(name, age){
		this.name = name;
		this.age = age;
	}
	display(){
		console.log(this.name, this.age);
	}
}
</pre>
<p>Для этого класса в файле <span class="b">globals.d.ts</span> определим следующее объявление класса:</p>
<pre class="brush:ts;">
declare class Person{
	
	name: string;
	age: number;
	constructor(name: string, age: number);
	display(): void;
}
</pre>
<p>Для класса прописываем все его поля и методы, при этом методы (в том числе конструктор) не имеют реализации, для них только определяются параметры и их типы и тип возвращаемого значения.</p>
<p>И в коде TypeScript используем этот класс:</p>
<pre class="brush:ts;">
let tom = new Person("Tom", 37);
tom.display();          // Tom 37
console.log(tom.name);  // Tom
</pre>

