<p>Паттерн "Наблюдатель" (Observer) представляет поведенческий шаблон проектирования, который использует отношение "один ко многим". 
В этом отношении есть один наблюдаемый объект и множество наблюдателей. И при изменении наблюдаемого объекта автоматически 
происходит оповещение всех наблюдателей.</p>
<p>Данный паттерн еще называют Publisher-Subscriber (издатель-подписчик), поскольку отношения издателя и подписчиков характеризуют действие 
данного паттерна: подписчики подписываются email-рассылку определенного сайта. Сайт-издатель с помощью email-рассылки уведомляет всех подписчиков 
о изменениях. А подписчики получают изменения и производят определенные действия: могут зайти на сайт, могут проигнорировать уведомления и т.д.</p>
<h3>Когда использовать паттерн Наблюдатель?</h3>
<ul>
<li><p>Когда система состоит из множества классов, объекты которых должны находиться в согласованных состояниях</p></li>
<li><p>Когда общая схема взаимодействия объектов предполагает две стороны: одна рассылает сообщения и является главным, другая получает сообщения и реагирует на них. 
Отделение логики обеих сторон позволяет их рассматривать независимо и использовать отдельно друга от друга.</p></li>
<li><p>Когда существует один объект, рассылающий сообщения, и множество подписчиков, которые получают сообщения. При этом точное число 
подписчиков заранее неизвестно и процессе работы программы может изменяться.</p></li>
</ul>
<p>С помощью диаграмм UML данный шаблон можно выразить следующим образом:</p>
<img src="pics/observer.png" alt="Паттерн Наблюдатель в C#" />
<p>Формальное определение паттерна на языке C# может выглядеть следующим образом:</p>
<pre class="brush:c#;">
interface IObservable
{
    void AddObserver(IObserver o);
    void RemoveObserver(IObserver o);
    void NotifyObservers();
}
class ConcreteObservable : IObservable
{
    private List&lt;IObserver&gt; observers;
	public ConcreteObservable()
    {
        observers = new List&lt;IObserver&gt;();
    }
    public void AddObserver(IObserver o)
    {
        observers.Add(o);
    }

    public void RemoveObserver(IObserver o)
    {
        observers.Remove(o);
    }

    public void NotifyObservers()
    {
        foreach (IObserver observer in observers)
            observer.Update();
    }
}

interface IObserver
{
    void Update();
}
class ConcreteObserver :IObserver
{
    public void Update()
    {
    }
}
</pre>
<h3>Участники</h3>
<ul>
<li><p><span class="bb">IObservable</span>: представляет наблюдаемый объект. Определяет три метода: <code>AddObserver()</code> (для добавления наблюдателя), 
<code>RemoveObserver()</code> (удаление набюдателя) и <code>NotifyObservers()</code> (уведомление наблюдателей)</p></li>
<li><p><span class="bb">ConcreteObservable</span>: конкретная реализация интерфейса IObservable. Определяет коллекцию объектов наблюдателей.</p></li>
<li><p><span class="bb">IObserver</span>: представляет наблюдателя, который подписывается на все уведомления наблюдаемого объекта. 
Определяет метод <code>Update()</code>, который вызывается наблюдаемым объектом для уведомления наблюдателя.</p></li>
<li><p><span class="bb">ConcreteObserver</span>: конкретная реализация интерфейса IObserver.</p></li>
</ul>
<p>При этом наблюдаемому объекту не надо ничего знать о наблюдателе кроме того, что тот реализует метод <code>Update()</code>. 
С помощью отношения агрегации реализуется слабосвязанность обоих компонентов. Изменения в наблюдаемом объекте не виляют на наблюдателя и 
наоборот.</p>
<p>В определенный момент наблюдатель может прекратить наблюдение. И после этого оба объекта - наблюдатель и наблюдаемый 
могут продолжать существовать в системе независимо друг от друга.</p>
<p>Рассмотрим реальный пример применения шаблона. Допустим, у нас есть биржа, где проходят торги, и есть брокеры и банки, которые 
следят за поступающей информацией и в зависимости от поступившей информации производят определенные действия:</p>
<pre class="brush:c#;">
class Program
{
    static void Main(string[] args)
    {
        Stock stock = new Stock();
        Bank bank = new Bank("ЮнитБанк", stock);
        Broker broker = new Broker("Иван Иваныч", stock);
        // имитация торгов
        stock.Market();
        // брокер прекращает наблюдать за торгами
        broker.StopTrade();
        // имитация торгов
        stock.Market();

        Console.Read();
    }
}

interface IObserver
{
    void Update(Object ob);
}

interface IObservable
{
    void RegisterObserver(IObserver o);
    void RemoveObserver(IObserver o);
    void NotifyObservers();
}

class Stock : IObservable
{
    StockInfo sInfo; // информация о торгах

    List&lt;IObserver&gt; observers;
    public Stock()
    {
        observers = new List&lt;IObserver&gt;();
        sInfo= new StockInfo();
    }
    public void RegisterObserver(IObserver o)
    {
        observers.Add(o);
    }

    public void RemoveObserver(IObserver o)
    {
        observers.Remove(o);
    }

    public void NotifyObservers()
    {
        foreach(IObserver o in observers)
        {
            o.Update(sInfo);
        }
    }

    public void Market()
    {
        Random rnd = new Random();
        sInfo.USD = rnd.Next(20, 40);
        sInfo.Euro = rnd.Next(30, 50);
        NotifyObservers();
    }
}

class StockInfo
{
    public int USD { get; set; }
    public int Euro { get; set; }
}

class Broker : IObserver
{
    public string Name { get; set; }
    IObservable stock;
    public Broker(string name, IObservable obs)
    {
        this.Name = name;
        stock = obs;
        stock.RegisterObserver(this);
    }
    public void Update(object ob)
    {
        StockInfo sInfo = (StockInfo)ob;

        if(sInfo.USD&gt;30)
            Console.WriteLine("Брокер {0} продает доллары;  Курс доллара: {1}", this.Name, sInfo.USD);
        else
            Console.WriteLine("Брокер {0} покупает доллары;  Курс доллара: {1}", this.Name, sInfo.USD);
    }
    public void StopTrade()
    {
        stock.RemoveObserver(this);
		stock=null;
    }
}

class Bank : IObserver
{
    public string Name { get; set; }
    IObservable stock;
    public Bank(string name, IObservable obs)
    {
        this.Name = name;
        stock = obs;
        stock.RegisterObserver(this);
    }
    public void Update(object ob)
    {
        StockInfo sInfo = (StockInfo)ob;

        if (sInfo.Euro &gt; 40)
            Console.WriteLine("Банк {0} продает евро;  Курс евро: {1}", this.Name, sInfo.Euro);
        else
            Console.WriteLine("Банк {0} покупает евро;  Курс евро: {1}", this.Name, sInfo.Euro);
    }
}
</pre>
<p>Итак, здесь наблюдаемый объект представлен интерфейсом <code>IObservable</code>, а наблюдатель - интерфейсом <code>IObserver</code>. 
Реализацией интерфейса IObservable является класс <code>Stock</code>, который символизирует валютную биржу. В этом классе определен метод 
<code>Market()</code>, который имитирует торги и инкапсулирует всю информацию о валютных курсах в объекте <code>StockInfo</code>. После 
проведения торгов производится уведомление всех наблюдателей.</p>
<p>Реализациями интерфейса IObserver являются классы <code>Broker</code>, представляющий брокера, и <code>Bank</code>, представляющий банк. 
При этом метод <code>Update()</code> интерфейса IObserver принимает в качестве параметра некоторый объект. Реализация этого метода 
подразумевает получение через данный параметр объекта StockInfo с текущей информацией о торгах и произведение некоторых действий: покупка или 
продажа долларов и евро. Дело в том, что часто необходимо информировать наблюдателя об изменении состояния наблюдаемого объекта. В данном 
случае состояние заключено в объекте StockInfo. И одним из вариантом информирования наблюдателя о состоянии является push-модель, при которой 
наблюдаемый объект передает (иначе говоря толкает - push) данные о своем состоянии, то есть передаем в виде параметра метода <code>Update()</code>.</p>
<p>Альтернативой push-модели является pull-модель, когда наблюдатель вытягивает (pull) из наблюдаемого объекта данные о состоянии с помощью 
дополнительных методов.</p>
<p>Также в классе брокера определен дополнительный метод <code>StopTrade()</code>, с помощью которого брокер 
может отписаться от уведомлений биржи и перестать быть наблюдателем.</p>
