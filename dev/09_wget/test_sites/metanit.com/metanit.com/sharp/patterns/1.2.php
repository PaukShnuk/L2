<p>Прежде чем приступить к изучению основных паттернов также рассмотрим основные отношения между объектами, которые помогут нам понять 
связи между сущностями при их использовании в паттернах. Мы можем выделить несколько основных отношений: наследование, 
реализация, ассоциация, композиция и агрегация.</p>
<h3>Наследование</h3>
<p>Наследование является базовым принципом ООП и позволяет одному классу (наследнику) унаследовать функционал другого класса (родительского). 
Нередко отношения наследования еще называют генерализацией или обобщением. 
Наследование определяет отношение <span class="bb">IS A</span>, то есть "является". Например:</p>
<pre class="brush:c#;">
class User
{
    public int Id { get; set; }
    public string Name { get; set; }
}

class Manager : User
{
    public string Company{ get; set; }
}
</pre>
<p>В данном случае используется наследование, а объекты класса Manager также <span class="bb">являются</span> и объектами класса User.</p>
<p>С помощью диаграмм UML отношение между классами выражается в незакрашенной стрелочке от класса-наследника к классу-родителю:</p>
<img src="pics/2.1.png" alt="Отношение наследования в UML и .NET" />
<h3>Реализация</h3>
<p>Реализация предполагает определение интерфейса и его реализация в классах. Например, имеется интерфейс IMovable с методом Move, 
который реализуется в классе Car:</p>
<pre class="brush:c#;">
public interface IMovable
{
    void Move();
}
public class Car : IMovable
{
    public void Move()
    {
        Console.WriteLine("Машина едет");
    }
}
</pre>
<p>С помощью диаграмм UML отношение реализации также выражается в незакрашенной стрелочке от класса к интерфейсу, только линия теперь пунктирная:</p>
<img src="pics/2.2.png" alt="Отношение реализации в UML и .NET" />
<h3>Ассоциация</h3>
<p>Ассоциация - это отношение, при котором объекты одного типа неким образом связаны с объектами другого типа. Например, объект одного типа 
содержит или использует объект другого типа. Например, игрок играет в определенной команде:</p>
<pre class="brush:c#;">
class Team
{

}
class Player
{
    public Team Team { get; set; }
}
</pre>
<p>Класс Player связан отношением ассоциации с класом Team. На схемах UML ассоциация обозначается в виде обычно стрелки:</p>
<img src="pics/2.5.png" alt="Отношение Ассоциации в UML и .NET" />
<p>Нередко при отношении ассоциации указывается кратность связей. В данном случае единица у Team и звездочка у Player на диаграмме отражает 
связь 1 ко многим. То есть одна команда будет соответствовать многим игрокам.</p>
<p>Агрегация и композиция являются частными случаями ассоциации.</p>
<h3>Композиция</h3>
<p>Композиция определяет отношение <span class="bb">HAS A</span>, то есть отношение "имеет". Например, в класс автомобиля содержит объект класса электрического двигателя:</p>
<pre class="brush:c#;">
public class ElectricEngine
{ }

public class Car
{
    ElectricEngine engine;
	public Car()
    {
        engine = new ElectricEngine();
    }
}
</pre>
<p>При этом класс автомобиля полностью управляет жизненным циклом объекта двигателя. При уничтожении объекта автомобиля в области памяти 
вместе с ним будет уничтожен и объект двигателя. И в этом плане объект автомобиля является главным, а объект двигателя - зависимой.</p>
<p>На диаграммах UML отношение композиции проявляется в обычной стрелке от главной сущности к зависимой, при этом со стороны 
главной сущности, которая содержит, объект второй сущности, располагается закрашенный ромбик:</p>
<img src="pics/2.3.png" alt="Отношение композиции в UML и .NET" />
<h3>Агрегация</h3>
<p>От композиции следует отличать агрегацию. Она также предполагает отношение <span class="bb">HAS A</span>, но реализуется она иначе:</p>
<pre class="brush:c#;">
public abstract class Engine
{ }

public class Car
{
    Engine engine;
	public Car(Engine eng)
    {
        engine = eng;
    }
}
</pre>
<p>При агрегации реализуется слабая связь, то есть в данном случае объекты Car и Engine будут равноправны. В конструктор Car передается 
ссылка на уже имеющийся объект Engine. И, как правило, определяется ссылка не на конкретный класс, а на абстрактный класс или интерфейс, 
что увеличивает гибкость программы.</p>
<p>Отношение агрегации на диаграммах UML отображается также, как и отношение композиции, только теперь ромбик будет незакрашенным:</p>
<img src="pics/2.4.png" alt="Отношение агрегации в UML и .NET" />
<p>При проектировании отношений между классами надо учитывать некоторые общие рекомендации. В частности, вместо наследования следует 
предпочитать композицию. При наследовании весь функционал класса-наследника жестко определен на этапе компиляции. И во время 
выполнения программы мы не можем его динамически переопределить. А класс-наследник не всегда может переопределить код, 
который определен в родительском классе. Композиция же позволяет динамически определять поведение объекта во время выполнения, и поэтому является более гибкой.</p>
<p>Вместо композиции следует предпочитать агрегацию, как более гибкий способ связи компонентов. В то же время не всегда агрегация 
уместна. Например, у нас есть класс человека, который содержит объект нервной системы. Понятно, что в реальности, по крайней мере на текущий момент, 
невозможно вовне определить нервную систему и внедрить ее в человека. То есть в данном случае человек будет главным компонентом, а нервная система - зависимым, подчиненным, 
и их создание и жизненный цикл будет происходить совместно, поэтому здесь лучше выбрать композицию.</p>
