<p>Большинство объектов, используемых в программах на C#, относятся к управляемым или managed-коду. Такие объекты управляются CLR и 
легко очищаются сборщиком мусора. Однако вместе с тем встречаются также и такие объекты, которые задействуют неуправляемые объекты 
(подключения к файлам, базам данных, сетевые подключения и т.д.). 
Такие неуправляемые объекты обращаются к API операционной системы. Сборщик мусора может справиться с управляемыми объектами, 
однако он не знает, как удалять неуправляемые объекты. В этом случае разработчик должен сам реализовывать механизмы очистки на уровне программного кода.</p>
<p>Освобождение неуправляемых ресурсов подразумевает реализацию одного из двух механизмов:</p>
<ul>
<li><p>Создание деструктора</p></li>
<li><p>Реализация классом интерфейса System.IDisposable</p></li>
</ul>
<h3>Создание деструкторов</h3>
<p>Если вы вдруг программировали на языке C++, то наверное уже знакомы с концепцией деструкторов. Метод деструктора носит имя класса (как и конструктор), 
перед которым стоит знак тильды (<code>~</code>).</p>
<p>Деструкторы можно определить только в классах. Деструктор в отличие от конструктора не может иметь модификаторов доступа и параметры. Деструктор не может быть унаследован или переопределен. При этом каждый класс может 
иметь только один деструктор.</p>
<p>Например, определим в классе Person простейший деструктор:</p>
<pre class="brush:c#;">
class Person
{
    public string Name { get;}
    public Person(string name) =&gt; Name = name;

    ~Person()
    {
        Console.WriteLine($"{Name} has deleted");
    }
}
</pre>
<p>В данном случае в деструкторе в целях демонстрации просто выводится строка 
на консоль, которая уведомляет, что объект удален. Но в реальных программах в деструктор вкладывается логика освобождения неуправляемых ресурсов.</p>
<p>Однако на деле при очистке сборщик мусора вызывает не деструктор, а метод <span class="b">Finalize</span>. Все потому, что компилятор C# компилирует 
деструктор в конструкцию, которая эквивалентна следующей:</p>
<pre class="brush:c#;">
protected override void Finalize()
{
    try
    {
        // здесь идут инструкции деструктора
    }
	finally
	{
		base.Finalize();
	}
}
</pre>
<p>Метод Finalize уже определен в базовом для всех типов классе Object, однако данный метод нельзя так просто переопределить. И фактическая 
его реализация происходит через создание деструктора.</p>
<p>Используя в программе класс Person, после ее завершения можно будет увидеть на консоли сообщение об удалении объекта tom:</p>
<pre class="brush:c#;">
Test();        
GC.Collect();   // очистка памяти под объект tom
Console.Read(); // ставим задержку

void Test()
{
    Person tom = new Person("Tom");
}

public class Person
{
    public string Name { get;}
    public Person(string name) =&gt; Name = name;

    ~Person()
    {
        Console.WriteLine($"{Name} has been deleted");
    }
}
</pre>
<p>Обратите внимание, что даже после завершения метода Test и соответственно удаления из стека ссылки на объект Person в куче, может не последовать немедленного вызова деструктора. 
Лишь при завершении всей программы гарантировано произойдет очистка памяти. Однако с .NET 5 и в последующих версиях при завершении программы деструкторы не вызываются. 
Поэтому в программе выше для более быстрой очистки памяти применяется метод GC.Collect и для гарантированного вызова деструктора устанавливается задержка с помощью вызова 
<code>Console.Read()</code>, который ожидает от пользователя ввода.</p>
<p>На уровне памяти это выглядит так: сборщик мусора при размещении объекта в куче определяет, поддерживает ли данный объект метод <code>Finalize</code>. 
И если объект имеет метод Finalize, то указатель на него сохраняется в специальной таблице, которая называется очередь финализации. Когда наступает 
момент сборки мусора, сборщик видит, что данный объект должен быть уничтожен, и если он имеет метод Finalize, то он копируется 
в еще одну таблицу и окончательно уничтожается лишь при следующем проходе сборщика мусора.</p>
<p>Стоит отметить, что точное время вызова деструктора не определено. Кроме того, при финализации двух связанных объектов порядок вызова деструкторов не гарантируется. То есть если объект A хранит ссылку на объект B, 
и при этом оба эти объекта имеют деструкторы, то для объекта B деструктор моет уже отработать в то время, как для 
объекта A деструктор только начнет работу.</p>
<p>И здесь мы можем столкнуться со следующей проблемой: а что если нам немедленно надо вызвать деструктор и освободить все связанные с объектом неуправляемые ресурсы? 
В этом случае мы можем использовать второй подход - реализацию интерфейса IDisposable.</p>

<h3>Интерфейс IDisposable</h3>
<p>Интерфейс IDisposable объявляет один единственный метод <span class="b">Dispose</span>, в котором при реализации интерфейса в классе должно происходить 
освобождение неуправляемых ресурсов. Например:</p>
<pre class="brush:c#;">
Test();

void Test()
{
    Person? tom = null;
    try
    {
        tom = new Person("Tom");
    }
    finally
    {
        tom?.Dispose();
    }
}

public class Person : IDisposable
{
    public string Name { get;}
    public Person(string name) =&gt; Name = name;

    public void Dispose()
    {
        Console.WriteLine($"{Name} has been disposed");
    }
}
</pre>
<p>В данном коде используется конструкция try...finally. По сути эта конструкция по функционалу в общем эквивалентна следующим двум строкам кода:</p>
<pre class="brush:c#;">
Person tom = new Person("Tom");
tom.Dispose();
</pre>
<p>Но конструкцию try...finally предпочтительнее использовать при вызове метода Dispose, так как она гарантирует, что даже в случае возникновения исключения 
произойдет освобождение ресурсов в методе Dispose.</p>
<h3>Комбинирование подходов</h3>
<p>Мы рассмотрели два подхода. Какой же из них лучше? С одной стороны, метод Dispose позволяет в любой момент времени вызвать освобождение связанных ресурсов, 
а с другой - программист, использующий наш класс, может забыть поставить в коде вызов метода Dispose. В общем бывают различные ситуации. И чтобы сочетать плюсы 
обоих подходов мы можем использовать комбинированный подход. Microsoft предлагает нам использовать следующий формализованный шаблон:</p>
<pre class="brush:c#;">
public class SomeClass: IDisposable
{
    private bool disposed = false;

    // реализация интерфейса IDisposable.
    public void Dispose()
    {
        // освобождаем неуправляемые ресурсы
        Dispose(true);
		// подавляем финализацию
        GC.SuppressFinalize(this);
    }

    protected virtual void Dispose(bool disposing)
    {
        if (disposed) return;
        if (disposing)
        {
            // Освобождаем управляемые ресурсы
        }
        // освобождаем неуправляемые объекты
        disposed = true;
    }

    // Деструктор
    ~SomeClass()
    {
        Dispose (false);
    }
}
</pre>
<p>Логика очистки реализуется перегруженной версией метода <code>Dispose(bool disposing)</code>. 
Если параметр disposing имеет значение true, то данный метод вызывается из публичного метода Dispose, если false - то из деструктора.</p>
<p>При вызове деструктора в качестве параметра 
disposing передается значение false, чтобы избежать очистки управляемых ресурсов, так как мы не можем быть уверенными в их состоянии, что они до сих 
пор находятся в памяти. И в этом случае остается полагаться на деструкторы этих ресурсов. Ну и в обоих случаях освобождаются неуправляемые ресурсы.</p>
<p>Еще один важный момент - вызов в методе Dispose метода <code>GC.SuppressFinalize(this)</code>. GC.SuppressFinalize не позволяет системе выполнить метод Finalize для данного объекта. Если же 
в классе деструктор не определен, то вызов этого метода не будет иметь никакого эффекта.</p>
<p>Таким образом, даже если разработчик не использует в программе метод Dispose, все равно произойдет очистка и освобождение ресурсов.</p>
<h4>Общие рекомендации по использованию Finalize и Dispose</h4>
<ul>
<li><p>Деструктор следует реализовывать только у тех объектов, которым он действительно необходим, так как метод Finalize оказывает сильное 
влияние на производительность</p></li>
<li><p>После вызова метода Dispose необходимо блокировать у объекта вызов метода Finalize с помощью  GC.SuppressFinalize</p></li>
<li><p>При создании производных классов от базовых, которые реализуют интерфейс IDisposable, следует также вызывать метод Dispose базового класса:</p>
<pre class="brush:c#;">
public class Derived: Base
{
    private bool IsDisposed = false;

    protected override void Dispose(bool disposing)
    {
        if (IsDisposed) return;
        if (disposing)
        {
			// Освобождение управляемых ресурсов
		}
        IsDisposed = true;
		// Обращение к методу Dispose базового класса
        base.Dispose(disposing);
    }
}
</pre>
</li>
<li><p>Отдавайте предпочтение комбинированному шаблону, реализующему как метод Dispose, так и деструктор</p></li>
</ul>
