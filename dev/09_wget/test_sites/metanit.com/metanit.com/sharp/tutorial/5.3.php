<p>Подобно паре <code>Directory/DirectoryInfo</code> для работы с файлами предназначена пара классов <span class="b">File</span> и 
<span class="b">FileInfo</span>. С их помощью мы можем создавать, удалять, перемещать файлы, получать их свойства и многое другое.</p>
<h3>FileInfo</h3>
<p>Некоторые полезные методы и свойства класса <span class="b">FileInfo</span>:</p>
<ul>
<li><p><span class="b">CopyTo(path)</span>: копирует файл в новое место по указанному пути path</p></li>
<li><p><span class="b">Create()</span>: создает файл</p></li>
<li><p><span class="b">Delete()</span>: удаляет файл</p></li>
<li><p><span class="b">MoveTo(destFileName)</span>: перемещает файл в новое место</p></li>
<li><p>Свойство <span class="b">Directory</span>: получает родительский каталог в виде объекта DirectoryInfo</p></li>
<li><p>Свойство <span class="b">DirectoryName</span>: получает полный путь к родительскому каталогу</p></li>
<li><p>Свойство <span class="b">Exists</span>: указывает, существует ли файл</p></li>
<li><p>Свойство <span class="b">Length</span>: получает размер файла</p></li>
<li><p>Свойство <span class="b">Extension</span>: получает расширение файла</p></li>
<li><p>Свойство <span class="b">Name</span>: получает имя файла</p></li>
<li><p>Свойство <span class="b">FullName</span>: получает полное имя файла</p></li>
</ul>
<p>Для создания объекта FileInfo применяется конструктор, который получает в качестве параметра путь к файлу:</p>
<pre class="brush:c#;">
FileInfo fileInf = new FileInfo(@"C:\app\content.txt");
</pre>
<h3>File</h3>
<p>Класс <code>File</code> реализует похожую функциональность с помощью статических методов:</p>
<ul>
<li><p><span class="b">Copy()</span>: копирует файл в новое место</p></li>
<li><p><span class="b">Create()</span>: создает файл</p></li>
<li><p><span class="b">Delete()</span>: удаляет файл</p></li>
<li><p><span class="b">Move</span>: перемещает файл в новое место</p></li>
<li><p><span class="b">Exists(file)</span>: определяет, существует ли файл</p></li>
</ul>
<h3>Пути к файлам</h3>
<p>Для работы с файлами можно применять как абсолютные, так и относительные пути:</p>
<pre class="brush:c#;">
// абсолютные пути
string path1 = @"C:\Users\eugene\Documents\content.txt";  // для Windows
string path2 = "C:\\Users\\eugene\\Documents\\content.txt";  // для Windows
string path3 = "/Users/eugene/Documents/content.txt";  // для MacOS/Linux

// относительные пути
string path4 = "MyDir\\content.txt";  // для Windows
string path5 = "MyDir/content.txt";  // для MacOS/Linux
</pre>
<h3>Получение информации о файле</h3>
<pre class="brush:c#;">
string path = @"C:\Users\eugene\Documents\content.txt";
// string path = "/Users/eugene/Documents/content.txt";  // для MacOS/Linux
FileInfo fileInfo = new FileInfo(path);
if (fileInfo.Exists)
{
    Console.WriteLine($"Имя файла: {fileInfo.Name}");
    Console.WriteLine($"Время создания: {fileInfo.CreationTime}");
    Console.WriteLine($"Размер: {fileInfo.Length}");
}
</pre>
<h3>Удаление файла</h3>
<pre class="brush:c#;">
string path = @"C:\app\content.txt";
FileInfo fileInf = new FileInfo(path);
if (fileInf.Exists)
{
   fileInf.Delete();
   // альтернатива с помощью класса File
   // File.Delete(path);
}
</pre>
<h3>Перемещение файла</h3>
<pre class="brush:c#;">
string path =  @"C:\OldDir\content.txt";
string newPath = @"C:\NewDir\index.txt";
FileInfo fileInf = new FileInfo(path);
if (fileInf.Exists)
{
   fileInf.MoveTo(newPath);       
   // альтернатива с помощью класса File
   // File.Move(path, newPath);
}
</pre>
<p>Если файл по новому пути уже существует, то с помощью дополнительного параметра можно указать, надо ли перезаписать файл (при значении true файл перезаписывается)</p>
<pre class="brush:c#;">
string path =  @"C:\OldDir\content.txt";
string newPath = @"C:\NewDir\index.txt";
FileInfo fileInf = new FileInfo(path);
if (fileInf.Exists)
{
   fileInf.MoveTo(newPath, true);       
   // альтернатива с помощью класса File
   // File.Move(path, newPath, true);
}
</pre>
<h3>Копирование файла</h3>
<pre class="brush:c#;">
string path =  @"C:\OldDir\content.txt";
string newPath = @"C:\NewDir\index2.txt";
FileInfo fileInf = new FileInfo(path);
if (fileInf.Exists)
{
   fileInf.CopyTo(newPath, true);      
   // альтернатива с помощью класса File
   // File.Copy(path, newPath, true);
}
</pre>
<p>Метод <code>CopyTo</code> класса <code>FileInfo</code> принимает два параметра: путь, по которому файл будет копироваться, и булевое значение, 
которое указывает, надо ли при копировании перезаписывать файл (если <code>true</code>, как в случае выше, файл при копировании перезаписывается). Если же в 
качестве последнего параметра передать значение <code>false</code>, то если такой файл уже существует, приложение выдаст ошибку.</p>
<p>Метод <code>Copy</code> класса <code>File</code> принимает три параметра: путь к исходному файлу, путь, по которому файл будет копироваться, и 
булевое значение, указывающее, будет ли файл перезаписываться.</p>
<h3>Чтение и запись файлов</h3>
<p>В дополнение к вышерассмотренным методам класс File также предоставляет ряд методов для чтения-записи текстовых и бинарных файлов:</p>
<ul>
<li><p><span class="b">AppendAllLines(String, IEnumerable&lt;String&gt;)</span> / 
<span class="b">AppendAllLinesAsync(String, IEnumerable&lt;String&gt;, CancellationToken)</span></p>
<p>добавляют в файл набор строк. Если файл не существует, то он создается</p>
</li>
<li><p><span class="b">AppendAllText(String, String)</span> / <span class="b">AppendAllTextAsync(String, String, CancellationToken)</span></p>
<p>добавляют в файл строку. Если файл не существует, то он создается</p>
</li>

<li><p><span class="b">byte[] ReadAllBytes (string path)</span> / 
<span class="b">Task&lt;byte[]&gt; ReadAllBytesAsync (string path, CancellationToken cancellationToken)</span></p>
<p>считывают содержимое бинарного файла в массив байтов</p>
</li>

<li><p><span class="b">string[] ReadAllLines (string path)</span> / 
<span class="b">Task&lt;string[]&gt; ReadAllLinesAsync (string path, CancellationToken cancellationToken)</span></p>
<p>считывают содержимое текстового файла в массив строк</p>
</li>


<li><p><span class="b">string ReadAllText (string path)</span> / 
<span class="b">Task&lt;string&gt; ReadAllTextAsync (string path, CancellationToken cancellationToken)</span></p>
<p>считывают содержимое текстового файла в строку</p>
</li>


<li><p><span class="b">IEnumerable&lt;string&gt; ReadLines (string path)</span></p>
<p>считывают содержимое текстового файла в коллекцию строк</p>
</li>

<li><p><span class="b">void WriteAllBytes (string path, byte[] bytes)</span> / 
<span class="b">Task WriteAllBytesAsync (string path, byte[] bytes, CancellationToken cancellationToken)</span></p>
<p>записывают массив байт в бинарный файл. Если файл не существует, он создается. Если существует, то перезаписывается</p>
</li>

<li><p><span class="b">void WriteAllLines (string path, string[] contents)</span> / 
<span class="b">Task WriteAllLinesAsync (string path, IEnumerable&lt;string&gt; contents, CancellationToken cancellationToken)</span></p>
<p>записывают массив строк в текстовый файл. Если файл не существует, он создается. Если существует, то перезаписывается</p>
</li>

<li><p><span class="b">WriteAllText (string path, string? contents)</span> / 
<span class="b">Task WriteAllTextAsync (string path, string? contents, CancellationToken cancellationToken)</span></p>
<p>записывают строку в текстовый файл. Если файл не существует, он создается. Если существует, то перезаписывается</p>
</li>
</ul>
<p>Как видно, эти методы покрывают практически все основные сценарии - чтение и запись текстовых и бинарных файлов. Причем в зависимости от задачи можно применять 
как синхронные методы, так и их асинхронные аналоги.</p>
<p>Например, запишем и считаем обратно в строку текстовый файл:</p>
<pre class="brush:c#;">
string path = @"c:\app\content.txt";

string originalText = "Hello Metanit.com";
// запись строки
await File.WriteAllTextAsync(path, originalText);
// дозапись в конец файла
await File.AppendAllTextAsync(path, "\nHello work");

// чтение файла
string fileText = await File.ReadAllTextAsync(path);
Console.WriteLine(fileText);
</pre>
<p>Консольный вывод:</p>
<div class="console">
<pre class="consoletext">
Hello Metanit.com
Hello work
</pre>
</div>
<p>Стоит отметить, что при добавлении текста я добавил в строку последовательность "\n", которая выполняет перевод на следующую строку. Благодаря этому 
добавляемый текст располагается в файле на новой строке.</p>
<p>Если мы хотим, что в файле изначально шло добавление на новую строку, то для записи стоит использовать метод <code>WriteAllLines/ WriteAllLinesAsync</code>, 
а для добавления -  <code>AppendAllLines / AppendAllLinesAsync</code></p>
<pre class="brush:c#;">await File.WriteAllLinesAsync(path, new[] { "Hello Metanit.com", "Hello work" });</pre>
<p>Аналогично при чтении файла если мы хотим каждую строку файла считать отдельно, то вместо <code>ReadAllText / ReadAllTextAsync</code> применяется 
<code>ReadAllLines / ReadAllTextAsync</code>.</p>
<h3>Кодировка</h3>
<p>В качестве дополнительного параметра методы чтения-записи текстовых файлов позволяют установить кодировку в виде объекта 
<span class="b">System.Text.Encoding</span>:</p>
<pre class="brush:c#;">
using System.Text;

string path = "/Users/eugene/Documents/app/content.txt";

string originalText = "Привет Metanit.com";
// запись строки
await File.WriteAllTextAsync(path, originalText, Encoding.Unicode);
// дозапись в конец файла
await File.AppendAllTextAsync(path, "\nПривет мир", Encoding.Unicode);

// чтение файла
string fileText = await File.ReadAllTextAsync(path, Encoding.Unicode);
Console.WriteLine(fileText);
</pre>
<p>Для установки кодировки при записи и чтении здесь применяется встроенное значение <code>Encoding.Unicode</code>. Также можно указать название кодировки, единственное 
следует удостовериться, что текущая операционная система поддерживает выбранную кодировку:</p>
<pre class="brush:c#;">
using System.Text;

string path = @"c:\app\content.txt";

string originalText = "Hello Metanit.com";
// запись строки
await File.WriteAllTextAsync(path, originalText, Encoding.GetEncoding("iso-8859-1"));
// дозапись в конец файла
await File.AppendAllTextAsync(path, "\nHello code", Encoding.GetEncoding("iso-8859-1"));

// чтение файла
string fileText = await File.ReadAllTextAsync(path, Encoding.GetEncoding("iso-8859-1"));
Console.WriteLine(fileText);
</pre>

