<p>Во-первых, для обработки запросов по ссылкам типа <code>href="Request/Details/@item.Id"</code>, которые определены в представлении Index.cshtml, 
нам надо создать в контроллере соответствующие методы. Вначале создадим функциональность для вывода общей информации по заявке. 
Для этого добавим в контроллер RequestController следующий метод Details:</p>
<pre class="brush:c#;">
// Просмотр подробных сведений о заявке
public ActionResult Details(int id)
{
    Request request = db.Requests.Find(id);

    if (request != null)
    {
        //получаем кабинет
        var activ = db.Activs.Where(m =&gt; m.Id == request.ActivId);
		// так как кабинет у нас может быть не указан, и набор может возвращать 0 значений
        if (activ.Count()&gt;0)
        {
            request.Activ = activ.First();
        }
        //получаем категорию
        request.Category = db.Categories.Where(m =&gt; m.Id == request.CategoryId).First();
		return PartialView("_Details", request);
    }
    return View("Index");
}
</pre>
<p>По параметру из бд получаем объект Request и связанные с ним актив и категорию и возвращаем частичное представление. Теперь для этого 
метода добавим частичное представление <i>_Details.cshtml</i>:</p>
<pre class="brush:xml;">
@model HelpDeskTrain.Models.Request
&lt;div&gt;
	&lt;p&gt;&lt;b&gt;Подробные сведения: Заявка №  @Html.DisplayFor(m =&gt; m.Id)&lt;/b&gt;&lt;/p&gt;
	&lt;p&gt;Описание: @Html.DisplayFor(m=&gt;m.Description)&lt;/p&gt;
	&lt;p&gt;Кабинет: @Html.DisplayFor(m=&gt;m.Activ.CabNumber)&lt;/p&gt;
	&lt;p&gt;Категория: @Html.DisplayFor(m=&gt;m.Category.Name)&lt;/p&gt;
	&lt;p&gt;Комментарий: @Html.DisplayFor(m=&gt;m.Comment)&lt;/p&gt;
&lt;/div&gt;
</pre>
<p>И также нам надо связать воедино функциональность jquery-ui и получаемую с сервера разметку в виде частичного представления. Для этого воспользуемся 
кодом javascript. Добавим в проект в папку скриптов <i>Scripts</i> новый файл javascript, который назовем <i>helpdesk.js</i>, со следующим 
содержимым:</p>
<pre class="brush:js;">
$(document).ready(function () {
             
    $.ajaxSetup({ cache: false });
 
            $(".openDialog").on("click", function (e) {
                e.preventDefault();
 
                $("&lt;div&gt;&lt;/div&gt;")
                    .addClass("dialog")
                    .attr("id", $(this)
                    .attr("data-dialog-id"))
                    .appendTo("body")
                    .dialog({
                        title: $(this).attr("data-dialog-title"),
                        close: function () { $(this).remove() },
                        modal: true
                    })
                    .load(this.href);
            });
 
            $(".close").on("click", function (e) {
                e.preventDefault();
                $(this).closest(".dialog").dialog("close");
      });
});
</pre>
<p>Этот javascript-код по клику на кнопку будет выводить содержимое в всплывающее окно. Поскольку мы будем использовать этот скрипт только там, 
где также будем использовать библиотеку jquery-ui, то логично будет добавить данный скрипт в бандл jquery-ui. Благодаря этому данный скрип сможет 
воспользоваться преимуществами бандлинга и минификации.</p>
<p>Итак, перейдем в папку <b>App_Start</b> и откроем файл <i>BundleConfig.cs</i>. Сейчас в нем объявление бандла jquery-ui должно выглядеть следующим 
образом:</p>
<pre class="brush:c#;">
bundles.Add(new ScriptBundle("~/bundles/jqueryui").Include(
                "~/Scripts/jquery-ui-{version}.js"));
</pre>
<p>Подключим в него вышеопределенный файл helpdesk.js:</p>
<pre class="brush:c#;">
bundles.Add(new ScriptBundle("~/bundles/jqueryui").Include(
                        "~/Scripts/jquery-ui-{version}.js",
                         "~/Scripts/helpdesk.js"));
</pre>
<p>Теперь скрипт подключен и мы можем протестировать данную функциональность. Теперь нажав на синий кружочек, который у нас выполняет роль ссылки на 
общую информацию по заявке, я получу в отдельном окне эту информацию:</p>
<img src="pics/22.png" />
<p>Теперь также добавим в контроллер действия Executor и Lifecycle, при помощи которых в представлении Index будут отображаться такие же всплывающие 
окна, только для информации об исполнителе и жизненном цикле.</p>
<p>Действие Executor:</p>
<pre class="brush:c#;">
public ActionResult Executor(int id)
{
    User executor = db.Users.Where(m =&gt; m.Id == id).First();

    if (executor != null)
    {
        return PartialView("_Executor", executor);
    }
    return View("Index");
}
</pre>
<p>И данные об исполнителе будут передаваться в следующее частичное представление <i>_Executor.cshtml</i>:</p>
<pre class="brush:xml;">
@model HelpDeskTrain.Models.User

&lt;div&gt; 
    &lt;p&gt;&lt;b&gt;Сведения об исполнителе&lt;/b&gt;&lt;/p&gt;
    &lt;p&gt;ФИО:  @Html.DisplayFor(m=&gt;m.Name)&lt;/p&gt;
    &lt;p&gt;Должность:  @Html.DisplayFor(m=&gt;m.Position)&lt;/p&gt;
&lt;/div&gt;
</pre>
<p>И в конце добавим в контроллер действие Lifecycle:</p>
<pre class="brush:c#;">
public ActionResult Lifecycle(int id)
{
    Lifecycle lifecycle = db.Lifecycles.Where(m =&gt; m.Id == id).First();

    if (lifecycle != null)
    {
        return PartialView("_Lifecycle", lifecycle);
    }
    return View("Index");
}
</pre>
<p>И для него добавим частичное представление <i>_Lifecycle.cshtml</i>:</p>
<pre class="brush:xml;">
@model HelpDeskTrain.Models.Lifecycle

&lt;div&gt;
    &lt;p&gt;&lt;b&gt;Заявка № @Html.DisplayFor(m =&gt; m.Id)&lt;/b&gt;&lt;/p&gt;
    &lt;p&gt;Жизненный цикл&lt;/p&gt;
    &lt;p&gt;Открыта:  @Html.DisplayFor(m=&gt;m.Opened)&lt;/p&gt;
    &lt;p&gt;Распределена:  @Html.DisplayFor(m=&gt;m.Distributed)&lt;/p&gt;
    &lt;p&gt;В процессе:  @Html.DisplayFor(m=&gt;m.Proccesing)&lt;/p&gt;
    &lt;p&gt;На проверке:  @Html.DisplayFor(m=&gt;m.Checking)&lt;/p&gt;
    &lt;p&gt;Закрыта:  @Html.DisplayFor(m=&gt;m.Closed)&lt;/p&gt;
&lt;/div&gt;
</pre>
