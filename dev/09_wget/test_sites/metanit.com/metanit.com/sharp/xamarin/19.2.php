<p>В прошлой теме был создан и настроен проект, а также модель Friend и контекст данных для работы с SQLite в Xamarin Forms 
через Entity Framework Core. Теперь определим простейший CRUD-интерфейс.</p>
<p>Вначале изменим в главном проекте класс <span class="b">App</span> в файле App.xaml.cs:</p>
<pre class="brush:c#;">
using System;
using Xamarin.Forms;
using Xamarin.Forms.Xaml;
using System.Linq;

namespace EFCoreApp
{
	public partial class App : Application
	{
        public const string DBFILENAME = "friendsapp.db";
		public App()
		{
			InitializeComponent();

            string dbPath = DependencyService.Get&lt;IPath&gt;().GetDatabasePath(DBFILENAME);
            using (var db = new ApplicationContext(dbPath))
            {
                // Создаем бд, если она отсутствует
                db.Database.EnsureCreated();
                if(db.Friends.Count() == 0)
                {
                    db.Friends.Add(new Friend { Name = "Tom", Email = "tom@gmail.com", Phone = "+1234567" });
                    db.Friends.Add(new Friend { Name = "Alice", Email = "alice@gmail.com", Phone = "+3435957" });
                    db.SaveChanges();
                }
            }
            MainPage = new NavigationPage(new MainPage());
		}

		protected override void OnStart ()
		{ }

		protected override void OnSleep ()
		{ }

		protected override void OnResume ()
		{ }
	}
}
</pre>
<p>В классе App устанавливается название базы данных в виде константы DBFILENAME. В конструкторе класса мы получаем путь к базе данных и 
затем с помощью контекста данных создаем базу данных, если она отсутствует, и наполняем ее некоторыми начальными данными (если они отсутствуют).</p>
<p>Далее добавим в главный проект новую страницу <span class="b">FriendPage.xaml</span>, которая будет отвечать за работу с отдельным объект Friend. 
В коде <span class="b">FriendPage.xaml</span> определим следующий интерфейс:</p>
<pre class="brush:xml;">
&lt;?xml version="1.0" encoding="utf-8" ?&gt;
&lt;ContentPage xmlns="http://xamarin.com/schemas/2014/forms"
             xmlns:x="http://schemas.microsoft.com/winfx/2009/xaml"
             x:Class="EFCoreApp.FriendPage" Title="Информация о друге"&gt;
    &lt;StackLayout&gt;
        &lt;Label Text="Имя" /&gt;
        &lt;Entry Text="{Binding Name}" /&gt;
        &lt;Label Text="Email" /&gt;
        &lt;Entry Text="{Binding Email}" /&gt;
        &lt;Label Text="Телефон" /&gt;
        &lt;Entry Text="{Binding Phone}" /&gt;
        &lt;StackLayout Orientation="Horizontal"&gt;
            &lt;Button Text="Сохранить" Clicked="SaveFriend" /&gt;
            &lt;Button Text="Удалить" Clicked="DeleteFriend" /&gt;
        &lt;/StackLayout&gt;
    &lt;/StackLayout&gt;
&lt;/ContentPage&gt;
</pre>
<p>Здесь определены три поля ввода для трех свойств объекта Friend, а также две кнопки для различных действий. В файле 
<span class="b">FriendPage.xaml.cs</span> определим обработчики для этих кнопок:</p>
<pre class="brush:c#;">
using System;
using Xamarin.Forms;
using Xamarin.Forms.Xaml;

namespace EFCoreApp
{
	[XamlCompilation(XamlCompilationOptions.Compile)]
	public partial class FriendPage : ContentPage
	{
        string dbPath;
        
        public FriendPage()
		{
			InitializeComponent ();
            dbPath = DependencyService.Get&lt;IPath&gt;().GetDatabasePath(App.DBFILENAME);
        }
        private void SaveFriend(object sender, EventArgs e)
        {
            var friend = (Friend)BindingContext;
            if (!String.IsNullOrEmpty(friend.Name))
            {
                using (ApplicationContext db = new ApplicationContext(dbPath))
                {
                    if (friend.Id == 0)
                        db.Friends.Add(friend);
                    else
                    {
                        db.Friends.Update(friend);
                    }
                    db.SaveChanges();
                }
            }
            this.Navigation.PopAsync();
        }
        private void DeleteFriend(object sender, EventArgs e)
        {
            var friend = (Friend)BindingContext;
            using (ApplicationContext db = new ApplicationContext(dbPath))
            {
                db.Friends.Remove(friend);
                db.SaveChanges();
            }
            this.Navigation.PopAsync();
        }
    }
}
</pre>
<p>В конструкторе класса устанавливается полный путь к базе данных. Объект Friend, с которым работает страницы, передается из вне через свойтво BindingContext. 
То есть объект Friend представляет контекст страницы.</p>
<p>Обработчик SaveFriend вызывается по нажатию на кнопку сохранения. В этом случае у нас могут быть две ситуации: добавление нового объекта 
и изменение старого объекта. Чтобы идентифицировать эти ситуации, проверяем значение Id объекта Friend. Если добавление, то вызываем метод 
<code>db.Friends.Add()</code>, если редактирование - вызываем метод <code>db.Friends.Update()</code>.</p>
<p>Обработчик DeleteFriend вызывается при нажатии на кнопку удаления и удаляет объект из бд с помощью метода <code>db.Friends.Remove()</code>.</p>
<p>Также изменим код страницы <span class="b">MainPage.xaml</span>:</p>
<pre class="brush:xml;">
&lt;?xml version="1.0" encoding="utf-8" ?&gt;
&lt;ContentPage xmlns="http://xamarin.com/schemas/2014/forms"
             xmlns:x="http://schemas.microsoft.com/winfx/2009/xaml"
             x:Class="EFCoreApp.MainPage" Title="Список друзей"&gt;
    &lt;StackLayout&gt;
        &lt;Button Text="Добавить" Clicked="CreateFriend" /&gt;
        &lt;ListView x:Name="friendsList" ItemSelected="OnItemSelected"&gt;
            &lt;ListView.ItemTemplate&gt;
                &lt;DataTemplate&gt;
                    &lt;ViewCell&gt;
                        &lt;ViewCell.View&gt;
                            &lt;StackLayout Orientation="Horizontal"&gt;
                                &lt;Label Text="{Binding Name}" FontSize="Medium" /&gt;
                            &lt;/StackLayout&gt;
                        &lt;/ViewCell.View&gt;
                    &lt;/ViewCell&gt;
                &lt;/DataTemplate&gt;
            &lt;/ListView.ItemTemplate&gt;
        &lt;/ListView&gt;
    &lt;/StackLayout&gt;
&lt;/ContentPage&gt;
</pre>
<p>На странице теперь размещается кнопка для добавления нового объекта и список, который выводит все объекты из базы данных.</p>
<p>В файле <span class="b">MainPage.xaml.cs</span> определим обработчики для списка и кнопки:</p>
<pre class="brush:c#;">
using System;
using System.Linq;
using Xamarin.Forms;

namespace EFCoreApp
{
	public partial class MainPage : ContentPage
	{
		public MainPage()
		{
			InitializeComponent();
        }

        protected override void OnAppearing()
        {
            string dbPath = DependencyService.Get&lt;IPath&gt;().GetDatabasePath(App.DBFILENAME);
            using (ApplicationContext db = new ApplicationContext(dbPath))
            {
                friendsList.ItemsSource = db.Friends.ToList();
            }
            base.OnAppearing();
        }

        // обработка нажатия элемента в списке
        private async void OnItemSelected(object sender, SelectedItemChangedEventArgs e)
        {
            Friend selectedFriend = (Friend)e.SelectedItem;
            FriendPage friendPage = new FriendPage();
            friendPage.BindingContext = selectedFriend;
            await Navigation.PushAsync(friendPage);
        }
        // обработка нажатия кнопки добавления
        private async void CreateFriend(object sender, EventArgs e)
        {
            Friend friend = new Friend();
            FriendPage friendPage = new FriendPage();
            friendPage.BindingContext = friend;
            await Navigation.PushAsync(friendPage);
        }
    }
}
</pre>
<p>В методе <code>OnAppearing()</code>, который вызывается при отображении страницы, загружаются все объекты Friend из базы данных и устанавливаются 
в качестве источника данных для списка.</p>
<p>Метод <code>CreateFriend()</code>, вызываемый при нажатии на кнопку добавления, создает новый объект Friend и передает его странце FriendPage через установку свойства BindingContext.</p>
<p>Метод <code>OnItemSelected()</code>, вызываемый при выборе элемента в списке, подобным образом передает выбранный объект Friend странице FriendPage.</p>
<p>В итоге после запуска приложения база данных будет автоматически создана, и мы увидим в списке начальные данные. Соответственно мы можем 
добавить новые элементы или изменить или удалить уже имеющиеся.</p>
