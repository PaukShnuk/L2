<p>В прошлой теме был рассмотрен вывод в ListView простейших данных - массива строк. На самом деле в простейшем варианте ListView получает для 
каждого объекта списка стоковое представление и выводит его в отдельной ячейке. Однако, как правило, данные, которыми манипулируют приложения, 
представляют более сложные по структуре объекты, которые могут содержать множество свойств. И для организации отображения каждого объекта в ListView определяется 
шаблон данных или DataTemplate.</p>
<p>Рассмотрим, как выполнять привязку к списку объектов на примере ListView. Для создания объектов определим класс Phone:</p>
<pre class="brush:c#;">
public class Phone
{
    public string Title { get; set; }
    public string Company { get; set; }
    public int Price { get; set; }
}
</pre>
<p>Теперь определим следующий класс страницы:</p>
<pre class="brush:c#;">
public partial class MainPage : ContentPage
{
    public List&lt;Phone&gt; Phones { get; set; }

    public MainPage()
    {
        Label header = new Label
        {
            Text = "Список моделей",
            FontSize = Device.GetNamedSize(NamedSize.Large, typeof(Label)),
            HorizontalOptions = LayoutOptions.Center
        };

        Phones = new List&lt;Phone&gt;
        {
            new Phone {Title="Galaxy S8", Company="Samsung", Price=48000 },
            new Phone {Title="Huawei P10", Company="Huawei", Price=35000 },
            new Phone {Title="HTC U Ultra", Company="HTC", Price=42000 },
            new Phone {Title="iPhone 7", Company="Apple", Price=52000 }
        };
        ListView listView = new ListView
        {
            HasUnevenRows = true,
            // Определяем источник данных
            ItemsSource = Phones,

            // Определяем формат отображения данных
            ItemTemplate = new DataTemplate(() =&gt;
            {
                // привязка к свойству Name
                Label titleLabel = new Label { FontSize=18 };
                titleLabel.SetBinding(Label.TextProperty, "Title");

                // привязка к свойству Company
                Label companyLabel = new Label();
                companyLabel.SetBinding(Label.TextProperty, "Company");

                // привязка к свойству Price
                Label priceLabel = new Label();
                priceLabel.SetBinding(Label.TextProperty, "Price");

                // создаем объект ViewCell.
                return new ViewCell
                {
                    View = new StackLayout
                    {
                        Padding = new Thickness(0, 5),
                        Orientation = StackOrientation.Vertical,
                        Children = { titleLabel, companyLabel, priceLabel}
                    }
                };
            })
        };
        this.Content = new StackLayout { Children = { header, listView } };
    }
}
</pre>
<p>Для создания списков сложных объектов нам надо использовать три свойства. <code>ItemsSource</code> определяет, какие данные будет содержать список. 
Свойство <code>HasUnevenRows</code> со значением <code>true</code> позволит динамически устанавливать высоту строк в ListView в зависимости от размера содержимого. 
А свойство <code>ItemTemplate</code> определяет, как эти данные будут отображаться. ItemTemplate представляет объект <span class="b">DataTemplate</span>, 
который в качестве параметра принимает делегат. Содержимое DataTemplate представляет одну из ячеек, то есть класс Cell. В данном случае это тип ViewCell, поэтому в качестве итога возвращается объект 
<span class="b">ViewCell</span>, определяющий структуру строки в ListView.</p>
<p>Внутри ViewCell внутренние элементы упавляются контейнером StackLayout, а отдельные элементы Label внутри StackLayout привязаны к отдельным свойствам объекта Phone.</p>
<img src="./pics/3.15.png" alt="DataTemplate и ViewCell в Xamarin Forms" />
<p>Определение DataTemplate в коде XAML:</p>
<pre class="brush:xml;">
&lt;?xml version="1.0" encoding="utf-8" ?&gt;
&lt;ContentPage xmlns="http://xamarin.com/schemas/2014/forms"
             xmlns:x="http://schemas.microsoft.com/winfx/2009/xaml"
             xmlns:local="clr-namespace:HelloApp;assembly=HelloApp"
             x:Class="HelloApp.MainPage"&gt;
    &lt;StackLayout&gt;
        &lt;Label Text="{Binding Source={x:Reference Name=phonesList}, Path=SelectedItem.Title}"
            FontSize="Large" /&gt;
        &lt;ListView x:Name="phonesList"
              HasUnevenRows="True"
              ItemsSource="{Binding Phones}"
              ItemTapped="OnItemTapped"&gt;
            &lt;ListView.ItemTemplate&gt;
                &lt;DataTemplate&gt;
                    &lt;ViewCell&gt;
                        &lt;ViewCell.View&gt;
                            &lt;StackLayout&gt;
                                &lt;Label Text="{Binding Company}" FontSize="18" /&gt;
                                &lt;Label Text="{Binding Title}" /&gt;
                                &lt;Label Text="{Binding Price}" /&gt;
                            &lt;/StackLayout&gt;
                        &lt;/ViewCell.View&gt;
                    &lt;/ViewCell&gt;
                &lt;/DataTemplate&gt;
            &lt;/ListView.ItemTemplate&gt;
        &lt;/ListView&gt;
    &lt;/StackLayout&gt;
&lt;/ContentPage&gt;
</pre>
<p>Здесь практически тоже самое, за исключением некоторых моментов. Так, атрибут <code>ItemsSource="{Binding Phones}"</code> устанавливает привязку к свойству Phones, которое мы длее определим.</p>
<p>И атрибут <code>ItemTapped="OnItemTapped"</code> устанавливает обработчик, который будет срабатывать при нажатии на один из элементов списка.</p>
<p>Для определения визуального шаблона выводимых данных применяется объект DataTemplate, в котором через элемент ViewCell устанавливается формат отображения - 
в виде вертикального стека элементов Label, каждый из которых привязан к одному из свойств объекта Phone:</p>
<pre class="brush:xml;">
&lt;DataTemplate&gt;
   &lt;ViewCell&gt;
      &lt;ViewCell.View&gt;
        &lt;StackLayout&gt;
           &lt;Label Text="{Binding Company}"  FontSize="18" /&gt;
           &lt;Label Text="{Binding Title}" /&gt;
           &lt;Label Text="{Binding Price}" /&gt;
        &lt;/StackLayout&gt;
      &lt;/ViewCell.View&gt;
   &lt;/ViewCell&gt;
&lt;/DataTemplate&gt;
</pre>
<p>Также надо отметить, что сверху определен элемент Label, текст которого привязан к названию выбранной в списке модели:</p>
<pre class="brush:xml;">
&lt;Label Text="{Binding Source={x:Reference Name=phonesList}, Path=SelectedItem.Title}" 
		FontSize="Large" /&gt;
</pre>
<p>Затем в коде на c# определим свойство Phones, к которому будет привязан список ListView, и обработчик нажатия на объект списка:</p>
<pre class="brush:c#;">
using System.Collections.Generic;
using Xamarin.Forms;

namespace HelloApp
{
    public partial class MainPage : ContentPage
    {
        public List&lt;Phone&gt; Phones { get; set; }
        public MainPage()
        {
            InitializeComponent();
            Phones = new List&lt;Phone&gt;
            {
                new Phone {Title="Galaxy S8", Company="Samsung", Price=48000 },
                new Phone {Title="Huawei P10", Company="Huawei", Price=35000 },
                new Phone {Title="HTC U Ultra", Company="HTC", Price=42000 },
                new Phone {Title="iPhone 7", Company="Apple", Price=52000 }
            };
            this.BindingContext = this;
        }

        public async void OnItemTapped(object sender, ItemTappedEventArgs e)
        {
            Phone selectedPhone = e.Item as Phone;
            if (selectedPhone != null)
                await DisplayAlert("Выбранная модель", $"{selectedPhone.Company} - {selectedPhone.Title}", "OK");
        }
    }
}
</pre>
<p>Установка в качестве контекста данных текущего объекта:</p>
<pre class="brush:c#;">this.BindingContext = this;</pre>
<p>Позволит использовать все свойства этого объекта (в данном случае свойство Phones) в коде XAML.</p>
<p>В обработчике события <code>OnItemTapped</code> получаем выбранный объект. И так как каждый объект в DataTemplate представляет тип Phone, то мы можем привести 
значение <code>e.Item</code> к типу Phone.</p>
<p>В итоге, когда мы запустим приложение, то в списке отобразится несколько начальных объектов, а название выбранного объекта появится в верхе страницы.</p>
