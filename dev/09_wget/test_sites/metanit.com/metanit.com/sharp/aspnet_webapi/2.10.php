<p>По умолчанию в целях безопасности браузер ограничивает ajax-запросы между различными доменами. Однако может возникнуть ситуация, 
когда в Web API нам потребуется выполнять подобные запросы. Для этого нам надо использовать при отправке запроса формат JSONP.</p>
<h3>JSONP в Web API</h3>
<p>Чтобы задействовать JSONP в Web API, вначале добавим в проект NuGet-пакет <span class="bb">WebApiContrib.Formatting.Jsonp</span>:</p>
<img src="pics/jsonp_in_webapi.png" alt="JSONP in ASP.NET Web API" />
<p>Затем изменим в проекте Web API файл Global.asax:</p>
<pre class="brush:c#;">
using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.Http;
using System.Web.Mvc;
using System.Web.Optimization;
using System.Web.Routing;
using WebApiContrib.Formatting.Jsonp; // добавляем поддержку jsonp

namespace CORSApp
{
    public class WebApiApplication : System.Web.HttpApplication
    {
        protected void Application_Start()
        {
            GlobalConfiguration.Configuration.AddJsonpFormatter(); // добавляем поддержку jsonp

            AreaRegistration.RegisterAllAreas();
            GlobalConfiguration.Configure(WebApiConfig.Register);
            FilterConfig.RegisterGlobalFilters(GlobalFilters.Filters);
            RouteConfig.RegisterRoutes(RouteTable.Routes);
            BundleConfig.RegisterBundles(BundleTable.Bundles);
        }
    }
}
</pre>
<p>Допустим, приложение Web API запускается по адресу <i>http://localhost:33384</i>. Для теста создадим второе приложение ASP.NET MVC, которое запускается 
по другому адресу. Теперь чтобы обратиться из второго приложения к приложению web api через ajax, пропишем в представлении второго приложения следующий код:</p>
<pre class="brush:xml;">
&lt;div id="result"&gt;

&lt;/div&gt;
&lt;div&gt;&lt;p&gt;&lt;button class="btn" id="request"&gt;Запрос&lt;/button&gt;&lt;/p&gt;&lt;/div&gt;

@section scripts
{
    &lt;script&gt;
        $(function () {

            $("#request").click(function() {

                $.ajax({
                    url: 'http://localhost:33384/api/values/',
                    type: 'GET',
                    dataType: 'jsonp',
                    success: function (data) {

                        $("#result").text(data);
                    },
                    error: function (jqXHR, textStatus, errorThrown) {
                        $('#result').text(jqXHR.responseText || textStatus);
                    }
                });
            });
        })
    &lt;/script&gt;
}
</pre>
<p>И после нажатия на кнопку будет выполняться запрос к стандартному методу Get контроллера ValuesController приложения Web API.</p>
<h3>CORS в Web API</h3>
<p>Несколько иное поведение реализует применение стандарта Cross Origin Resource Sharing (CORS), который также позволяет выполнять кроссдоменные 
запросы, но при этом имеет следующие преимущества: CORS более безопасный, поддерживает не только запросы Get, но и POST, PUT и т.д.</p>
<p>Но чтобы сделать CORS доступным в проекте Web API, надо добавить специальный NuGet-пакет <span class="bb">Microsoft.AspNet.WebApi.Cors</span> 
(пакет добавляется только в проект Web API, к которому идет запрос):</p>
<img src="pics/cors_in_webapi.png" alt="CORS in ASP.NET Web API" />
<p>Затем откроем файл <span class="bb">WebApiConfig.cs</span> в папке App_Start и изменим в нем метод <code>WebApiConfig.Register</code>:</p>
<pre class="brush:c#;">
public static class WebApiConfig
{
    public static void Register(HttpConfiguration config)
    {
        // Добавляем поддержку CORS
        config.EnableCors();
		
        config.MapHttpAttributeRoutes();

        config.Routes.MapHttpRoute(
            name: "DefaultApi",
            routeTemplate: "api/{controller}/{id}",
           defaults: new { id = RouteParameter.Optional }
        );
    }
}
</pre>
<p>Также в файле конфигурации web.config добавим в узел <span class="b">system.webServer</span> новый элемент <span class="b">modules</span>:</p>
<pre class="brush:xml;">
&lt;system.webServer&gt;
&lt;!-- остальное содержимое узла --&gt;
&lt;modules runAllManagedModulesForAllRequests="true"&gt;&lt;/modules&gt;
&lt;/system.webServer&gt;
</pre>
<p>Затем добавим атрибут <span class="b">[EnableCors]</span> к контроллеру Web API:</p>
<pre class="brush:c#;">
using System;
using System.Collections.Generic;
using System.Linq;
using System.Net;
using System.Net.Http;
using System.Web.Http;
using System.Web.Http.Cors; // пространство имен CORS

namespace CORSApp.Controllers
{
    [EnableCors(origins: "http://localhost:33419", headers: "*", methods: "*")]
    public class ValuesController : ApiController
    {
        // GET api/values
        public IEnumerable&lt;string&gt; Get()
        {
            return new string[] { "value1", "value2" };
        }

        // остальные методы
    }
}
</pre>
<p>Атрибут имеет три параметра:</p>
<ul>
<li><p><span class="bb">origins</span>: задает набор url, с которых разрешен доступ к данному ресурсу</p></li>
<li><p><span class="bb">headers</span>: задает набор заголовков, которые допустимы для доступа к ресурсу</p></li>
<li><p><span class="bb">methods</span>: определяет набор допустимых типов запроса</p></li>
</ul>
<p>В использованной версии атрибута предполагается, что доступ к контроллеру values разрешен только с адреса <i>http://localhost:33419</i> 
(в конце концевой слеш не используется!), причем разрешен для всех типов запроса - GET, POST, PUT, и любых заголовков.</p>
<p>Из приложения, которое запущено по адресу http://localhost:33419, мы можем обратиться к приложению web api с помощью стандартного запроса ajax:</p>
<pre class="brush:xml;">
&lt;div id="result"&gt;

&lt;/div&gt;
&lt;div&gt;&lt;p&gt;&lt;button class="btn" id="request" value="Запрос"&gt;Запрос&lt;/button&gt;&lt;/p&gt;&lt;/div&gt;

@section scripts
{
    &lt;script&gt;
        $(function () {

            $("#request").click(function() {

                $.ajax({
                    url: 'http://localhost:33384/api/values/',
                    type: 'GET',
                    //dataType: 'json',
                    success: function (data) {

                        $("#result").text(data);
                    },
                    error: function (jqXHR, textStatus, errorThrown) {
                        $('#result').text(jqXHR.responseText || textStatus);
                    }
                });
            });
        })
    &lt;/script&gt;
}
</pre>
<p>Если мы хотим, чтобы ресурс был доступен для всех url, то для параметра origins можно поставить звездочку:</p>
<pre class="brush:c#;">
[EnableCors(origins: "*", headers: "*", methods: "*")]
</pre>
<p>Также мы можем задать набор ресурсов, методов, заголовков через запятую:</p>
<pre class="brush:c#;">
[EnableCors(origins: "http://localhost:34822,http://www.example.com", headers: "accept,content-type,origin", methods: "get,post")]
</pre>
<p>Как и другие атрибуты, этот атрибут может быть задан на уровне, контроллера, на уровне одного метода или глобально для всего приложения. Чтобы задать атрибут глобально, 
изменим класс WebApiConfig в папке App_Start:</p>
<pre class="brush:c#;">
using System.Web.Http.Cors;
//........
public static class WebApiConfig
{
    public static void Register(HttpConfiguration config)
    {
        var cors = new EnableCorsAttribute("http://www.example.com", "*", "*");
        config.EnableCors(cors);
		
        config.MapHttpAttributeRoutes();

        config.Routes.MapHttpRoute(
            name: "DefaultApi",
            routeTemplate: "api/{controller}/{id}",
            defaults: new { id = RouteParameter.Optional }
        );
    }
}
</pre>
<p>Если мы применяем атрибут глобально или к контроллеру, но при этом хотим исключить из его действия какой-нибудь метод, то для метода можно использовать атрибут 
<span class="bb">[DisableCors]</span>:</p>
<pre class="brush:c#;">
[EnableCors(origins: "http://localhost:33419", headers: "*", methods: "*")]
public class ValuesController : ApiController
{
    [DisableCors]
    public IEnumerable&lt;string&gt; Get()
    {
        return new string[] { "value1", "value2" };
    }
}
</pre>
