<p>Возьмем проект из прошлой темы и добавим в него страницы и логику для манипуляции объектами из пользовательского интерфейса.</p>
<p>Для начала надо определить логику создания таблицы и определения подключения. Для этого перейдем в файл <i>App.xaml.cs</i> и изменим его конструктор 
следующим образом:</p>
<pre class="brush:c#;">
public static BookRepository repo;
public App()
{
    this.InitializeComponent();
    this.Suspending += this.OnSuspending;

    repo = new BookRepository("library.db");
    repo.CreateTable();
}
</pre>
<p>С помощью статической переменной <code>BookRepository repo</code> мы будем обращаться к базе данных из любого места в приложении. В самом конструкторе 
создается бд <code>library.db</code> (если она отсутствует), а также таблица Book.</p>
<p>У нас уже есть стандартная страница <i>MainPage.xaml</i>. Пусть она будет выводить список книг. Определим в ней следующий код xaml:</p>
<pre class="brush:xml;">
&lt;Page
    x:Class="SQLLiteApp.MainPage"
    xmlns="http://schemas.microsoft.com/winfx/2006/xaml/presentation"
    xmlns:x="http://schemas.microsoft.com/winfx/2006/xaml"
    xmlns:local="using:SQLLiteApp"
    xmlns:d="http://schemas.microsoft.com/expression/blend/2008"
    xmlns:mc="http://schemas.openxmlformats.org/markup-compatibility/2006"
    mc:Ignorable="d"
    Background="{ThemeResource ApplicationPageBackgroundThemeBrush}"&gt;
    &lt;Grid&gt;
        &lt;StackPanel&gt;
            &lt;TextBlock Text="Библиотека" Style="{ThemeResource HeaderTextBlockStyle}" /&gt;
            &lt;ListView x:Name="booksList" IsItemClickEnabled="True" 
                      ItemClick="ItemView_ItemClick"&gt;
                &lt;ListView.ItemTemplate&gt;
                    &lt;DataTemplate&gt;
                        &lt;StackPanel&gt;
                            &lt;TextBlock Text="{Binding Name}"  FontSize="26" /&gt;
                        &lt;/StackPanel&gt;
                    &lt;/DataTemplate&gt;
                &lt;/ListView.ItemTemplate&gt;
            &lt;/ListView&gt;
            &lt;Button x:Name="createButton" Content="Добавить" Click="createButton_Click" /&gt;
        &lt;/StackPanel&gt;
    &lt;/Grid&gt;
&lt;/Page&gt;
</pre>
<p>Элементу <code>ListView</code> задается шаблон для отображения связанных данных. По нажатию на каждый объект в списке выполняется обработчик 
<code>ItemView_ItemClick</code>. Также имеется кнопка для создания новых объектов.</p>
<p>А в файле кода <i>MainPage.xaml.cs</i> изменим код метода <code>OnNavigatedTo</code> и добавим обработчики:</p>
<pre class="brush:c#;">
protected override void OnNavigatedTo(NavigationEventArgs e)
{
    booksList.ItemsSource = App.repo.GetBooks();
}
private void ItemView_ItemClick(object sender, ItemClickEventArgs e)
{
    var itemId = ((Book)e.ClickedItem).Id;
    Frame.Navigate(typeof(DetailsPage), itemId);
}
private void createButton_Click(object sender, RoutedEventArgs e)
{
    Frame.Navigate(typeof(AddPage));
} 
</pre>
<p>Метод <code>OnNavigatedTo()</code>, срабатывающий при переходе на страницу, будет загружать в список данные. Обработчик <code>ItemView_ItemClick()</code> 
будет по нажатию на элемент списка передавать id объекта на страницу DetailsPage, которую сейчас создадим. А метод <code>createButton_Click()</code> будет переходить 
на страницу создания объекта AddPage.</p>
<p>Теперь добавим в проект новую страницу по типу Blank Page и назовем ее AddPage.xaml. Определим в ней следующую разметку:</p>
<pre class="brush:xml;">
&lt;Page
    x:Class="SQLLiteApp.AddPage"
    xmlns="http://schemas.microsoft.com/winfx/2006/xaml/presentation"
    xmlns:x="http://schemas.microsoft.com/winfx/2006/xaml"
    xmlns:local="using:SQLLiteApp"
    xmlns:d="http://schemas.microsoft.com/expression/blend/2008"
    xmlns:mc="http://schemas.openxmlformats.org/markup-compatibility/2006"
    mc:Ignorable="d"
    Background="{ThemeResource ApplicationPageBackgroundThemeBrush}"&gt;

    &lt;Grid&gt;
        &lt;StackPanel&gt;
            &lt;TextBlock Text="Новый элемент" Style="{ThemeResource HeaderTextBlockStyle}" /&gt;
            &lt;TextBox x:Name="nameBox" PlaceholderText="Название" /&gt;
            &lt;TextBox x:Name="authorBox" PlaceholderText="Автор" /&gt;
            &lt;TextBox x:Name="yearBox" PlaceholderText="Год" /&gt;
            &lt;Button x:Name="addButton" Content="Добавить" Click="addButton_Click" /&gt;
        &lt;/StackPanel&gt;
    &lt;/Grid&gt;
&lt;/Page&gt;
</pre>
<p>Здесь определены поля для заполнения. По клику на кнопку срабатывает обработчик <code>addButton_Click</code>. Добавим для него код в файл 
<i>AddPage.xaml.cs</i>:</p>
<pre class="brush:c#;">
private void addButton_Click(object sender, RoutedEventArgs e)
{
    long year = -1;
    if(Int64.TryParse(yearBox.Text,out year) && !String.IsNullOrWhiteSpace(nameBox.Text) 
        && !String.IsNullOrWhiteSpace(authorBox.Text))
    {
        Book book = new Book { Name = nameBox.Text, Author = authorBox.Text, Year = year };
        App.repo.Insert(book);
        if (Frame.CanGoBack)
            Frame.GoBack();
    }
}
</pre>
<p>Вначале проверяем поля, потом создаем объект Book и добавляем его в базу данных. В конце возвращаемся на предыдущую страницу.</p>
<p>И также добавим в проект новую станицу DetailsPage.xaml, которая будет выводить данные об объекте:</p>
<pre class="brush:xml;">
&lt;Page
    x:Class="SQLLiteApp.DetailsPage"
    xmlns="http://schemas.microsoft.com/winfx/2006/xaml/presentation"
    xmlns:x="http://schemas.microsoft.com/winfx/2006/xaml"
    xmlns:local="using:SQLLiteApp"
    xmlns:d="http://schemas.microsoft.com/expression/blend/2008"
    xmlns:mc="http://schemas.openxmlformats.org/markup-compatibility/2006"
    mc:Ignorable="d"
    Background="{ThemeResource ApplicationPageBackgroundThemeBrush}"&gt;

    &lt;Grid x:Name="detailsGrid"&gt;
        &lt;StackPanel&gt;
            &lt;TextBlock Text="О книге" Style="{ThemeResource HeaderTextBlockStyle}" /&gt;
            &lt;TextBlock Text="Название" FontSize="21" Foreground="Silver" /&gt;
            &lt;TextBlock x:Name="nameBlock" FontSize="25" /&gt;
            &lt;TextBlock Text="Автор" FontSize="21" Foreground="Silver"/&gt;
            &lt;TextBlock x:Name="authorBlock" FontSize="25" /&gt;
            &lt;TextBlock Text="Год" FontSize="21" Foreground="Silver"/&gt;
            &lt;TextBlock x:Name="yearBlock" FontSize="25" /&gt;
            &lt;StackPanel Orientation="Horizontal"&gt;
                &lt;Button Content="Назад" Click="Button_Click" /&gt;
                &lt;Button x:Name="deleteButton" Content="Удалить" Click="Button_Click" IsEnabled="False" /&gt;
            &lt;/StackPanel&gt;
            
        &lt;/StackPanel&gt;
    &lt;/Grid&gt;
&lt;/Page&gt;
</pre>
<p>Здесь определены поля для отображения свойств объекта, а также две кнопки - для удаления и для возвращения назад. И в файл кода добавим обработчик для этих кнопок 
и изменим метод <code>OnNavigatedTo()</code>:</p>
<pre class="brush:c#;">
public long Id { get; set; }

protected override void OnNavigatedTo(NavigationEventArgs e)
{
    if (e.Parameter != null)
    {
        long id = (long)e.Parameter;
        Book b = App.repo.GetBook(id);
        if(b!=null)
        {
            nameBlock.Text = b.Name;
            authorBlock.Text = b.Author;
            yearBlock.Text = b.Year.ToString();
            deleteButton.IsEnabled = true;
            Id = id;
        }
    }    
}
private void Button_Click(object sender, RoutedEventArgs e)
{
    Button button = (Button)sender;
    if(button.Name=="deleteButton")
        App.repo.Delete(Id);
    if (Frame.CanGoBack)
        Frame.GoBack();
}
</pre>
<p>При переходе с главной страницы на DetailsPage в нее будет передаваться id выбранного объекта. Это id будет храниться в свойстве <code>public long Id</code>. 
По id получаем объект из бд и выводим все значения его свойств на страницу.</p>
<p>В обработчике кнопок, если была нажата кнопка удаления, то удаляем из бд, и затем переходим на предыдущую страницу.</p>
<p>При запуске приложения на главной странице будут отображаться все объекты из базы данных:</p>
