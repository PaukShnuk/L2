<p>Рано или поздно большинство созданных на платформе ASP.NET проектов начинают свою жизнь в большом интернете и возникает вопрос хостинга. 
Где лучше разместить проект? Какой хостинг выбрать?</p>
<p>При выборе хостинга надо учитывать множество параметров. Во-первых, это, прежде всего, аппаратные возможности хостеров - какой сервер, скорость 
передачи данных, ОЗУ сервера, тип процессора, сколько процессорного времени хостер выделяет по выбранному тарифному плану. 
Тут все просто: чем больше (мощность сервера, скорость) - тем лучше. Правда, как правило, и дороже.</p>
<p>Второй момент - это программные возможности. Тут следует учитывать следующие аспекты: версия ОС Windows, MSSQL Server, поддержка нужных версий платформы 
ASP.NET (MVC). Если ваш проект использует базу данных MSSQL Server 2012, то естественно надо искать соответствующий хостинг.</p>
<p>Также если вы используете последние версии платформы ASP.NET, какие-то новейшие компоненты, то стоит предварительно узнать, а поддерживает ли 
хостинг подобные компоненты. Например, к настоящему моменты не все хостеры имеют поддержку ASP.NET MVC 5. И иногда возникает ситуация, 
когда человек в спешке выбирает вроде бы приличный и недорогой хостинг, оплачивает, а в итоге оказывается, что он что-то не поддерживает.</p>
<p>Третий, наверное, менее важный момент - это пинг или скорость передачи. В принципе это больше аппаратный компонент, зависящий от пропускной 
способности сети или аппаратных возможностей того же сервера. Однако иногда следует также учитывать и географическое положение сервера 
и потенциальных посетителей сайта. Например, насколько мне известно ряд российских компаний перенесли свои сервисы из Amazonа в Windows Azure, 
потому что серверы последнего находятся ближе к потенциальной аудитории. Хотя аппаратная поддержка передачи данных все время развивается и улучшается, да и в большинстве 
проектов вряд ли пинг будет играть большой роли, но если у вас высоконагруженный проект (или, например, сервер находится в России, а аудитория в 
Латинской Америке), то, возможно, пинг является тем местом, где можно уменьшить издержки.</p>
<p>Теперь рассмотрим некоторые из наиболее популярных компаний и сервисов, предлагающих услуги в области хостинга под asp.net проекты.</p>
