<p>Обычно при обращении к веб-приложению пользователь ожидает получить веб-страницу с какими-нибудь данными. В MVC для этого, как 
правило, используются <span class="b">представления</span>, которые определяют внешний вид приложения и на основе которых потом формируется веб-страница. В ASP.NET MVC Core представления - это файлы с расширением cshtml, которые 
содержат код пользовательского интерфейса в основном на языке html, а также конструкции Razor - специального движка представлений, который позволяет переходить 
от кода html к коду на языке C#.</p>
<p>Для хранения представлений в проекте ASP.NET Core предназначена папка <span class="b">Views</span>. Например, если мы возьмем проект по типу <span class="b">ASP.NET Core Web App (Model-View-Controller)</span>, 
то мы увидим, что он содержит ряд представлений:</p>
<img src="./pics/3.1.png" alt="Представления в проекте ASP.NET Core MVC на C#" />
<p>Подобный проект для хранения представлений в папке <span class="b">Views</span> определяет некоторую структуру:</p>
<ul><li><p>Во-первых, как правило, для каждого контроллера в проекте создается подкаталог в папке Views, который называется по имени 
контроллера и который хранит представления, используемые методами данного контроллера. Так, по умолчанию имеется контроллер HomeController и для него в папке 
Views есть подкаталог <span class="b">Home</span> с представлениями для методов контроллера HomeController - в данном случае это файлы <span class="b">Index.cshtml</span> и 
<span class="b">Privacy.cshtml</span>.</p>
</li>
<li><p>Также здесь есть папка <span class="b">Shared</span>, которая хранит общие представления для всех контроллеров. 
По умолчанию это файлы <span class="b">_Layout.cshtml</span> (используется в качестве мастер-страницы), <span class="b">Error.cshtml</span> 
(использутся для отображения ошибок) и <span class="b">_ValidationScripsPartial.cshtml</span> 
(частичное представление, которое подключает скрипты валидации формы).</p>
</li>
<li><p>И в корне каталога <span class="b">Views</span> также можно найти два файла <span class="b">_ViewImports.cshtml</span> и <span class="b">_ViewStart.cshtml</span>. Эти файлы 
содержат код, который автоматически добавляется ко всем представлениям. <span class="ii">_ViewImports.cshtml</span> устанавливает некоторые общие для всех представлений пространства имен, 
а <span class="ii">_ViewStart.cshtml</span> устанавливает общую мастер-страницу.</p></li>
</ul>
<p>Мы можем продолжить работу с этим проектом, но в данном случае, чтобы упростить рассмотрение представлений и их функционала для работы возьмем простейший проект 
ASP.NET Core по типу <span class="b">ASP.NET Core Empty</span>.</p>
<img src="./pics/1.6.png" alt="Подключение MVC в пустой проект ASP.NET Core" />
<p>Пусть этот проект называется <span class="b">MvcApp</span>. По умолчанию проект по данному шаблону не содержит никаких контроллеров и представлений. Поэтому сначала определим контроллер, который будет использовать представления.</p>
<h3>Определение контроллера и вызов представления. ViewResult</h3>
<p>Для начала определим в проекте папку <span class="b">Controllers</span>, в которую добавим новый контроллер - <span class="b">HomeController</span> со следующим кодом:</p>
<pre class="brush:c#;">
using Microsoft.AspNetCore.Mvc;

namespace MvcApp.Controllers
{
    public class HomeController : Controller
    {
        public IActionResult Index()
        {
            return View();
        }
    }
}
</pre>
<p>За работу с представлениями отвечает объект <span class="b">ViewResult</span>. Он производит рендеринг представления в веб-страницу и 
возвращает ее в виде ответа клиенту. Чтобы возвратить объект ViewResult, в методе контроллера вызывается метод <span class="b">View</span>:</p>
<p>Вызов метода View возвращает объект ViewResult. Затем уже ViewResult производит рендеринг определенного представления в ответ. По умолчанию контроллер 
производит поиск представления в проекте по следующим путям:</p>
<pre>
/Views/Имя_контроллера/Имя_представления.cshtml
/Views/Shared/Имя_представления.cshtml
</pre>
<p>Согласно настройкам по умолчанию, если название представления не указано явным образом, то в качестве представления будет использоваться то, имя которого 
совпадает с именем действия контроллера. Например, вышеопределенное действие Index по умолчанию будет производить поиск представления <span class="b">Index.cshtml</span> в папке 
<span class="b">/Views/Home/</span>.</p>
<p>Однако пока у нас нет подобного представления, поэтому далее создадим его.</p>
<h3>Создание представлений</h3>
<p>Сначала создадим в проекте новую папку <span class="b">Views</span>, а в ней определим новый каталог <span class="b">Home</span> - для представлений контроллера HomeController.</p>
<p>Далее добавим в каталог <span class="b">Views/Home</span> новый элемент по типу <span class="b">Razor View - Empty</span>, который назовем <span class="b">index.cshtml</span>:</p>
<img src="./pics/3.2.png" alt="Добавление нового представления Razor View в проект ASP.NET Core MVC на C#" />
<p>То есть в итоге проект будет выглядеть следующим образом:</p>
<img src="./pics/3.3.png" alt="Добавление представлений Razor View в проект ASP.NET Core MVC на C#" />
<p>Откроем представление <span class="b">Index.cshtml</span>. По умолчанию оно имеет код наподобие следующего:</p>
<pre class="brush:c#;">
@*
    For more information on enabling MVC for empty projects, visit https://go.microsoft.com/fwlink/?LinkID=397860
*@
@{
}
</pre>
<p>Изменим этот код следующим образом:</p>
<pre class="brush:c#;">
&lt;!DOCTYPE html&gt;
&lt;html&gt;
&lt;head&gt;
    &lt;title&gt;METANIT.COM&lt;/title&gt;
    &lt;meta charset="utf-8" /&gt;
&lt;/head&gt;
&lt;body&gt;
    &lt;h2&gt;Привет METANIT.COM!&lt;/h2&gt;
&lt;/body&gt;
&lt;/html&gt;
</pre>
<p>Данное представление напоминает обычную страницу html. Здесь могут быть определены все стандартные элементы разметки html, здесь могут подключаться стили, скрипты. 
Но полноценной html-страницей представление все равно не является, потому что во время выполнения эти представления компилируются в сборки и уже затем используются для генерации 
html-страниц, которые видит пользователь в своем браузере.</p>
<h3>Подключение функционала представлений</h3>
<p>Но просто определить в проекте файлы представлений недостаточно. Чтобы приложение могло использовать представления, нам надо подключить соответствующие сервисы. 
Для этого изменим код файла <span class="b">Program.cs</span> следующим образом:</p>
<pre class="brush:c#;">
var builder = WebApplication.CreateBuilder(args);

// добавляем поддержку контроллеров с представлениями
builder.Services.AddControllersWithViews();
var app = builder.Build();

// устанавливаем сопоставление маршрутов с контроллерами
app.MapControllerRoute(
    name: "default",
    pattern: "{controller=Home}/{action=Index}/{id?}");

app.Run();
</pre>
<p>Для подключения в приложение функционала контроллеров с представлениями применяется вызов <span class="b">AddControllersWithViews()</span> (также можно применять методы 
<code>AddMvcCore()</code> и <code>AddMvc()</code>)</p>
<p>И после запуска приложения и обращении к методу Index контроллера Home браузер отобразит нам веб-страницу, которая будет сгенерирована на основе представления <span class="b">Index.cshtml</span>.</p>
<img src="./pics/3.4.png" alt="Рендеринг представлений Razor View в приложении ASP.NET Core MVC на C#" />
<h3>ViewResult и пути к представлениям</h3>
<p>Чтобы возвратить объект ViewResult, который производит рендеринг представления в веб-страницу и возвращает ее в виде ответа клиенту, используется метод 
<span class="b">View()</span>. Например, в пример выше применялась версия этого метода, которая не применяла параметров:</p>
<pre class="brush:c#;">
public class HomeController : Controller
{
    public IActionResult Index()
    {
        return View();
    }
}
</pre>
<p>Но вообще метод <code>View()</code> имеет четыре перегруженных версии:</p>
<ul>
<li><p><code>View()</code>: для генерации ответа используется представление, которое по имени совпадает с вызывающим методом</p></li>
<li><p><code>View(string? viewName)</code>: в метод передается имя представления, что позволяет переопределить используемое по умолчанию представление</p></li>
<li><p><code>View(object? model)</code>: передает в представление данные в виде объекта model</p></li>
<li><p><code>View(string? viewName, object? model)</code>: переопределяет имя представления и передает в него данные в виде объекта model</p></li>
</ul>
<p>Вторая версия метода позволяет переопределить используемое представление. Если представление находится в той же папке, которая предназначена для данного контроллера, 
то в метод View() достаточно передать название представления без расширения:</p>
<pre class="brush:c#;">
public class HomeController : Controller
{
    public IActionResult Index()
    {
        return View("About");
    }
}
</pre>
<p>В этом случае метод Index будет использовать представление по пути <span class="ii">Views/Home/About.cshtml</span>. Если же представление находится в другой папке, 
то нам надо передать полный путь к представлению:</p>
<pre class="brush:c#;">
public class HomeController : Controller
{
    public IActionResult Index()
    {
        return View("~/Views/Some/Index.cshtml");
    }
}
</pre>
<p>В данном случае предполагается, что представление располагается по пути <span class="ii">Views/Some/About.cshtml</span>
