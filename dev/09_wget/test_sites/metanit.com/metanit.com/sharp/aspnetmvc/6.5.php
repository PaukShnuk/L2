<p>Наиболее простой способ создания ссылки в коде html представляет использование анкора - элемента <code>a</code>:</p>
<pre class="brush:xml;">&lt;a href="~/Home/Index/3" /&gt;</pre>
<p>Но также для генерации ссылок в ASP.NET Core MVC, например, при выводе их в представлении, можно применять специальные методы или хелперы рендеринга - <span class="b">Html.ActionLink</span> и <span class="b">Html.RouteLink</span>.</p>
<h3>Html.ActionLink</h3>
<p>Хелпер <code>ActionLink</code> создает гиперссылку на действие контроллера. Если мы создаем ссылку на действие, определенное в том же 
контроллере, то можем просто указать имя действия:</p>
<pre class="brush:c#;">@Html.ActionLink("О сайте", "About")</pre>
<p>Что создаст вам следующую разметку (если текущий контроллер - Home):</p>
<pre class="brush:xml;">&lt;a href="/Home/About"&gt;О сайте&lt;/a&gt;</pre>
<p>Если необходимо указать ссылку на действие из другого контроллера, то в хелпере <code>ActionLink</code> в качестве третьего аргумента 
имя контроллера. Например, ссылка на действие <code>List</code> контроллера <code>Book</code> будет создаваться так:</p>
<pre class="brush:c#;">@Html.ActionLink("Список книг", "List", "Book")</pre>
<p>Кроме того, если у нас в некотором методе Index контроллера HomeController определено несколько параметров:</p> 
<pre class="brush:c#;">
public class HomeController : Controller
{
    public string Details(string name = "Тоm", int id = 1)
    {
        return $"{id}. {name}";
    }
	// остальные методы
}
</pre>
<p>То перегруженная версия хелпера <code>ActionLink</code> позволяет передать параметр объекта (обычно анонимный тип) для параметра <code>routeValues</code>. 
Среда выполнения принимает свойства объекта и использует их для создания значений маршрутизации (имена свойств становятся именами параметров маршрута, а значения свойств представляют значения параметра маршрута). 
Создадим ссылку для вышеопределенного действия контроллера:</p>
<pre class="brush:c#;">
@Html.ActionLink("Пользователь 10", "Details", "Home", new { id = 10 }, null)

@Html.ActionLink("Alice", "Details", "Home", new { name = "Alice", id = 5 }, null)
</pre>
<p>Последний параметр в данном хелпере является параметром <code>htmlAttributes</code>. Мы можем использовать этот параметр для установки 
значения атрибута элемента HTML. В данном случае передается значение null (то есть никаких атрибутов не устанавливается).</p>
<p>Теперь попробуем передать атрибуты, например, установить атрибуты <code>id</code> и <code>class</code>:</p>
<pre class="brush:c#;">
@Html.ActionLink("Sam", "Details", "Home", new { name = "Sam", id = 12 }, new { id = "sam", @class = "link" })
</pre>
<p>Сгенерированная html-разметка будет выглядеть следующим образом:</p>
<pre class="brush:xml;">
&lt;a class="link" href="/Home/Details/12?name=Sam" id="sam"&gt;Sam&lt;/a&gt;
</pre>
<p>Обратите внимание на знак <code>@</code> перед словом class: поскольку слово "class" является зарезервированным словом в C#, то 
для правильного рендеринга нам надо перед ним указать знак @.</p>
<h3>Html.RouteLink</h3>
<p>Хелпер <code>RouteLink</code> использует похожий шаблон, что и <code>ActionLink</code>: он принимает имя маршрута, но не требует 
аргументов для имени контроллера и имени действия. Так, последний пример с <code>ActionLink</code> эквивалентен следующему коду:</p>
<pre class="brush:c#;">
@Html.RouteLink("Sam", new { controller = "Home", action = "Details", name = "Sam", id = 12 }, new { id = "sam", @class = "link" })
</pre>
<p>Чтобы использовать маршрут, нам просто надо указать имя определенного нами маршрута и затем определить при необходимости дополнительные параметры. 
Например, возьмем стандартный маршрут default, который определен в файле <span class="b">Program.cs</span>:</p>
<pre class="brush:c#;">
app.MapControllerRoute(
    name: "default",
    pattern: "{controller=Home}/{action=Index}/{id?}");
</pre>
<p>Тогда создать ссылку мы можем, например, так:</p>
<pre class="brush:c#;">@Html.RouteLink("Все пользователи","Default",new { action = "UsersList" })</pre>
<p>В данном случае используется шаблон марщрута с именем default - то есть трехсегментный шаблон, где последний сегмент не обязателен. Мы указали действие 
UsersList, но не указали контроллер, поэтому будет использоваться контроллер по умолчанию (если он указан в определении маршрута) - HomeController. 
В итоге сгенерированная ссылка будет выглядеть так:</p>
<pre class="brush:xml;">&lt;a href="/Home/UsersList"&gt;Все пользователи&lt;/a&gt;</pre>
<p>Собственно имена маршрутов и используются в основном для создания ссылок.</p>
<h3>Система маршрутизации и генерация ссылок</h3>
<p>При генерации ссылок фрейворк MVC опирается на систему маршрутизации. Допустим, для приложения определено несколько маршрутов:</p>
<pre class="brush:c#;">
app.MapControllerRoute(
        name: "products",
        pattern: "Products/{action}/{id?}",
        defaults: new { controller = "Home" });

app.MapControllerRoute(
    name: "default",
    pattern: "{controller=Home}/{action=Index}/{id?}");
</pre>
<p>Первым здесь установлен маршрут "products", который будет сопоставлять запросы к контроллеру Home с маршрутом "Products/{action}/{id?}". В частности, 
запрос <span class="ii">http://localhost:55994/Products/Index</span> будет обрабатываться методом Index контроллера Home.</p>
<p>И теперь выражение</p>
<pre class="brush:c#;">
@Html.ActionLink("Жми здесь", "Index", "Home")
</pre>
<p>Сгенерирует следующую ссылку</p>
<pre class="brush:xml;">&lt;a href="/Products/Index"&gt;Жми здесь&lt;/a&gt;</pre>
<p>И не смотря на то, что у нас также определен стандартный маршрут, который позволяет генерировать стандартные ссылки, включающие имя контроллера и имя его метода, 
но в данном случае для генерации ссылок будет применяться первый маршрут, который маршрут который совпадает с определением ссылки.</p>
<p>Так, пусть у нас определены следующие маршруты:</p>
<pre class="brush:c#;">
app.MapControllerRoute(
		name: "default",
		pattern: "BookStore",
		defaults: new { controller = "Book", action = "Index" });

app.MapControllerRoute(
	name: "default1",
	pattern: "Store/Sub{action}/{id?}",
	defaults: new { controller = "Home" });

app.MapControllerRoute(
    name: "default2",
    pattern: "{controller=Home}/{action=Index}/{id?}");
</pre>
<p>Из определения маршрутов ясно, то запрос <span class="ii">http://localhost:55994/BookStore</span> будет обрабатываться методом Index контроллера Book. 
А запрос <span class="ii">http://localhost:55994/Store/SubIndex/</span> будет обрабатываться контроллером Home и его методом Index.</p>
<p>Теперь пусть у нас в представлении создаются две ссылки:</p>
<pre class="brush:c#;">
@Html.ActionLink("Home", "Index", "Home")
@Html.ActionLink("Book", "Index", "Book")
</pre>
<p>Первая ссылка соответствует второму маршруту, так как для определения ссылки используется контроллер Home. Поэтому в соответствии со вторым маршрутом будет сгенерирована 
следующая ссылка:</p>
<pre class="brush:xml;">http://localhost:55994/Store/SubIndex</pre>
<p>Вторая же ссылка соответствует первому маршруту, который для обработки запроса применяет контроллер BookController. Поэтому второе выражение сгенерирует следующую ссылку:</p>
<pre class="brush:xml;">http://localhost:55994/BookStore</pre>
<p>Поэтому если в приложении определено несколько маршрутов, а в представлениях для генерации ссылок применяются хелперы типа Html.ActionLink, то следует проверять 
сгенерированные ссылки, иначе в результате можно получить совсем не то, что ожидалось.</p>
<p>Данные правила в отношении создания ссылок справедливы не только в отношении Html.ActionLink, но и в отношении хелпера Html.RouteLink.</p>
<p>Например, вызов</p>
<pre class="brush:c#;">@Html.RouteLink("Все книги", new { action = "Index", controller="Home" })</pre>
<p>сгенерирует следующую ссылку:</p>
<pre class="brush:xml;">&lt;a href="/Store/SubIndex"&gt;Все книги&lt;/a&gt;</pre>
<p>Все, что было выше сказано в отношении маршрутов, применяется также в отношении атрибутов маршрутизации. Так, допустим, в контроллере HomeController используется 
атрибут Route:</p>
<pre class="brush:c#;">
public class HomeController : Controller
{
    [Route("api/{controller}/{action}")]
    public IActionResult Index()
    {
        return View();
    }
	
	// остальные методы
}
</pre>
<p>В этом случае вызов</p>
<pre class="brush:c#;">
@Html.ActionLink("Home", "Index", "Home")
</pre>
<p>сгенерирует следующую ссылку:</p>
<pre class="brush:xml;">http://localhost:55994/api/Home/Index</pre>

