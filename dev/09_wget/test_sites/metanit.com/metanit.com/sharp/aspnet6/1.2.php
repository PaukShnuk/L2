<p>Для создания приложений на C# будем использовать бесплатную и полнофункциональную среду разработки - Visual Studio Community 2022, которую можно загрузить по следующему адресу: 
<a href="https://www.visualstudio.com/en-us/downloads" rel="nofollow">Microsoft Visual Studio 2022</a></p>
<img src="./pics/1.7.png" alt="Установка Visual Studio 2022" />
<p>Стоит отметить, что Visual Studio 2019 и все предыдущие версии Visual Studio не поддерживают .NET 6 и C# 10, необходима именно версия Visual Studio 2022.</p>
<p>После загрузки запустим программу установщика. В открывшемся окне нам будет предложено выбрать те компоненты, которые мы хотим установить вместе Visual Studio. 
Стоит отметить, что Visual Studio - очень функциональная среда разработки и позволяет разрабатывать приложения с помощью множества языков и платформ. 
В нашем случае нам будет интересовать прежде всего C# и .NET.</p>
<p>Чтобы добавить в Visual Studio поддержку проектов для C# и .NET 6, в программе установки среди рабочих нагрузок можно выбрать только пункт 
<span class="b">ASP.NET и разработка веб-приложений</span>. Можно выбрать и больше опций или вообще 
все опции, однако стоит учитывать свободный размер на жестком диске - чем больше опций будет выбрано, соответственно тем больше места на диске будет занято.</p>
<img src="./pics/1.8.png" alt="Установка Visual Studio 2022" />
<p>И при инсталляции Visual Studio на ваш компьютер будут установлены все необходимые инструменты для разработки программ, в том 
числе фреймворк .NET 6.</p>
<p>После завершения установки создадим первый проект на ASP.NET 6. Вначале откроем Visual Studio. На стартовом экране выберем 
<span class="b">Create a new project</span> (Создать новый проект)</p>
<img src="./pics/1.9.png" alt="Создание первого проекта в Visual Studio 2022" />
<p>На следующем окне в качестве типа проекта выберем <span class="b">ASP.NET Core Empty</span></p>
<img src="./pics/1.10.png" alt="Проект консольного приложения на C# и .NET 6 в Visual Studio 2022" />
<p>Далее на следующем этапе нам будет предложено указать имя проекта и каталог, где будет располагаться проект.</p>
<img src="./pics/1.1.png" alt="Создание первого приложения на C#" />
<p>В поле <span class="b">Project Name</span> дадим проекту какое-либо название. В моем случае это <span class="b">HelloApp</span>.</p>
<p>На следующем окне Visual Studio предложит нам выбрать версию .NET, которая будет использоваться для проекта. По умолчанию здесь выбрана последняя на данный момент версия - .NET 6.0.</p>
<img src="./pics/1.13.png" alt="Установка C# 10 и .NET 6 в Visual Studio" />
<p>Кроме того, здесь с помощью флажка <span class="b">Configure for HTTPS</span> можно установить использование протокола https. По умолчанию этот флажок отмечен, 
это значит, что проект по умолчанию будет использовать протокол https.</p>
<p>Другой флажок - <span class="b">Enable Docker</span> позволяет задействвовать Docker. Если этот флажок установлен, то в поле ниже можно будет выбрать ОС, которая будет 
использоваться под Docker. Но в данном случае оставим этот флажок неотмеченным.</p>
<p>Оставим все остальные настройки по умолчанию и нажмем на кнопку Create (Создать) для создания проекта. После этого Visual Studio создаст и откроет нам проект:</p>
<img src="./pics/1.2.png" alt="Первый проект на C#" />
<h3>Структура проекта ASP.NET Core</h3>
<p>Рассмотрим базовую структуру стандартного проекта ASP.NET Core. Проект <span class="b">ASP.NET Core Empty</span> содержит очень простую структуру - необходимый минимум для запуска приложения:</p>
<ul>
<li><p><span class="b">Connected Services</span>: подключенные сервисы из Azure</p></li>
<li><p><span class="b">Dependencies</span>: все добавленные в проект пакеты и библиотеки, иначе говоря зависимости</p></li>
<li><p><span class="b">Properties</span>: узел, который содержит некоторые настройки проекта. В частности, в файле <span class="b">launchSettings.json</span> описаны 
настройки запуска проекта, например, адреса, по которым будет запускаться приложение.</p></li>
<li><p><span class="b">appsettings.json</span>: файл конфигурации проекта в формате json</p>
<li><p><span class="b">Program.cs</span>: главный файл приложения, с которого и начинается его выполнение. Код этого файла настраивает и запускает веб-приложение</p></li>
</ul>
<p>Это базовая минимальная структура проекта веб-приложения на ASP.NET. При создании других типов проектов ASP.NET структура будет отличаться, соответственно начальный проект будет иметь больше функционала, однако это тот каркас, от которого мы можем отталкиваться, 
добавляя в него какие-то свои файлы и папки.</p>
<h3>Запуск проекта</h3>
<p>Проект по умолчанию не представляет какой-то грандиозной функциональности, тем не менее этот проект мы уже можем запустить. Итак, запустим проект. 
При запуске нам может отобразиться окно, где надо подтвердить доверие для серфиката SSL, а также его установку</p>
<img src="./pics/1.3.png" alt="Потверждение сертификата SSL при запуске приложения ASP.NET Core NET 6" />
<img src="./pics/1.5.png" alt="Установка сертификата SSL при запуске приложения ASP.NET Core NET 6" />
<p>И после подтверждения и установки сертификата отобразиться консоль, где выводися некоторая базовая информация о приложении:</p>

<p>И, кроме того, будет запущен браузер, где мы сможем лицезреть строку "Hello World!" - результат работы кода по умолчанию из файла <span class="b">Program.cs</span>:</p>
<img src="./pics/1.4.png" alt="Первое приложение на ASP.NET Core NET 6" />

<h3>Запуск приложения и файл Program.cs</h3>
<p>Рассмотрим код файла <span class="b">Program.cs</span>, который создает подобное приложение:</p>
<pre class="brush:c#;">
var builder = WebApplication.CreateBuilder(args);
var app = builder.Build();

app.MapGet("/", () =&gt; "Hello World!");

app.Run();

</pre>
<p>Это так называемое <span class="b">Minimal API</span> - упрощенная минизированная модель для запуска веб-приложения в ASP.NET.</p>
<p>Приложение в ASP.NET Core .NET 6 представляет объект <span class="b">Microsoft.AspNetCore.Builder.WebApplication</span>. Этот объект настраивает всю 
конфигурацию приложения, его маршруты, используемые зависимости и т.д.. Исходный код класса на github: <a href="https://github.com/dotnet/aspnetcore/blob/main/src/DefaultBuilder/src/WebApplication.cs" rel="nofollow">https://github.com/dotnet/aspnetcore/blob/main/src/DefaultBuilder/src/WebApplication.cs</a></p>
<p>Для создания объекта <span class="b">WebApplication</span> необходим специальный класс-строитель - <span class="b">WebApplicationBuilder</span>. И в файле Program.cs вначале создается данный объект 
с помощью статического метода <code>WebApplication.CreateBuilder</code>:</p>
<pre class="brush:c#;">var builder = WebApplication.CreateBuilder(args);</pre>
<p>В качестве параметра в метод передаются аргументы, которые передаются приложению при запуске.</p>
<p>Получив объект <span class="b">WebApplicationBuilder</span>, у него вызывается метод <span class="b">Build()</span>, который собствено и 
создает объект <span class="b">WebApplication</span>:</p>
<pre class="brush:c#;">var app = builder.Build();</pre>
<p>С помощью объекта <span class="b">WebApplication</span> можно настроить всю инфраструктуру приложения - его конфигурацию, маршруты и так далее. В 
файле Program.cs по умолчанию для приложения определяется один маршрут:</p>
<pre class="brush:c#;">app.MapGet("/", () =&gt; "Hello World!");</pre>
<p>Метод <span class="b">MapGet()</span> в качестве первого параметра принимает путь, по которому можно обратиться к приложению. В данном случае это путь "/", то есть 
по сути корень веб-приложения - имя домена и порта, после которых может идти слеш, например, <code>https://localhost:7256/</code></p>
<p>В качестве второго параметра в метод <span class="b">MapGet()</span> передаются обработчик запроса по этому маршруту в виде функции. Здесь это лямбда-выражение, 
которое возвращает строку "Hello World!". Именно поэтому при обращении к приложению мы увидим данную строку в браузере.</p>
<p>И в конце необходимо запустить приложение. Для этого у класса <span class="b">WebApplication</span> вызывается метод <span class="b">Run()</span>:</p>
<pre class="brush:c#;">app.Run();</pre>
<p>В итоге запустится приложение в виде консоли, и мы сможем обращаться к приложению из различных браузеров.</p>
