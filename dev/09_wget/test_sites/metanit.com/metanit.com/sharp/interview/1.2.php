<h3>Вопрос 6</h3>
<p>Какие результаты выведет следующий код:</p>
<pre class="brush:c#;">
internal class Program
{
    private static Object syncObject = new Object();
    private static void Write()
    {
        lock (syncObject)
        {
            Console.WriteLine("test");
        }
    }
    static void Main(string[] args)
    {
        lock (syncObject)
        {
            Write();
        }
    }
}
</pre>
<p>Варианты ответов:</p>
<ol>
<li><p>Выбросит исключение</p></li>
<li><p>Напечатает слово "test"</p></li>
<li><p>Произойдет взаимоблокировка (deadlock)</p></li>
<li><p>Напечатает слово "test" бесконечное число раз</p></li>
</ol>
