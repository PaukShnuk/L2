<div class="question">
<h3>Вопрос 8</h3>
<p>Что будет выведено на консоль в результате выполнения следующего кода:</p>
<pre class="brush:c#;">
static IEnumerable&lt;int&gt; Square(IEnumerable&lt;int&gt; a)
{
    foreach(var r in a)
    {
        Console.WriteLine(r * r);
        yield return r * r;
    }
}
class Wrap
{
    private static int init = 0;
    public int Value
    {
        get { return ++init; }
    }
}
static void Main(string[] args)
{
    var w = new Wrap();
    var wraps = new Wrap[3];
    for(int i=0; i&lt;wraps.Length; i++)
    {
        wraps[i] = w;
    }

    var values = wraps.Select(x =&gt; x.Value);
    var results = Square(values);
    int sum = 0;
    int count = 0;
    foreach(var r in results)
    {
        count++;
        sum += r;
    }
    Console.WriteLine("Count {0}", count);
    Console.WriteLine("Sum {0}", sum);

    Console.WriteLine("Count {0}", results.Count());
    Console.WriteLine("Sum {0}", results.Sum());
}
</pre>
