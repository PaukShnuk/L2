<p>Итак, создадим первое мобильное приложение на ASP.NET MVC 4. Для этого выберем специально предназначенный для мобильных приложений шаблон 
<span class="bb">Mobile Application</span>:</p>
<img src="mvcpics/14.3.png" alt="Шаблон проекта asp.net mvc Mobile Application" />
<p>По сути данный тип проекта напоминает проект по типу Internet Application, но только в данном случае он предназначен непосредственно для мобильных устройств.</p>
<p>Одним из ключевых отличий данного проекта будет использование библиотеки jQuery.mobile. Так, в проекте в каталоге Scripts можно 
найти полную и минимизированную версию библиотеки: <i>jquery.mobile-1.1.0.js</i> и <i>jquery.mobile-1.1.0.min.js</i></p>
<p>А в каталоге Content будут находится связанные с jQuery.mobile стили: <i>jquery.mobile-1.1.0.css</i> и др.</p>
<img src="mvcpics/14.4.png" />
<p>Собственно наличие библиоетки jQuery.mobile и представляет главное отличие от других типов проектов.</p>
<p>Если мы откроем в проекте мастер-страницу <i>_Layout.cshtml</i>, то мы найдем код, который использует атрибуты jQuery.mobile:</p>
<pre class="brush:xml;">
&lt;!DOCTYPE html&gt;
&lt;html lang="en"&gt;
    &lt;head&gt;
        &lt;meta charset="utf-8" /&gt;
        &lt;title&gt;@ViewBag.Title&lt;/title&gt;
        &lt;meta name="viewport" content="width=device-width" /&gt;
        &lt;link href="~/favicon.ico" rel="shortcut icon" type="image/x-icon" /&gt;
        @Styles.Render("~/Content/mobileCss", "~/Content/css")
        @Scripts.Render("~/bundles/modernizr")
    &lt;/head&gt;
    &lt;body&gt;
        &lt;div data-role="page" data-theme="b"&gt;
            &lt;div data-role="header"&gt;
                @if (IsSectionDefined("Header")) {
                    @RenderSection("Header")
                } else {
                    &lt;h1&gt;@ViewBag.Title&lt;/h1&gt;
                    @Html.Partial("_LoginPartial")
                }
            &lt;/div&gt;
            &lt;div data-role="content"&gt;
                @RenderBody()
            &lt;/div&gt;
        &lt;/div&gt;

        @Scripts.Render("~/bundles/jquery", "~/bundles/jquerymobile")
        @RenderSection("scripts", required: false)
    &lt;/body&gt;
&lt;/html&gt;
</pre>
<p>Чтобы дать понять браузеру, что он имеет дело с сайтом для мобильных устройств, в секции head, идет объявление метатега vieport: <code>&lt;meta name="viewport" content="width=device-width" /&gt;</code>.</p>
<p>Далее у нас идут ряд элементов непосредственно с атрибутами jQuery.mobile. Так, тег <code>&lt;div data-role="page" data-theme="b"&gt;</code> определяет роль элемента div на экране - в данном случае 
элемент играет роль страницы(data-role="page"), а также определяет тему оформления элемента (data-theme="b").</p>
<p>Страница (data-role="page") содержит два элемента div с ролями header (data-role="header") и content (data-role="content"). 
От роли элемента зависит, как он будет отображаться на экране.</p>
<p>И ниже в секции скриптов идет само подключение библиотеки jQuery.mobile вместе с основной библиотекой jQuery: <code>@Scripts.Render("~/bundles/jquery", "~/bundles/jquerymobile")</code></p>
<p>Представление <i>Index.cshtml</i>, которое будет отображаться при запуске, по сути представляет обычное представление, только опять же использующее 
арибуты jQuery.mobile:</p>
<pre class="brush:xml;">
@{
    ViewBag.Title = "Home Page";
}

&lt;h2&gt;@ViewBag.Message&lt;/h2&gt;
&lt;p&gt;
    To learn more about ASP.NET MVC visit &lt;a href="https://asp.net/mvc" title="ASP.NET MVC Website"&gt;http://asp.net/mvc&lt;/a&gt;.
&lt;/p&gt;

&lt;ul data-role="listview" data-inset="true"&gt;
    &lt;li data-role="list-divider"&gt;Navigation&lt;/li&gt;
    &lt;li&gt;@Html.ActionLink("About", "About", "Home")&lt;/li&gt;
    &lt;li&gt;@Html.ActionLink("Contact", "Contact", "Home")&lt;/li&gt;
&lt;/ul&gt;
</pre>
<p>Использованный здесь атрибут <code>data-role="listview"</code> указывет, что элемент ul будет отображаться в виде списка. Возможно, 
несовсем ясно, почему мы к списку ul должны применять дополнительный атрибут списка? В данном случае атрибут <code>data-role="listview"</code> лишь 
просто помогает управлять визуализацией элемента в виде списка на мобильных устройствах.</p>
<p>Нам необязательно использовать библиотеку jquery.mobile, просто она упрощает построение интерфейса для мобильных приложений.</p>
<p>Для более подробного ознакомления с работой библиотеки jQuery.mobile и используемыми ею атрибутами можно обратиться к специализированным источником, а пока в данном случае 
важно понять суть работы мастер-страницы. То есть, это обычная мастер страницы, только использующая библиотеку jQuery.mobile - ее стили и скрипты, ее 
атрибуты для управления элементами на странице.</p>
<p>Запустим проект на выполнение и потом обратимся к нему в эмуляторе Opera Mobile:</p>
<img src="mvcpics/14.5.png" />
<p>Теперь, отталкиваясь от этого шаблона и используя библиотеку jQuery.mobile, мы можем создавать мобильные сайты на ASP.NET MVC.</p>
