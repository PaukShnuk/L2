<p>В Dart есть специальная константа <span class="b">null</span>, которая говорит, что у переменной или константы по сути 
отсутствует значение. Однако при использовании этой константы следует учитывать, что, начиная с версии 2.12 в Dart по умолчанию применяется null-безопасность или 
null-safety. Что это значит?</p>
<p>Возьмем, к примеру, следующую программу:</p>
<pre class="brush:dart;">
void main() {
	String name;
	print(name);
	name = "Tom";
	print(name);
}
</pre>
<p>Здесь переменной name при ее определении не присваивается никакого значения. И, начиная с версии Dart 2.12, при компиляции программы мы получим ошибку:</p>
<img src="./pics/2.1.png" alt="Non-nullable типы в Dart и null-safety" />
<p>Ошибка говорит нам о том, что мы пытаемся использовать переменную, которая не может принимать значение null, до присвоения ей значения.</p>
<p>По умолчанию все типы данных расцениваются как типы, которые не допускают значение <span class="b">null</span>, поэтому мы обязательно должны присваивать 
их переменным и константам значение перед использованием.</p>
<pre class="brush:dart;">
void main() {
	String name = "Bob";
	print(name);
	name = "Tom";
	print(name);
}
</pre>
<h3>Оператор ? и nullable-типы</h3>
<p>Однако в некоторых ситуациях может потребоваться указать, что переменная или константа не имеет никакого значения. То есть необходимо, чтобы она могла принимать значение <span class="b">null</span>. 
В этом случае мы можем использовать <span class="b">nullable-типы</span> Они являются по сути двойниками обычных типов за тем исключением, что их переменные 
могут принимать значение <code>null</code>.</span>.</p>
<p>Чтобы отметить тип как <code>nullable</code> (допускающий значение null), после названия типа ставится оператор <span class="b">?</span> (вопросительный знак). 
Например:</p>
<pre class="brush:dart;">
void main() {
	String? name;
	print(name);
	name = "Tom";
	print(name);
}
</pre>
<p>Тип <code>String?</code> указывает, что его переменная может представлять либо строку, либо значение null. И при выполнении этой программы мы получим 
следующий конольный вывод:</p>
<div class="console"><pre class="consoletext">
null
Tom
</pre></div>
<p>Переменным таких типов также можно явным образом присваивать null:</p>
<pre class="brush:dart;">
void main() {
	String? name = "Tom";
	print(name);
	name = null;
	print(name);
}
</pre>
<p>Конольный вывод:</p>
<div class="console"><pre class="consoletext">
Tom
null
</pre></div>
<h3>Оператор ??</h3>
<p>Оператор <span class="b">??</span> при присвоении переменной значений выполнит проверку на null. Зачем он нужен? Рассмотрим следующий пример:</p>
<pre class="brush:dart;">
void main() {
	int? num1 = 23;
	int num2 = num1;
	print(num2);
	
	num1 = null;
	num2 = num1;
	print(num2);
}
</pre>
<p>Здесь определены две переменных: num1 и num2. Обе они могут хранить целые числа. Однако кроме того, переменная num1 может также хранить значение <code>null</code>. 
И переменной num2, которая НЕ может принимать null, передается значение num1, которая может хранить null. Соответственно мы можем столкнуться с потенциальной 
ошибкой, в случае если <code>num1 = null</code>.</p>
<p>Для решения подобной проблемы можно использовать оператор <span class="b">??</span>:</p>
<pre class="brush:dart;">value1 ?? value2</pre>
<p>Если значение <code>value1</code> (значение слева от оператора ??) не равно <code>null</code>, то оператор возвращает именно это значение value1. 
Если же это значение равно <code>null</code>, то оператор ?? возвращает значение value2 (справа от оператора).</p>
<pre class="brush:dart;">
void main() {
	int? num1 = 23;
	int num2 = num1 ?? 0;
	print(num2);		// 23
	
	num1 = null;
	num2 = num1 ?? 0;
	print(num2);		// 0
}
</pre>
<p>При первом присвоении переменная num2 получит значение переменной num1 (число 23), так как num1 не равна null. 
При втором присвоении num2 получит значение 0, так как num1 равна null.</p>
<p>Конольный вывод:</p>
<div class="console"><pre class="consoletext">
23
0
</pre></div>
<h3>Оператор !</h3>
<p>В некоторых ситуациях переменная может быь определена как переменная nullable-типа, тем не менее при этом могут быть исключены ситуации, что данная переменная будет 
хранить <code>null</code>. И если мы точно уверены, что эта переменная в процессе работы программы не получит значение <code>null</code>, 
то в этом случае мы можем принимать оператор <span class="b">!</span>, который ставится после названия переменной:</p>
<pre class="brush:dart;">
void main() {
	int? a = 23;
	int b = a!;	// мы уверены, что a не равна null
	print(b);		// 23
}
</pre>
<p>В то же время, если такой уверенности нет, то лучше не злоупотреблть данным оператором и не использовать его.</p>

