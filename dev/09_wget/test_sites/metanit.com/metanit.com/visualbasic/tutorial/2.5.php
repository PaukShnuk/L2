<p>В VB.NET имеются следующие условные структуры:</p>
<ul>
<li><p>If ... Then</p></li>
<li><p>Select Case</p></li>
</ul>
<h3>Конструкция If...Then</h3>
<p>Конструкция If ... Then проверяет истинность некоторого условия и зависимости от результатов проверки выполняет определенный код:</p>
 <pre class="brush : vb;">
Dim num1 As Integer = 10
Dim num2 As Integer = 9
If (num1 &gt; num2) Then
    Console.WriteLine("Число {0} больше числа {1}", num1, num2)
End If
</pre>
<p>Здесь проверяем, больше ли число num1 числа num2, и если num1 больше чем num2. то выводим сообщение. Но, допустим, мы захотим, чтобы в случае 
невыполнения этого условия тоже совершалось какое-нибудь действие. Тогда мы добавляем выражение Else и после него определяем те действия, которые будут совершаться, 
если программа не удовлетворяет условию в выражении If. Например, мы будем выводить сообщение, что первое число меньше второго:</p>
<pre class="brush : vb;">
Dim num1 As Integer = 10
Dim num2 As Integer = 9
If (num1 &gt; num2) Then
    Console.WriteLine("Число {0} больше числа {1}", num1, num2)
Else
	Console.WriteLine("Число {0} меньше числа {1}", num1, num2)
End If
</pre>

<p>Но как вы уже заметили, мы сделали немного неправильно. Так как первое число может быть не только больше или меньше второго: оба числа могут быть равны друг другу. 
В этом случае мы можем определить альтернативные условия с помощью ключевого слова <b>ElseIf</b>:</p>
<pre class="brush : vb;">
Dim num1 As Integer = 10
Dim num2 As Integer = 9
If (num1 &gt; num2) Then
    Console.WriteLine("Число num1 больше числа num2")
ElseIf num1 &lt; num2 Then
    Console.WriteLine("Число num1 меньше числа num2")
Else
    Console.WriteLine("Число num1 равно числу num2")
End If
</pre>
<p>или так:</p>
<pre class="brush : vb;">
Dim R As New Random()
Dim num1 As Integer = R.Next(100)
If num1 = 1 Then
    Console.WriteLine("Число num1 равно 1")
ElseIf num1 = 2 Then
    Console.WriteLine("Число num1 равно 2")
ElseIf num1 = 3 Then
    Console.WriteLine("Число num1 равно 3")
Else
    Console.WriteLine("Число num1 больше 3")
End If
</pre>
<p>В данном примере мы использовали генератор случайных чисел, который представлен классом <b>Random</b>. 
Строка <code>Dim R As New Random()</code> выполняет инициализацию генератора случайных чисел. А в следующей строке <code>Dim num1 As Integer = R.Next(100)</code> 
мы присваиваем числу num1 случайное значение, которое выдаст нам генератор, причем в качестве максимально возможного значения выбрано число 100.</p>
<h4>Конструкция Select Case</h4>
<p>Конструкция Select Case подобна в конструкции If...Then, так как позволяет обрабатывать сразу несколько условий. После слов <b>Select Case</b> 
указывается сравниваемое выражение. Значение этого выражения последовательно сравнивается со значениями, помещенными после оператора <b>Case</b>. 
И в случае если значения совпали, то выполняется блок команд, помещенных после данного оператора <b>Case</b>. Конструкция завершается словами <b>End Select</b>. 
Если мы хотим определить действия, которые будут выполняться, если совпадений не выявлено, то мы можем использовать оператор <b>Case Else</b>, 
после которого помещаем блок действий по умолчанию. Блок <b>Case Else</b> необязателен и может не употребляться Так, мы можем переписать предыдущий пример с помощью <b>Select Case</b>:</p>
<pre class="brush : vb;">
Dim R As New Random()
Dim num1 As Integer = R.Next(100)
Select Case num1
	Case 1
		Console.WriteLine("Число num1 равно 1")
    Case 2
        Console.WriteLine("Число num1 равно 2")
    Case 3 To 25
        Console.WriteLine("Число num1 находится на отрезке от 3 до 25")
    Case Else
        Console.WriteLine("Число num1 больше 25")
End Select
</pre>
<p>Здесь мы также генерируем случайное число и последовательно сравниваем его значения. Обратите внимание на строку <code>Case 3 To 25</code> - 
здесь мы задаем целый диапазон значений от 3 до 25. Также обратите внимание, что если будет найдено совпадение с одним из выражений <b>Case</b>, 
то остальные выражения <b>Case</b> рассматриваться не будут. То есть следующий код отработает только для выражения <code>Case 1</code>, после чего 
программа осуществит выход из Select Case, несмотря на то, что новое значение числа совпадает с значением в следующем выражении Case:</p>
<pre class="brush : vb;">
Dim num2 As Integer = 1
Select Case num2
	Case 1
		Console.WriteLine("Число num2 равно 1")
		num2 += 1
    Case 2
        Console.WriteLine("Число num2 равно 2")
		num2 += 1
    Case 3 To 25
        Console.WriteLine("Число num2 равно 3")
End Select
</pre>
<H4>Конструкция GoTo</H4>
<p>Ну и в конце говоря об условных конструкций нельзя не упомянуть и оператор <b>GoTo</b>. Этот оператор требует после себя указание метки, на которую 
осуществляется переход. Например:</p>
<pre class="brush : vb;">
		Dim time As String = "day"
		If time = "day" Then
			GoTo M1
		Else
			GoTo M2
        End If
M1:     Console.WriteLine("Hello World")
M2:     Console.WriteLine("Good Bye World")
</pre>
<p>Как показывает практика, начинающие часто злоупотребляют использованием этого оператора, что приводит к написанию некрасивого и непонятного кода. 
Поэтому рекомендуется использовать данный оператор как можно реже и по возможности обходиться без него.</p>
