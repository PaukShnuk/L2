<p>Visual Basic.NET поддерживает большинство стандартных операций, принятых и в других языках программирования. Рассмотрим все виды операций.</p>
<h3>Арифметические операции</h3>
<ul>
<li><p><code>+</code></p><p>сложение двух чисел</p></li>
<li><p><code>-</code></p><p>вычитание двух чисел</p></li>
<li><p><code>*</code></p><p>умножение</p></li>
<li><p><code>^</code></p><p>возведение в степень</p></li>
<li><p><code>/</code></p><p>обычное деление</p></li>
<li><p><code>\</code></p><p>целочисленное деление двух чисел</p></li>
<li><p><code>Mod</code></p><p>Получение остатка от деления двух чисел</p></li>
</ul>
<p>Примеры:</p>
<pre class="brush : vb;">
Dim x1 As Integer = 6 + 7 'Результат равен 13 
Dim x2 As Integer = 6 - 7 'Результат равен -1
Dim x3 As Integer = 6 * 7 'Результат равен 42
Dim x4 As Integer = 12 / 6 'Результат равен 2
Dim x5 As Integer = 13 \ 6 'Результат равен 2, а остаток отбрасывается
Dim x6 As Integer = 13 Mod 6 'Результат (он же остаток от деления 13 на 6) равен 1
Dim x7 As Integer = 6 ^ 2 'Результат равен 36
</pre>
<h3>Операции сравнения и логические операции</h3>
<p>В Visual Basic.NET также имеются операторы сравнения:</p>
<ul>
<li><p>&gt;</p><p>Больше</p></li>
<li><p>&gt;=</p><p>Больше или равно</p></li>
<li><p>&lt;</p><p>Меньше</p></li>
<li><p>&lt;=</p><p>Меньше или равно</p></li>
<li><p>=</p><p>Равно (в данном случае используется как знак сравнения на равенство двух значений)</p></li>
<li><p>&lt;&gt;</p><p>Не равно</p></li>
</ul>
<p>Также в языке определены логические операторы, которые возвращают в отличие от арифметических операций значение типа Boolean:</p>
<ul>
<li><p>And</p><p>Логическое умножение (логическое И)</p></li>
<li><p>Or</p><p>Логическое сложение (логическое ИЛИ)</p></li>
<li><p>Xor</p><p>Исключающее "или"</p></li>
<li><p>Not</p><p>Логическое отрицание</p></li>
<li><p>AndAlso</p><p>Сокращенный оператор And</p></li>
<li><p>OrElse</p><p>Сокращенный оператор Or</p></li>
</ul>
<p>Оператор And возвращает результат True только в том случае, если все выражения истинны. В остальных случаях он возвращает False. 
Оператор Or возвращает True, если хотя бы одно из выражений истинно. Он возвращает False тогда, когда неверны все выражения. 
Оператор Xor возвращает True, если одно из выражений истинно, а другое ложно. В остальных случаях, если оба выражения либо истинны, либо ложны, возвращается False. 
Оператор Not возвращает True, если выражение ложно, и False, если выражение истинно. Примеры использования:</p>
 <pre class="brush : vb;">
Dim x As Boolean = 6 &gt; 2 And 2 &lt; 4 'Результат True, так как и первое выражение и второе выражение истинны
Dim y As Boolean = 6 &gt; 2 And 2 &gt; 4 'Результат False, так как только одно выражение истинно
	
Dim x1 As Boolean = 6 &gt; 2 Or 2 &lt; 4 'Результат True, так как хотя бы одно выражение истинно
Dim y1 As Boolean = 6 &gt; 2 Or 2 &gt; 4 'Результат True, так как опять же одно выражение истинно
Dim y2 As Boolean = 6 &lt; 2 Or 2 &gt; 4 'Результат False, так как оба выражения ложны
	
Dim x2 As Boolean = 6 &gt; 2 Xor 2 &lt; 4 'Результат False, так как оба выражения истинны
Dim y2 As Boolean = 6 &gt; 2 Xor 2 &gt; 4 'Результат True, так как одно выражение истинно, а другое ложно
	
Dim x3 As Boolean = Not 2 &lt; 4 'Результат False, так как выражение истинно
Dim y3 As Boolean = Not 2 &gt; 4 'Результат True, так как выражение ложно
</pre>
<p>Операторы AndAlso и OrElse хотя возвращают тот же самый результат, что и And и Or, но все же между ними есть отличия. Так допустим у нас есть следующие 
два выражения, возвращающие идентичный результат :</p>
<pre class="brush : vb;">
Dim x As Boolean = 6 &gt; 2 And 2 &lt; 4 
Dim x As Boolean = 6 &gt; 2 AndAlso 2 &lt; 4
</pre>
<p>В первом случае (в случае с <code>And</code>) программа сначала проверяет истинность первого выражения - <code>6 &gt; 2</code> и не зависимости от результата проверки затем 
проверяет истинность второго выражения - <code>2 &lt; 4</code>. Что у нас происходит в случае с <code>AndAlso</code>? Сначала также проверяется истинность первого 
выражения, и если оно истинно, тогда уже проверяется истинность второго. В случае если первое выражение ложно, тогда нету смысла проверять истинность 
второго выражения, так как в любом случае итоговый результат будет False.</p>
<p>Все сказанное в отношении пары <span class="b">And/AndAlso</span> характерно и для пары <span class="b">Or/OrElse</span>: если оператор Or проверяет истинность правого и левого выражения, то 
оператор OrElse проверяет сначала истинность первого выражения, и только если оно False, проверяет на истинность второе выражение.</p>
<p>Поэтому в целях ускорения работы программы рекомендуется использовать <span class="b">AndAlso</span> и <span class="b">OrElse</span>. Однако иногда сокращенные логические операторы 
не могу употребляться: в поразрядных операциях. В таких операциях необязательно возвращается значение типа Boolean, оно может представлять и другой тип данных. 
Например:</p>
<pre class="brush : vb;">
Dim i As Integer
i = 4 And 5
</pre>
<p>Здесь числа 4 и 5 рассматриваются как двоичные:</p>
<p>число 4 в двоичной форме имеет представление 100,</p>
<p>а число 5 в двоичной форме имеет представление 101</p>
<p>Здесь действует та же логика, что и в логических операциях, только теперь сравниваются разряды чисел. В итоге это выражение возвращает нам число 4, 
потому что результат операции в двоичной форме будет иметь форму 100.</p>
<p>С помощью поразрядной операции Xor удобно применять примитивное шифрование:</p>
<pre class="brush : vb;">
Dim x As Byte = 102 'Пусть это будет ключ - в двоичной форме 1100110
Dim y As Byte = 45	'Значение, которое надо зашифровать - в двоичной форме 101101
Dim z As Byte = y Xor x
Console.WriteLine(z) 'Результатом будет число 1001011 или 75
'Обратная операция - расшифровка
y = z Xor x
Console.WriteLine(y) 'Результатом будет исходное число 45
</pre>
<h3>Операции сдвига</h3>
<p>Еще один класс операций представляют операции поразрядного сдвига &lt;&lt; и &gt;&gt;. Они имеют следующий синтаксис:</p>
<p><code>результат = число &lt;&lt; (или &gt;&gt;) число разрядов</code></p>
<p>Сдвинем число 64 на два разряда влево и мы получим 256</p>
<pre class="brush : vb;">
Console.WriteLine(64 &lt;&lt; 2)
</pre>
<p>Теперь сдвинем число 64 вправо также на два разряда и мы получим 16:</p>
<pre class="brush : vb;">
Dim num1  = 64 &gt;&gt; 2
Console.WriteLine(num1)
</pre>
<p>Несмотря на то, что с первого взгляда кажется, что данные операторы редко используются. нельзя не отметить их практическую ценность. Поскольку каждый сдвиг 
на один разряд вправо приравнивается к делению на 2, а сдвиг влево - к умножению на 2, то операции сдвига можно использовать вместо деления/умножения на степени двойки, 
тем более с точки зрения архитектуры компьютера операции сдвига будут отрабатывать быстрее, чем операции умножения и деления на 2.</p>
<h3>Операции присваивания</h3>
<p>Ну и в конце мы рассмотрим операции присваивания.</p>
<ul>
<li><p>=</p><p>Присваивание</p></li>
<li><p>^=</p><p>Присваивание после возведения в степень</p></li>
<li><p>*=</p><p>Присваивание после умножения</p></li>
<li><p>/=</p><p>Присваивание после деления</p></li>
<li><p>\=</p><p>Присваивание после целочисленного деления</p></li>
<li><p>+=</p><p>Присваивание после сложения</p></li>
<li><p>-=</p><p>Присваивание после вычитания</p></li>
<li><p>&gt;&gt;=</p><p>Присваивание после сдвига вправо</p></li>
<li><p>&lt;&lt;=</p><p>Присваивание после сдвига влево</p></li>
<li><p>&=</p><p>Присваивание после конкатенации двух значений</p></li>
</ul>
<p>Большинство операций присваивания представляют сокращенную форму других операций. Так, выражение </p>
<pre class="brush : vb;">
x1=x1 + 7	
</pre>
<p>эквивалентно следующему:</p>
<pre class="brush : vb;">
x1+=7	
</pre>
<p>Подобным образом работают и другие операторы присваивания. Отдельно скажем про конкатенацию. Оператор &= предназначен для сцепления двух строк:</p>
<pre class="brush : vb;">
Dim s As String = "hello"
s&=" world" 'В итоге переменная s будет иметь значение "Hello world"
</pre>
<p>Если же эту операцию применить к числам, то они сначала будут преобразованы в строки, а затем эти строки будут подобным образом объединены:</p>
<pre class="brush : vb;">
Dim s As Integer = 6
s&=7 'В итоге переменная s будет иметь значение 67
</pre>
