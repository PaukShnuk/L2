<p>Строки играют большую роль, задачи на парсинг строк довольно часто встречаются в PHP , поэтому рассмотрим некоторые базовые функции работы со строками.</p>
<p>Строки представляют некоторый текст, который заключен в одинарные или двойные кавычки:</p>
<pre class="brush:php;">
$str1 = "Hello";
$str2 = 'Welcome';
</pre>
<p>Но также PHP позволяет определить строку и без кавычек с помощью оператора <span class="b">&lt;&lt;&lt;</span>, за которым следует метка завершения строки:</p>
<pre class="brush:php;">
$s = &lt;&lt;&lt; LABEL
Hello World!
Bye World..
LABEL;
echo $s;
</pre>
<p>В данном случае "LABEL" - это название метки, которая указывает на начало и конец строки. Название метки произвольное. То есть все, что расположено 
между <code>&lt;&lt;&lt; LABEL</code> и <code>LABEL;</code>, будет представлять сроку <code>$s</code>. В частности, браузер выведет нам следующее:</p>
<pre class="browser">
Hello World! Bye World..
</pre>
<p>И в данном случае, как и в обычной строке мы можем использовать переменные:</p>
<pre class="brush:php;">
$name = "Tom";
$age = 36;
$s = &lt;&lt;&lt; USER
Name = $name
Age = $age
USER;
echo $s;
</pre>
<h3>Обращение к символам строки</h3>
<p>PHP позволяет обращаться к отдельным символам строки как к элементам массива по числовому индексу:</p>
<pre class="brush:php;">
$str = "Hello Tom";

echo $str[0];// получим первый символ - H

$str[1] = "i";	// поменяем второй символ
echo "&lt;br&gt;" . $str . "&lt;br&gt;";	// Hillo Tom
</pre>
<p>Индексация символов, как и в массиве, начинается с нуля.</p>
<h3>Специальные функции и расширение mbstring</h3>
<p>Кроме некоторых базовых возможностей по работе со строками PHP также предоставляет ряд специальных функций. Для некоторых из них требуется подключить 
специальное расширение - <span class="b">mbstring.dll</span>. Для подключения этого расширения откроем файл конфигурации PHP  -
<span class="b">php.ini</span> и найдем в нем следующую строку:</p>
<pre class="brush:php;">;extension=mbstring</pre>
<p>Раскомментируем ее, убрав точку с запятой.</p>
<pre class="brush:php;">extension=mbstring</pre>
<p>Также убедимся, что у нас указан путь к папке расширений.</p>
<p>Найдем в файле php.ini строку:</p>
<pre class="brush:php;">;extension_dir = "ext"</pre>
<p>Эта строка указывает на каталог с подключаемыми расширениями для PHP. По умолчанию все расширения располагаются в папке <span class="b">ext</span>. 
Раскомментируем эту строку, убрав точку с запятой, и укажем полный путь к папке <span class="b">ext</span>. Так, в моем случае папка c php расположена в корне 
диска C (ОС Windows), соответственно я указываю путь "C:\php\ext":</p>
<pre class="brush:php;">extension_dir = "C:\php\ext"</pre>
<p>И так как у нас файл php.ini изменился, перезапустим веб-сервер Apache.</p>
<p>Подключение расширения <span class="b">mbstring</span> позволит задействовать некоторые дополнительные функции для работы со строками, которые 
в стандартном наборе PHP отсутствуют (такие функции обычно начинаются с префикса <code>mb_</code>).</p>
<h3>Функции strpos() и mb_strpos()</h3>
<p>Функция <code>strpos($str, $search)</code> возвращает позицию подстроки или символа $search в строке $str или значение <code>false</code>, если 
строка $str не содержит подстроки $search:</p>
<pre class="brush:php;">
$input = "This is the end"; 
$search = "is";
$position = strpos($input, $search); // 2
if($position!==false)
{
	echo "Позиция подстроки '$search' в строке '$input': $position";
}
</pre>
<p>При использовании этой функции надо учитывать, что индексация символов в строке начинается с нуля, поэтому позиция символа "T" будет равна 0. 
Поэтому сравнение <code>$position!=false</code> будет работать некорректно, ведь false и 0 при сравнении и приведении к общему типу будут представлять одно и то же значение. 
Поэтому в данном случае корректно использовать только операцию эквивалентности: <code>$position!==false</code> или <code>$position===false</code>.</p>
<p>Теперь применим функцию на другом примере:</p>
<pre class="brush:php;">
$input = "Мама мыла раму"; 
$search = "мы";
$position = strpos($input, $search); // 9
</pre>
<p>Неожиданно, но результатом функции будет число 9. Хотя мы видим, что истинная позиция подстроки "мы" в исходной строке равна 5.</p>
<p>Все дело в том, что некоторые строковые функции не всегда корректно обрабатывают кириллические символы, и для них лучше использовать другую функцию - 
<span class="b">mb_strpos()</span>:</p>
<pre class="brush:php;">$position = mb_strpos($input, $search); // 5 </pre>
<h3>Функция strrpos()</h3>
<p>Функция strrpos() во многом аналогична функции <code>strpos()</code>, только ищет позицию не первого, а последнего вхождения подстроки в строку:</p>
<pre class="brush:php;">
$input = "This is the end"; 
$search = "is";
$position = strrpos($input, $search); // 5
</pre>
<p>Но опять же данная функция не совсем корректно работает с кириллическими символами, поэтому нам надо использовать ее аналог - <span class="b">mb_strrpos()</span>:</p>
<pre class="brush:php;">$position = mb_strrpos($input, $search);</pre>
<h3>Функция trim()</h3>
<p>Функция <code>trim($str)</code> удаляет из строки начальные и конечные пробелы, а также управляющие символы \n, \r, \t:</p>
<pre class="brush:php;">
$input = "  Мама мыла раму  ";
$input = trim($input);
</pre>
<h3>Изменение регистра</h3>
<p>Для перевода строки в нижний регистр используется функция <span class="b">strtolower</span>:</p>
<pre class="brush:php;">
$input = "The World is Mine";
$input = strtolower($input);
</pre>
<p>Для перевода в нижний регистр строки с кириллическими символами можно использовать функцию <span class="b">mb_strtolower</span>:</p>
<pre class="brush:php;">$input = mb_strtolower($input);</pre>
<p>Для перевода строки в верхний регистр примеяются функции <span class="b">strtoupper()</span>/<span class="b">mb_ strtoupper()</span>, 
которые работают аналогично.</p>
<h3>Функция strlen()</h3>
<p>Функция strlen() возвращает длину строки, то есть количество символов в ней:</p>
<pre class="brush:php;">
$input = "Hello world";
$num = strlen($input);
echo $num;
</pre>
<p>Функция strlen() также некорректно работает с кириллицей, поэтому в этом случае лучше применять функцию <span class="b">mb_strlen()</span>:</p>
<pre class="brush:php;">
$input = "Мама мыла раму";
$num = mb_strlen($input);
echo $num;
</pre>
<h3>Получение подстроки</h3>
<p>Применяя функцию <span class="b">substr($str, $start [, $length])</span>, можно получить из одной строки ее определенную часть. Данная функция обрезает строку $str, 
начиная c символа в позиции $start до конца строки. С помощью дополнительного необязательного параметра $length можно задать количество вырезаемых символов.</p>
<pre class="brush:php;">
$input = "The world is mine!"; 
$subinput1 = substr($input, 2);
$subinput2 = substr($input, 2, 6);
echo $subinput1;
echo "&lt;br&gt;";
echo $subinput2;
</pre>
<p>Так как данная функция некорректно работает с кириллицей, то вместо нее следует применять функцию <span class="b">mb_substr()</span>, 
которая действует аналогично:</p>
<pre class="brush:php;">
$input = "Мама мыла раму"; 
$subinput1 = mb_substr($input, 2);
$subinput2 = mb_substr($input, 2, 6);
</pre>
<h3>Замена подстрок</h3>
<p>Для замены определенной части строки применяется функция <span class="b">str_replace($old, $new, $input)</span>. Эта функция заменяет в строке $input все 
вхождения подстроки $old на подстроку $new с учетом регистра:</p>
<pre class="brush:php;">
$input = "Мама мыла раму"; 
$input = str_replace("мы", "ши", $input);
echo $input;
</pre>
