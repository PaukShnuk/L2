<p><span class="b">Trait</span> позволяет определить некоторое поведение или набор методов без реализации. А реализацию методов - конкретные действия, 
выполняемые методом, предоставляют другие типы (структуры и перечисления), которые применяют данный трейт. В других языках программирования 
есть в некоторой степени похожая функциональность - <span class="b">интерфейсы</span>.</p>
<p>Формальное определение трейта с одним методом:</p>
<pre class="brush:rs;">
trait название_трейта{     // определяем трейт
	fn метод_трейта(&self);     // определение метода трейта
}
</pre>
<p>После ключевого слова <span class="b">trait</span> идет название трейта (обычно начинается с большой буквы). Затем идет блок трейта, который содержит 
набор определений методов без реализации.</p>
<p>Первый и обязательный параметр метода (<span class="b">&amp;self</span>) представляет ссылку на текущий объект. Но как и другие функции может содержать и другие параметры. Единственное он не имеет реализации.</p>
<p>Затем структуры и перечисления могут применить трейт и реализовать его методы. Формальная реализация трейта:</p>
<pre class="brush:rs;">
impl название_трейта for структура/перечисление{	// реализация трейта
	
	fn метод_трейта(&self){ 	// реализация метода трейта
		// действия метода
	}
}
</pre>
<p>После ключевого слова <span class="b">impl</span> указывается имя реализуемого трейта. Далее идет оператор <span class="b">for</span>, после которого указывается 
для какой структуры или перечисления реализуется трейт.</p>
<p>Далее в блоке кода содержится реализация для методов трейта.</p>

<p>Например, определим простейший трейт, который будет применяться для вывода информации об объекте:</p>
<pre class="brush:rs;">
trait Printer{
	fn print(&self);
}
</pre>
<p>Данный трейт называется <code>Printer</code>. Он содержит только один метод - <code>print()</code>. Причем этот метод не содержит тела - конкретной реализации.</p>
<p>Допустим, у нас есть следующая структура Person:</p>
<pre class="brush:rs;">
struct Person { name: String, age: u8}
</pre>
<p>Теперь реализуем трейт Printer для структуры Person:</p>
<pre class="brush:rs;">
impl Printer for Person{

	fn print(&self){
		println!("Person {}; age: {}", self.name, self.age);
	}
}
</pre>
<p>Реализация метода <code>print</code> для структуры Person просто выводит значения его полей name и age на консоль.</p>
<p>Теперь применим эту реализацию:</p>
<pre class="brush:rs;">
fn main(){
	
	let tom = Person{ name: String::from("Tom"), age: 36 };
	tom.print();	// Person Tom; age: 36
}
struct Person { name: String, age: u8}

trait Printer{
	fn print(&self);
}

impl Printer for Person{

	fn print(&self){
		println!("Person {}; age: {}", self.name, self.age);
	}
}
</pre>
<p>Поскольку мы реализовали трейт Printer для структуры Person, то мы можем для объекта структуры Person вызывать метод <code>print()</code></p>
<pre class="brush:rs;">tom.print();	// Person Tom; age: 36</pre>
<p>При этом, как и в случае с методами структур, значение для параметра <code>&amp;self</code> не указывается.</p>
<p>Методы трейтов также могут принимать другие параметры и возвращать некоторый результат. Например, определим в трейте Printer метод, который будет 
принимать числовой параметр. Этот параметр будет указывать, сколько раз надо вывести информацию:</p>
<pre class="brush:rs;">
fn main(){
	
	let tom = Person{ name: String::from("Tom"), age: 36 };
	tom.print_multiple_copies(3);
}

struct Person { name: String, age: u8}

trait Printer{
	fn print_multiple_copies(&self, times: u8);
}

impl Printer for Person{

	fn print_multiple_copies(&self, mut number: u8){
		while number &gt; 0{
			println!("Person {}; age: {}", self.name, self.age);
			number-=1;
		}
	}
}
</pre>
<p>В данном случае метод для вывода информации называется <code>print_multiple_copies()</code>.</p>
<p>Для вывода информации несколько раз применяется цикл while, который срабатывает пока параметр <code>number</code> не станет равным 0.</p>
<p>И поскольку теперь метод принимает больше одного параметра, для второго параметра нам надо передать некоторое числовое значение:</p>
<pre class="brush:rs;">tom.print_multiple_copies(3);</pre>
<p>И в данном случае метод выведет информацию об объекте три раза.</p>
<p>И пример возвращения результата. Добавим метод, который будет выполнять условный предпросмотр - возвращать выводимую информацию:</p>
<pre class="brush:rs;">
fn main(){
	
	let tom = Person{ name: String::from("Tom"), age: 36 };
	let tom_preview = tom.preview();
	println!("{}", tom_preview);
}

struct Person { name: String, age: u8}

trait Printer{
	fn preview(&self) -&gt; String;
}

impl Printer for Person{

	fn preview(&self) -&gt; String{
		format!("[Предпросмотр] Person {}; age: {}", self.name, self.age)
	}
}
</pre>
<p>Здесь в трейте <code>Printer</code> определен метод <code>preview()</code>, который должен возвращать объект типа <code>String</code>.</p>
<p>В реализации метода <code>preview()</code> для структуры Person применяется макрос <span class="b">format!</span>, который похож на макрос <span class="b">println!</span>. 
Он тоже принимает набор значений и позволяет вложить их в строку. Но при этом <span class="b">format!</span> возвращает значение типа <code>String</code>, 
которое содержит полученную строку.</p>
<p>В примерах выше трейт содержал по одному методу. Однако если трейт содержит несколько методов, то при реализации трейта для структуры или перечисления 
необходимо реализовать все методы трейта.</p>
<h3>Реализация по умолчанию</h3>
<p>Трейт может содержать реализацию по умолчанию. В этом случае структура может не реализовать даный метод и воспользоваться реализацией по умолчанию:</p>
<pre class="brush:rs;">
fn main(){
	
	let tom = Person{ name: String::from("Tom"), age: 36 };
	tom.print();
	
	let hello = Message{ text: String::from("Hello Rust!")};
	hello.print();
}

trait Printer{
	fn print(&amp;self){
		println!("Вывод данных на консоль...");
	}
}

struct Person { name: String, age: u8}
struct Message { text: String}

impl Printer for Person{
	// переопределяем реализацию метода
	fn print(&amp;self){
		println!("Person {}; age: {}", self.name, self.age);
	}
}
impl Printer for Message{
	// используем реализацию метода по умолчанию
}
</pre>
<p>Консольный вывод:</p>
<div class="console">
<pre class="consoletext">
Person Tom; age: 36
Вывод данных на консоль...
</pre>
</div>
<p>Здесь трейт Printer определяет простейшую реализацию по умолчанию для метода <code>print()</code>:</p>
<pre class="brush:rs;">
trait Printer{
	fn print(&amp;self){
		println!("Вывод данных на консоль...");
	}
}
</pre>
<p>Далее данный трейт применяют две структуры - Person и Message. Но если для структуры Person определена своя реализация метода <code>print()</code>, то 
для структуры Message просто берется реализация по умолчанию:</p>
<pre class="brush:rs;">
impl Printer for Message{
	// используем реализацию метода по умолчанию
}
</pre>
<p>Однако если бы трейт Printer не содержал бы реализацию по умолчанию для метода <code>print()</code>, тогда ее обязательно надо было бы определять для всех применяющих даный трейт структур.</p>
<h3>Обращение к методам трейта внутри трейта</h3>
<p>Одни методы трейта могут обращаться к другим методам трейта:</p>
<pre class="brush:rs;">
fn main(){

	let tom = Person{ name: String::from("Tom"), age: 36 };
	tom.print();
}

struct Person { name: String, age: u8}

trait Printer{
	fn preview(&self) -&gt; String;
	fn print(&amp;self){
		println!("{}", self.preview());
	}
}

impl Printer for Person{

	fn preview(&amp;self) -&gt; String{
		format!("Person {}; age: {}", self.name, self.age)
	}
}
</pre>
<p>Здесь трейт <code>Printer</code> содержит два метода. Первый метод - <code>preview()</code> не имеет реалиации:</p>
<pre class="brush:rs;">fn preview(&self) -&gt; String;</pre>
<p>Мы не знаем, как для конкретных структур будет релизован этот метод. Мы только знаем, что он возвращает объект <code>String</code> - некоторую строку, которую мы можем использовать.</p>
<p>Второй метод - <code>print()</code> имеет реализацию по умолчанию:</p>
<pre class="brush:rs;">
fn print(&amp;self){
	println!("{}", self.preview());
}
</pre>
<p>Здесь мы используем первый метод трейта, обращаясь к нему через <code>self.preview()</code>. <code>self</code> внутри трейта указывает на текущий объект трейта, 
поэтому через <code>self</code> мы можем обращаться к любым методам трейта внутри этого трейта. Таким образом, на консоль будет выводится результат 
метода <code>preview()</code>, реализация которого будет определяться для каждой структуры отдельно.</p>
